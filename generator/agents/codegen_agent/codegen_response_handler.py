def parse_llm_response(response: Union[str, Dict[str, Any]], lang: str = "python") -> Dict[str, str]:
    """
    Parses the LLM response, handling both multi-file JSON and single-file code.

    Multi-file mode expects:
        {
          "files": {
            "main.py": "<code>",
            "util.py": "<code>"
          }
        }

    Behavior:
    - For multi-file JSON:
        - Each file is cleaned and syntax-validated.
        - Valid files are returned as-is.
        - Invalid files are summarized into ERROR_FILENAME.
    - For non-JSON:
        - Treat as a single file; validate.
        - Return DEFAULT_FILENAME or ERROR_FILENAME accordingly.
    
    Args:
        response: Either a string (legacy behavior) or a dict (OpenAI API response format)
        lang: Target programming language for syntax validation
    
    Returns:
        Dictionary mapping filenames to code content
    """
    # Handle dict response from OpenAI API
    if isinstance(response, dict):
        # Extract content from OpenAI chat completion response
        raw = ''
        try:
            # Try OpenAI chat completion format first
            if 'choices' in response and response['choices']:
                raw = response['choices'][0].get('message', {}).get('content', '')
        except (KeyError, TypeError):
            # KeyError: unexpected dict structure
            # TypeError: choices is not subscriptable or message is not a dict
            pass
        
        # Fallback: try to get 'content' or 'text' directly if extraction failed
        if not raw:
            raw = response.get('content', response.get('text', ''))
        
        logger.debug("Extracted content from dict response, length: %d", len(raw) if raw else 0)
    else:
        raw = response
    
    raw = raw.strip() if raw else ''
    # For robustness, strip outer code fences before JSON parsing attempt.
    cleaned_for_json = _clean_code_block(raw)

    # --- 1. Try multi-file JSON format ---
    try:
        payload = json.loads(cleaned_for_json)
        if isinstance(payload, dict) and isinstance(payload.get("files"), dict):
            files_obj = payload["files"]
            code_files: Dict[str, str] = {}
            errors: Dict[str, str] = {}

            for filename, content in files_obj.items():
                if not isinstance(content, str):
                    errors[filename] = "Content is not a string."
                    continue

                cleaned = _clean_code_block(content)
                is_valid, msg = _validate_syntax(cleaned, lang, filename)

                if is_valid:
                    code_files[filename] = cleaned
                else:
                    logger.warning(
                        "Syntax validation failed for '%s' (%s). Details: %s",
                        filename,
                        lang,
                        msg,
                    )
                    errors[filename] = msg

            # If any file failed, emit an aggregated error.txt
            if errors:
                lines = [
                    "One or more files failed syntax validation.",
                    "",
                ]
                for fname, emsg in errors.items():
                    lines.append(f"[{fname}]")
                    lines.append(str(emsg))
                    lines.append("")
                code_files[ERROR_FILENAME] = "\n".join(lines).rstrip()

            if code_files:
                log_action(
                    "Parsed Multi-File Response",
                    {
                        "files": [f for f in code_files.keys() if f != ERROR_FILENAME],
                        "language": lang,
                        "had_errors": bool(errors),
                    },
                )
                return code_files
            # If nothing usable, fall through to single-file handling.

    except json.JSONDecodeError:
        logger.info("LLM response is not valid JSON; using single-file fallback.")
    except Exception as e:
        logger.error("Unexpected error while parsing JSON LLM response: %s", e)

    # --- 2. Single-file fallback ---
    cleaned_code = _clean_code_block(raw)
    is_valid, msg = _validate_syntax(cleaned_code, lang, DEFAULT_FILENAME)

    if is_valid:
        log_action(
            "Parsed Single-File Response",
            {"file": DEFAULT_FILENAME, "language": lang},
        )
        return {DEFAULT_FILENAME: cleaned_code}

    logger.error(
        "Syntax validation failed for single-file response (%s). Details: %s",
        lang,
        msg,
    )
    error_content = (
        f"Generated code failed syntax validation for language '{lang}'.\n\n"
        f"Validation Tool Output:\n{msg}"
    )
    return {ERROR_FILENAME: error_content}


# ==============================================================================
# --- Helpers: Cleaning & Syntax Validation ---
# ==============================================================================


def _clean_code_block(code_content: str) -> str:
    """
    Removes markdown-style code fences and any conversational preamble/postamble.
    
    Handles patterns like:
        Here's the code:
        ```python
        code
        ```
        Let me know if you need changes.
    """
    text = code_content.strip()
    
    # Find ALL code blocks and extract the largest one (likely the main code)
    # Pattern: ```language\ncode\n```
    code_block_pattern = r'```(?:python|py)?\s*\n(.*?)```'
    matches = re.findall(code_block_pattern, text, flags=re.DOTALL | re.IGNORECASE)
    
    if matches:
        # Return the largest code block (most likely to be the actual code)
        return max(matches, key=len).strip()
    
    # Try without language specifier
    generic_pattern = r'```\s*\n(.*?)```'
    matches = re.findall(generic_pattern, text, flags=re.DOTALL)
    if matches:
        return max(matches, key=len).strip()
    
    # No code fences found - try to strip common preamble patterns
    # Remove lines that look like conversational text before actual code
    lines = text.split('\n')
    code_start_idx = 0
    
    for i, line in enumerate(lines):
        stripped = line.strip().lower()
        # Skip lines that look like conversational preamble
        if stripped.startswith(('here', 'this', 'the following', 'below', 'i ', "i'")):
            code_start_idx = i + 1
            continue
        # Stop at first line that looks like code
        if stripped.startswith(('import ', 'from ', 'def ', 'class ', '#', '"""', "'''")):
            break
        if '=' in stripped and not stripped.startswith(('here', 'this')):
            break
    
    return '\n'.join(lines[code_start_idx:]).strip()


def _validate_syntax(code: str, lang: str, filename: str) -> Tuple[bool, str]:
    """
    Validates code syntax using language-appropriate mechanisms.

    Returns:
        (is_valid, message)
        - is_valid: True if considered syntactically OK.
        - message: empty or diagnostic text.
    """
    lang_l = (lang or "").lower()

    if not code.strip():
        logger.warning("Empty code block for %s; treating as valid.", filename)
        return True, "Empty code treated as valid."

    # --- Python ---
    if lang_l in ("python", "py"):
        try:
            compile(code, filename, "exec")
            return True, ""
        except SyntaxError as e:
            msg = f"{e.__class__.__name__}: {e}"
            logger.error(
                "Error during python syntax validation for %s: %s",
                filename,
                msg,
            )
            return False, msg

    # --- JavaScript / TypeScript ---
    if lang_l in ("javascript", "js", "typescript", "ts"):
        if not _is_tool_available("node"):
            logger.info("Node.js not available; skipping JS/TS syntax validation.")
            return True, "node not available; skipped."

        try:
            with tempfile.NamedTemporaryFile("w", suffix=".js", delete=False) as tmp:
                tmp.write(code)
                tmp_path = tmp.name
            # node --check <file>
            proc = subprocess.run(
                ["node", "--check", tmp_path],
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                text=True,
            )
            os.unlink(tmp_path)
            if proc.returncode == 0:
                return True, ""
            return False, proc.stderr.strip() or proc.stdout.strip()
        except Exception as e:
            logger.error(
                "Error during JS/TS syntax validation for %s: %s",
                filename,
                e,
            )
            return False, str(e)

    # --- Java ---
    if lang_l == "java":
        if not _is_tool_available("javac"):
            logger.info("javac not available; skipping Java syntax validation.")
            return True, "javac not available; skipped."

        try:
            with tempfile.TemporaryDirectory() as tmpdir:
                src_path = os.path.join(tmpdir, filename or "Main.java")
                with open(src_path, "w", encoding="utf-8") as f:
                    f.write(code)
                proc = subprocess.run(
                    ["javac", src_path],
                    stdout=subprocess.PIPE,
                    stderr=subprocess.PIPE,
                    text=True,
                )
                if proc.returncode == 0:
                    return True, ""
                return False, proc.stderr.strip() or proc.stdout.strip()
        except Exception as e:
            logger.error(
                "Error during Java syntax validation for %s: %s",
                filename,
                e,
            )
            return False, str(e)

    # --- Go ---
    if lang_l == "go":
        if not _is_tool_available("go"):
            logger.info("Go toolchain not available; skipping Go syntax validation.")
            return True, "go not available; skipped."

        try:
            with tempfile.TemporaryDirectory() as tmpdir:
                src_path = os.path.join(tmpdir, filename or "main.go")
                with open(src_path, "w", encoding="utf-8") as f:
                    f.write(code)
                proc = subprocess.run(
                    ["go", "build", src_path],
                    stdout=subprocess.PIPE,
                    stderr=subprocess.PIPE,
                    text=True,
                )
                if proc.returncode == 0:
                    return True, ""
                return False, proc.stderr.strip() or proc.stdout.strip()
        except Exception as e:
            logger.error(
                "Error during Go syntax validation for %s: %s",
                filename,
                e,
            )
            return False, str(e)

    # --- Fallback for unsupported languages ---
    logger.info(
        "No specific syntax validator for language '%s'; treating '%s' as valid.",
        lang,
        filename,
    )
    return True, "No validator for this language; skipped."


