def _validate_yaml_content(code: str, filename: str) -> Tuple[bool, str]:
    """
    Validates YAML content to ensure it's actual YAML, not markdown wrapping.
    
    This function is critical for catching cases where LLM returns markdown-wrapped
    YAML content (e.g., ```yaml ... ```) that wasn't properly extracted by _clean_code_block.
    
    Args:
        code: The content to validate as YAML
        filename: The filename (for error messages)
        
    Returns:
        Tuple of (is_valid, message):
            - is_valid: True if content is valid YAML
            - message: Empty if valid, error description otherwise
    
    Examples:
        >>> _validate_yaml_content("apiVersion: v2\\nname: my-chart", "Chart.yaml")
        (True, "")
        >>> _validate_yaml_content("```yaml\\napiVersion: v2\\n```", "Chart.yaml")
        (False, "YAML file contains markdown code fences...")
    """
    if not code.strip():
        return False, "Empty YAML content"
    
    # Skip strict YAML validation for Helm/Jinja2 template files
    # Templates contain {{ }}, {%  %} syntax which is not valid YAML
    TEMPLATE_MARKERS = ('{{', '}}', '{%', '%}')
    if any(marker in code for marker in TEMPLATE_MARKERS) or 'templates/' in filename:
        logger.debug("Skipping strict YAML validation for template file %s", filename)
        return True, "Skipped validation for template file"

    # Check for markdown code fences - these indicate improper extraction
    if code.strip().startswith("```") or "```yaml" in code or "```yml" in code:
        return False, (
            f"YAML file '{filename}' contains markdown code fences. "
            "This indicates the content was not properly extracted from the LLM response. "
            "The file should contain pure YAML, not markdown-wrapped YAML."
        )
    
    # Check for other markdown indicators (badges, mermaid diagrams)
    markdown_indicators = ["![", "[![", "```mermaid", "##", "###"]
    for indicator in markdown_indicators:
        if indicator in code[:200]:  # Check first 200 chars for markdown
            return False, (
                f"YAML file '{filename}' appears to contain markdown content (found '{indicator}'). "
                "Expected pure YAML format."
            )
    
    # Validate actual YAML syntax
    if not HAS_YAML:
        logger.warning("PyYAML not available; skipping YAML syntax validation for %s", filename)
        return True, "PyYAML not available; skipped."
    
    try:
        # Parse YAML to verify it's valid
        yaml.safe_load(code)
        return True, ""
    except yaml.YAMLError as e:
        error_msg = f"Invalid YAML syntax in {filename}: {e}"
        logger.error(error_msg)
        return False, error_msg
    except Exception as e:
        error_msg = f"Error validating YAML in {filename}: {e}"
        logger.error(error_msg)
        return False, error_msg


def _validate_python_syntax(content: str, filename: str = "") -> str:
    """Validate Python syntax and attempt to fix common LLM artifacts.
    
    Common LLM artifacts that cause bare NameError at module level:
    - Comment text leaking as bare identifiers (e.g., 'directly' from '# Run this directly')
    - Prose descriptions mixed into code
    
    This function detects bare name expressions at module level (e.g., standalone
    identifiers that will cause NameError at runtime) and comments them out.
    
    Args:
        content: Python source code to validate
        filename: Optional filename for logging
        
    Returns:
        Validated (and possibly fixed) Python source code
    """
    try:
        tree = ast.parse(content)
        
        # Look for bare name expressions at module level
        # These are Expr nodes containing a Name node (single identifier with no assignment)
        bare_names = []
        for node in tree.body:
            if isinstance(node, ast.Expr) and isinstance(node.value, ast.Name):
                # This is a bare identifier - e.g., just "directly" on a line by itself
                bare_names.append((node.lineno, node.value.id))
        
        if bare_names:
            logger.warning(
                f"Found {len(bare_names)} bare identifier(s) in {filename}: {[name for _, name in bare_names]}"
            )
            # Detect line ending style
            line_ending = "\r\n" if "\r\n" in content else "\n"
            lines = content.splitlines()
            # Comment out each bare identifier line (in reverse order to preserve line numbers)
            for lineno, identifier in reversed(bare_names):
                if 1 <= lineno <= len(lines):
                    logger.info(
                        f"Commenting out bare identifier '{identifier}' at line {lineno}",
                        extra={"source_file": filename}
                    )
                    original_line = lines[lineno - 1]
                    # Only add comment prefix if the line isn't already a comment
                    if not original_line.strip().startswith("#"):
                        lines[lineno - 1] = f"# {original_line}  # AUTO-FIXED: bare identifier"
                    else:
                        # Already a comment, just add the marker
                        lines[lineno - 1] = f"{original_line}  # AUTO-FIXED: bare identifier"
            
            fixed_content = line_ending.join(lines)
            # Verify the fix worked
            try:
                ast.parse(fixed_content)
                return fixed_content
            except SyntaxError:
                logger.error(f"Failed to parse fixed content for {filename}")
                return content  # Return original if fix broke something
        
        return content  # Valid Python with no bare identifiers
        
    except SyntaxError as e:
        logger.warning(
            f"Syntax error in generated Python code ({filename}): {e}",
            extra={"source_file": filename, "syntax_error_line": e.lineno, "syntax_error_detail": str(e)}
        )
        return content  # Return original - let the normal validation handle syntax errors


def _validate_syntax(code: str, lang: str, filename: str) -> Tuple[bool, str]:
    """
    Validates code syntax using language-appropriate mechanisms.
    
    Industry Standards:
        - Input validation and type checking
        - File type detection to skip non-code files
        - Language inference from filename
        - Comprehensive error messages
        - Graceful degradation when tools unavailable
    
    Args:
        code: The code content to validate
        lang: The programming language
        filename: The filename (used for language detection and context)

    Returns:
        Tuple of (is_valid, message):
            - is_valid: True if syntactically valid or validation skipped
            - message: Empty if valid, error description otherwise
            
    Raises:
        TypeError: If inputs are not of expected types
        
    Examples:
        >>> _validate_syntax("def hello(): pass", "python", "main.py")
        (True, "")
        >>> _validate_syntax("# Documentation", "python", "README.md")
        (True, "Skipped validation for markdown file")
    """
    # Input validation - industry standard defensive programming
    if not isinstance(code, str):
        raise TypeError(f"code must be a string, got {type(code).__name__}")
    
    if not isinstance(lang, str):
        raise TypeError(f"lang must be a string, got {type(lang).__name__}")
    
    if not isinstance(filename, str):
        raise TypeError(f"filename must be a string, got {type(filename).__name__}")
    
    # SPECIAL CASE: Validate YAML files BEFORE skipping validation
    # This is critical to catch markdown-wrapped YAML content that wasn't properly extracted
    ext = os.path.splitext(os.path.basename(filename))[1].lstrip(".").lower()
    if ext in ("yaml", "yml") or lang.lower() in ("yaml", "yml"):
        logger.debug("Performing YAML validation for %s", filename)
        return _validate_yaml_content(code, filename)
    
    # Check if this file should skip validation (e.g., README.md, config files)
    try:
        if _should_skip_syntax_validation(filename):
            detected_type = _infer_language_from_filename(filename, default_lang="unknown")
            logger.debug(
                "Skipping syntax validation for %s (detected as %s file)",
                filename,
                detected_type
            )
            return True, f"Skipped validation for {detected_type} file"
    except (TypeError, ValueError):
        # Re-raise validation errors
        raise
    except Exception as e:
        # Log unexpected errors but continue with validation for safety
        logger.warning(
            "Error checking if validation should be skipped for '%s': %s. Proceeding with validation.",
            filename,
            e
        )
    
    # Infer language from filename - always prefer file extension for known code types
    try:
        # Get file extension to check if file has a known extension
        ext = os.path.splitext(os.path.basename(filename))[1].lstrip(".").lower()
        
        inferred_lang = _infer_language_from_filename(filename, default_lang="")
        
        # Use inferred language if:
        # 1. Original lang is empty or generic ("", "text", "plain"), OR
        # 2. File has a recognized extension AND inferred lang differs from provided lang
        # This ensures .ts files are validated as TypeScript even when lang="python"
        if not lang or lang.lower() in ("", "text", "plain"):
            # No valid language provided, use inferred
            if inferred_lang:
                lang = inferred_lang
                logger.debug(
                    "Inferred language '%s' from filename '%s'",
                    lang,
                    filename
                )
        elif ext in _EXTENSION_TO_LANGUAGE and inferred_lang and inferred_lang != lang.lower():
            # File has recognized extension but differs from provided lang - use extension
            logger.info(
                "Overriding provided lang '%s' with inferred '%s' from filename '%s'",
                lang,
                inferred_lang,
                filename
            )
            lang = inferred_lang
    except Exception as e:
        logger.warning(
            "Could not infer language from filename '%s': %s. Using provided lang '%s'.",
            filename,
            e,
            lang
        )
    
    lang_l = (lang or "").lower()

    # __init__.py files are allowed to be empty — they're Python package markers
    if filename.endswith("__init__.py") and not code.strip():
        return True, ""

    # Validate that code is not empty
    if not code.strip():
        logger.warning("Empty code block for %s; treating as error.", filename)
        return False, (
            "Empty code block received - no valid code generated. "
            "This may indicate the LLM returned explanatory text instead of code. "
            "Try providing more specific requirements or examples."
        )

    # --- Python ---
    if lang_l in ("python", "py"):
        # Normalize content before syntax validation to handle escaped chars
        normalized = _normalize_file_content(code)
        try:
            compile(normalized, filename, "exec")
            return True, ""
        except SyntaxError as e:
            msg = f"{e.__class__.__name__}: {e}"
            logger.error(
                "Error during python syntax validation for %s: %s",
                filename,
                msg,
            )
            return False, msg

    # --- JavaScript / TypeScript ---
    if lang_l in ("javascript", "js", "typescript", "ts"):
        if not _is_tool_available("node"):
            logger.info("Node.js not available; skipping JS/TS syntax validation.")
            return True, "node not available; skipped."

        try:
            with tempfile.NamedTemporaryFile("w", suffix=".js", delete=False) as tmp:
                tmp.write(code)
                tmp_path = tmp.name
            # node --check <file>
            proc = subprocess.run(
                ["node", "--check", tmp_path],
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                text=True,
            )
            os.unlink(tmp_path)
            if proc.returncode == 0:
                return True, ""
            return False, proc.stderr.strip() or proc.stdout.strip()
        except Exception as e:
            logger.error(
                "Error during JS/TS syntax validation for %s: %s",
                filename,
                e,
            )
            return False, str(e)

    # --- Java ---
    if lang_l == "java":
        if not _is_tool_available("javac"):
            logger.info("javac not available; skipping Java syntax validation.")
            return True, "javac not available; skipped."

        try:
            with tempfile.TemporaryDirectory() as tmpdir:
                src_path = os.path.join(tmpdir, filename or "Main.java")
                with open(src_path, "w", encoding="utf-8") as f:
                    f.write(code)
                proc = subprocess.run(
                    ["javac", src_path],
                    stdout=subprocess.PIPE,
                    stderr=subprocess.PIPE,
                    text=True,
                )
                if proc.returncode == 0:
                    return True, ""
                return False, proc.stderr.strip() or proc.stdout.strip()
        except Exception as e:
            logger.error(
                "Error during Java syntax validation for %s: %s",
                filename,
                e,
            )
            return False, str(e)

    # --- Go ---
    if lang_l == "go":
        if not _is_tool_available("go"):
            logger.info("Go toolchain not available; skipping Go syntax validation.")
            return True, "go not available; skipped."

        try:
            with tempfile.TemporaryDirectory() as tmpdir:
                src_path = os.path.join(tmpdir, filename or "main.go")
                with open(src_path, "w", encoding="utf-8") as f:
                    f.write(code)
                proc = subprocess.run(
                    ["go", "build", src_path],
                    stdout=subprocess.PIPE,
                    stderr=subprocess.PIPE,
                    text=True,
                )
                if proc.returncode == 0:
                    return True, ""
                return False, proc.stderr.strip() or proc.stdout.strip()
        except Exception as e:
            logger.error(
                "Error during Go syntax validation for %s: %s",
                filename,
                e,
            )
            return False, str(e)

    # --- Fallback for unsupported languages ---
    logger.info(
        "No specific syntax validator for language '%s'; treating '%s' as valid.",
        lang,
        filename,
    )
    return True, "No validator for this language; skipped."


def validate_and_repair_syntax(code: str, language: str, filename: str) -> Dict[str, Any]:
    """
    Validate syntax and attempt auto-repair if validation fails.
    
    This function implements a two-phase approach:
    1. First, try validating the original code
    2. If validation fails, attempt auto-repair and validate again
    
    Args:
        code: The source code to validate
        language: Programming language (e.g., "python", "javascript")
        filename: Name of the file (for context in error messages)
    
    Returns:
        Dictionary with keys:
            - 'valid': bool - Whether the code is syntactically valid
            - 'code': str - Original or repaired code
            - 'error': str or None - Error message if validation failed
            - 'repairs_applied': List[str] - List of repairs that were applied
            - 'auto_repaired': bool - Whether auto-repair was needed and successful
    """
    # Normalize content first to handle escaped characters from LLM
    code = _normalize_file_content(code)
    
    # For Python files, apply bare identifier fix before validation
    # Skip for non-Python files (e.g. README.md) to avoid spurious syntax warnings
    if language.lower() in ("python", "py") and not _should_skip_syntax_validation(filename):
        fixed_code = _validate_python_syntax(code, filename)
        if fixed_code != code:
            logger.info(f"Fixed bare identifiers in {filename} before validation")
            code = fixed_code
    
    # Try original code first
    is_valid, error_msg = _validate_syntax(code, language, filename)
    
    if is_valid:
        return {
            'valid': True,
            'code': code,
            'error': None,
            'repairs_applied': [],
            'auto_repaired': False
        }
    
    # Validation failed - try auto-repair
    logger.info(f"Syntax validation failed for {filename}. Attempting auto-repair...")
    
    repair_result = SyntaxAutoRepair.auto_repair(code, language)
    
    if repair_result['was_modified']:
        logger.info(f"Applied {len(repair_result['fixes_applied'])} auto-repairs: {repair_result['fixes_applied']}")
        
        # Validate repaired code
        is_valid_repaired, error_msg_repaired = _validate_syntax(
            repair_result['repaired_code'], 
            language, 
            filename
        )
        
        if is_valid_repaired:
            logger.info(f"✓ Auto-repair successful for {filename}")
            return {
                'valid': True,
                'code': repair_result['repaired_code'],
                'error': None,
                'repairs_applied': repair_result['fixes_applied'],
                'auto_repaired': True
            }
        else:
            logger.warning(f"Auto-repair failed to fix {filename}: {error_msg_repaired}")
    else:
        logger.info("No repairs were applicable for the detected syntax errors")
    
    # Return original validation error
    return {
        'valid': False,
        'code': code,
        'error': error_msg,
        'repairs_applied': [],
        'auto_repaired': False
    }


