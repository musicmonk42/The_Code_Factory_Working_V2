# Suffixes that indicate an imported name is a module-level variable rather than
# a callable.  Names ending with these strings get a simple object assignment
# instead of a stub function definition.
_VARIABLE_SUFFIXES: Tuple[str, ...] = (
    "_router", "_app", "_engine", "_client", "_pool",
    "_db", "_session", "_manager", "_factory", "_registry",
    "_config", "_settings", "_broker", "_bus", "_cache",
)

# Verb prefixes that indicate a callable (function/method) regardless of suffix.
# e.g. ``get_db`` looks like a variable due to ``_db`` suffix, but the ``get_``
# prefix signals it is a getter function.
_VERB_PREFIXES: Tuple[str, ...] = (
    "get_", "set_", "create_", "delete_", "update_", "fetch_",
    "handle_", "process_", "validate_", "check_", "build_", "make_",
    "run_", "load_", "save_", "send_", "receive_", "parse_", "format_",
    "compute_", "generate_", "initialize_", "init_", "setup_",
)

# Known router variable names that should be stubbed as ``APIRouter()`` instances
# rather than plain ``None`` assignments.
_ROUTER_VARIABLE_PATTERNS: frozenset = frozenset({
    "router", "api_router", "app_router", "main_router",
})

# Class name suffixes that strongly suggest a Pydantic model.  Used by
# ``ensure_local_module_stubs`` to decide whether a stub class should inherit
# from ``pydantic.BaseModel`` rather than being a bare class.
_PYDANTIC_CLASS_SUFFIXES: tuple = (
    "Response", "Request", "Schema", "Create", "Update",
    "Read", "InDB", "Base", "Input", "Output", "Model",
    "Params", "Filter", "Detail", "Summary", "List",
)

# Irregular English plurals used when deriving SQLAlchemy ``__tablename__``
# from a class name.  Stored at module scope to avoid re-constructing the
# dictionary on every ``_pluralize_tablename`` call.
_STUB_TABLENAME_IRREGULARS: Dict[str, str] = {
    "person": "people",   "man": "men",          "woman": "women",
    "child": "children",  "tooth": "teeth",       "foot": "feet",
    "mouse": "mice",      "goose": "geese",        "ox": "oxen",
    "leaf": "leaves",     "calf": "calves",        "half": "halves",
    "knife": "knives",    "life": "lives",          "wife": "wives",
    "wolf": "wolves",     "loaf": "loaves",         "shelf": "shelves",
    "index": "indices",   "matrix": "matrices",    "vertex": "vertices",
    "analysis": "analyses", "basis": "bases",      "crisis": "crises",
    "status": "statuses", "alias": "aliases",
}


def _pluralize_tablename(class_name: str) -> str:
    """Return a PostgreSQL-compatible plural of *class_name* for ``__tablename__``.

    Rules applied (in priority order):

    1. Exact-match irregular words (case-insensitive lookup against
       :data:`_STUB_TABLENAME_IRREGULARS`).
    2. Words ending in *s*, *x*, *z*, *ch*, *sh* → append **es**.
    3. Words ending in a consonant + *y* → replace *y* with **ies**.
    4. Words ending in *fe* → replace *fe* with **ves**.
    5. Words ending in *f* (not *ff*) → replace *f* with **ves**.
    6. Default: append **s**.

    The result is always lowercase.

    Args:
        class_name: Python class name (e.g. ``"Product"``, ``"Category"``).

    Returns:
        Lowercase plural string suitable for a database table name.

    Examples:
        >>> _pluralize_tablename("Product")
        'products'
        >>> _pluralize_tablename("Category")
        'categories'
        >>> _pluralize_tablename("Status")
        'statuses'
        >>> _pluralize_tablename("Index")
        'indices'
        >>> _pluralize_tablename("Leaf")
        'leaves'
    """
    lower = class_name.lower()
    if lower in _STUB_TABLENAME_IRREGULARS:
        return _STUB_TABLENAME_IRREGULARS[lower]
    if lower.endswith(("s", "x", "z", "ch", "sh")):
        return lower + "es"
    if lower.endswith("y") and len(lower) > 1 and lower[-2] not in "aeiou":
        return lower[:-1] + "ies"
    if lower.endswith("fe"):
        return lower[:-2] + "ves"
    if lower.endswith("f") and not lower.endswith("ff"):
        return lower[:-1] + "ves"
    return lower + "s"


def _is_router_variable(name: str) -> bool:
    """Return ``True`` when *name* is a FastAPI router variable.

    Matches exact names in :data:`_ROUTER_VARIABLE_PATTERNS` (e.g. ``router``,
    ``api_router``) as well as any name ending with ``_router`` (e.g.
    ``products_router``, ``orders_router``).

    Names that start with common verb prefixes (e.g. ``create_router``,
    ``get_router``) are excluded because they are factory or getter functions,
    not router instances.

    Args:
        name: The symbol name to check.

    Returns:
        ``True`` if the name should be stubbed as an ``APIRouter()`` instance.

    Examples:
        >>> _is_router_variable("router")
        True
        >>> _is_router_variable("products_router")
        True
        >>> _is_router_variable("create_router")
        False
        >>> _is_router_variable("db_engine")
        False
    """
    lower = name.lower()
    if any(lower.startswith(prefix) for prefix in _VERB_PREFIXES):
        return False
    return lower in _ROUTER_VARIABLE_PATTERNS or lower.endswith("_router")


def _is_likely_variable(name: str) -> bool:
    """Return ``True`` when *name* looks like a module-level variable, not a callable.

    Uses a suffix-based heuristic: well-known framework object names end with
    suffixes such as ``_router``, ``_engine``, ``_client``, etc.  When an
    imported name matches one of these patterns it should be stubbed as a simple
    variable assignment rather than a callable stub function.

    Names that start with common verb prefixes (``get_``, ``set_``, ``create_``,
    etc.) are always treated as callables even when their suffix would otherwise
    match a variable pattern.

    Args:
        name: The imported symbol name to classify.

    Returns:
        ``True`` if the name should be treated as a variable; ``False`` if it
        should be treated as a callable (function or class).

    Examples:
        >>> _is_likely_variable("api_router")
        True
        >>> _is_likely_variable("db_engine")
        True
        >>> _is_likely_variable("get_db")
        False
        >>> _is_likely_variable("get_current_user")
        False
        >>> _is_likely_variable("User")
        False
    """
    name_lower = name.lower()
    # Verb-prefixed names are always callables, never variables.
    if any(name_lower.startswith(prefix) for prefix in _VERB_PREFIXES):
        return False
    return any(name_lower.endswith(suffix) for suffix in _VARIABLE_SUFFIXES)


def fix_response_model_type_mismatches(code_files: Dict[str, str]) -> Dict[str, str]:
    """Scan router files and fix ``response_model=X`` usages where X is a SQLAlchemy ORM model.

    FastAPI requires Pydantic ``BaseModel`` subclasses for ``response_model=``.  When the LLM
    imports from ``app.models.*`` (SQLAlchemy) instead of ``app.schemas.*`` (Pydantic) and uses
    those classes as ``response_model`` arguments, FastAPI raises a ``FastAPIError`` at import
    time because SQLAlchemy ORM models are not valid Pydantic field types.

    Algorithm
    ~~~~~~~~~
    1. For each ``app/routers/*.py`` file, collect imports from ``app.models.*``.
    2. Find decorator arguments like ``response_model=X`` where ``X`` was imported from a models
       module.
    3. If a matching schema exists in ``app/schemas/`` with the same class name, rewrite the
       import to use the schema module instead.
    4. If no schema equivalent exists, replace ``response_model=X`` with ``response_model=None``
       and add a TODO comment to prevent the crash.

    Args:
        code_files: Mapping of relative file path → source code.

    Returns:
        The (potentially modified) *code_files* mapping.
    """
    _router_path_re = re.compile(r'^app/(?:routers|routes)/(?!__init__)[^/]+\.py$')
    # Match "from app.models.<module> import <names>" (supports multiline parenthesised forms)
    _model_import_re = re.compile(
        r'from\s+(app\.models\.[\w]+)\s+import\s+'
        r'(?:\(([^)]+)\)|([^\n]+))',
        re.MULTILINE,
    )
    # Match response_model=SomeName inside a decorator
    _response_model_re = re.compile(r'\bresponse_model\s*=\s*([A-Za-z_]\w*)')

    # Build a map of schema classes available in app/schemas/
    schema_classes: Dict[str, str] = {}  # class_name -> schema module path (dotted)
    for path, content in code_files.items():
        if not path.startswith("app/schemas/") or not path.endswith(".py"):
            continue
        module_dotted = path.replace("/", ".").removesuffix(".py")
        try:
            tree = ast.parse(content)
            for node in ast.walk(tree):
                if isinstance(node, ast.ClassDef):
                    schema_classes[node.name] = module_dotted
        except SyntaxError:
            pass

    updated = dict(code_files)

    for path, content in code_files.items():
        if not _router_path_re.match(path):
            continue

        # Collect which names were imported from app.models.*
        model_imports: Dict[str, str] = {}  # symbol_name -> original dotted module
        for m in _model_import_re.finditer(content):
            module_dotted = m.group(1)
            names_raw = m.group(2) or m.group(3) or ""
            for part in names_raw.split(","):
                name = part.split(" as ")[0].strip()
                if name and name.isidentifier():
                    model_imports[name] = module_dotted

        if not model_imports:
            continue

        # Find all response_model= usages that reference a models-imported name
        new_content = content
        changed = False
        for rm in _response_model_re.finditer(content):
            sym = rm.group(1)
            if sym not in model_imports:
                continue
            # Determine whether a schema equivalent exists
            if sym in schema_classes:
                schema_module = schema_classes[sym]
                old_model_module = model_imports[sym]
                # Rewrite the import line: replace models import with schema import
                old_import_pattern = re.compile(
                    r'from\s+' + re.escape(old_model_module) + r'\s+import\s+'
                    r'(?:\([^)]*\)|[^\n]+)',
                    re.MULTILINE,
                )
                def _rewrite_import(m_imp: re.Match, _sym: str = sym, _schema_mod: str = schema_module) -> str:
                    original = m_imp.group(0)
                    # Replace only the module path, keep the symbols
                    return original.replace(old_model_module, _schema_mod)
                new_content = old_import_pattern.sub(_rewrite_import, new_content, count=1)
                changed = True
                logger.info(
                    "fix_response_model_type_mismatches: %s — rewrote import of '%s' "
                    "from '%s' to '%s' (Pydantic schema)",
                    path, sym, old_model_module, schema_module,
                )
            else:
                # No schema equivalent — null out response_model to avoid crash
                old_rm_str = rm.group(0)  # e.g. "response_model=Product"
                new_rm_str = f"response_model=None  # TODO: Create Pydantic schema for {sym}"
                new_content = new_content.replace(old_rm_str, new_rm_str, 1)
                changed = True
                logger.warning(
                    "fix_response_model_type_mismatches: %s — no Pydantic schema found for '%s' "
                    "(imported from '%s'); set response_model=None to prevent FastAPIError",
                    path, sym, model_imports[sym],
                )

        if changed:
            updated[path] = new_content

    return updated


def disambiguate_model_schema_imports(code_files: Dict[str, str]) -> Dict[str, str]:
    """Alias ORM model imports in router files when models/ and schemas/ share a class name.

    When both ``app/models/product.py`` and ``app/schemas/product.py`` define a class called
    ``Product``, router files that import ``from app.models.product import Product`` and use
    it as ``response_model=Product`` will crash FastAPI.  This function detects such collisions
    and aliases the ORM import to ``<Name>Model`` (e.g. ``ProductModel``) so the Pydantic
    schema name remains available unaliased for ``response_model=`` usage.

    Args:
        code_files: Mapping of relative file path → source code.

    Returns:
        The (potentially modified) *code_files* mapping.
    """
    _router_path_re = re.compile(r'^app/(?:routers|routes)/(?!__init__)[^/]+\.py$')

    # Collect class names defined in models/ and schemas/ respectively
    model_class_modules: Dict[str, str] = {}   # class_name -> dotted module e.g. "app.models.product"
    schema_class_modules: Dict[str, str] = {}  # class_name -> dotted module e.g. "app.schemas.product"

    for path, content in code_files.items():
        is_model = bool(re.match(r'^app/models/(?!__init__)[^/]+\.py$', path))
        is_schema = bool(re.match(r'^app/schemas/(?!__init__)[^/]+\.py$', path))
        if not (is_model or is_schema):
            continue
        module_dotted = path.replace("/", ".").removesuffix(".py")
        try:
            tree = ast.parse(content)
            for node in ast.walk(tree):
                if isinstance(node, ast.ClassDef):
                    if is_model:
                        model_class_modules[node.name] = module_dotted
                    else:
                        schema_class_modules[node.name] = module_dotted
        except SyntaxError:
            pass

    # Names that appear in both models/ and schemas/
    colliding: Set[str] = set(model_class_modules) & set(schema_class_modules)
    if not colliding:
        return code_files

    updated = dict(code_files)

    _model_import_re = re.compile(
        r'from\s+(app\.models\.[\w]+)\s+import\s+(?:\(([^)]+)\)|([^\n]+))',
        re.MULTILINE,
    )

    for path, content in code_files.items():
        if not _router_path_re.match(path):
            continue

        new_content = content
        changed = False

        for m in _model_import_re.finditer(content):
            model_module = m.group(1)
            names_raw = m.group(2) or m.group(3) or ""
            names_in_import = []
            for part in names_raw.split(","):
                name = part.split(" as ")[0].strip()
                if name and name.isidentifier() and name in colliding:
                    names_in_import.append(name)

            for sym in names_in_import:
                alias = f"{sym}Model"
                # Rewrite: "from app.models.x import Product" → "... import Product as ProductModel"
                # Handle both plain name and "name as existing_alias"
                def _alias_sym(text: str, _s: str = sym, _a: str = alias) -> str:
                    # Replace bare symbol with aliased version in import list
                    return re.sub(
                        r'\b' + re.escape(_s) + r'\b(?!\s+as\b)',
                        f'{_s} as {_a}',
                        text,
                    )
                new_content = _alias_sym(new_content)
                # Now add the schema import if not already present
                schema_module = schema_class_modules[sym]
                schema_import_line = f"from {schema_module} import {sym}"
                if schema_import_line not in new_content:
                    # Insert after the models import line
                    new_content = new_content.replace(
                        m.group(0),
                        m.group(0) + f"\n{schema_import_line}",
                        1,
                    )
                changed = True
                logger.info(
                    "disambiguate_model_schema_imports: %s — aliased '%s' from '%s' as '%s'; "
                    "added schema import from '%s'",
                    path, sym, model_module, alias, schema_module,
                )

        if changed:
            updated[path] = new_content

    return updated


def _remove_symbol_from_import(source: str, module: str, symbol: str) -> str:
    """Remove a single *symbol* from a ``from <module> import ...`` statement.

    Handles both single-symbol and multi-symbol import lines:

    * ``from sqlalchemy.orm import declarative_base`` → line removed entirely.
    * ``from sqlalchemy.orm import Session, declarative_base`` → rewritten as
      ``from sqlalchemy.orm import Session`` (symbol stripped, rest preserved).
    * Parenthesized multi-line imports are **not** modified; they are rare in
      LLM-generated code and would require a more complex parser.

    Args:
        source: Python source code to process.
        module: Dotted module name (e.g. ``"sqlalchemy.orm"``).
        symbol: Symbol name to remove (e.g. ``"declarative_base"``).

    Returns:
        Modified source with the named symbol removed.  The original source
        is returned unchanged if no matching import is found.
    """
    escaped_module = re.escape(module)

    # Pattern for a flat (single-line, no parens) from … import … statement
    # that contains the target symbol.
    flat_import_re = re.compile(
        rf'^([ \t]*from\s+{escaped_module}\s+import\s+)([^\n(]+)\n?',
        re.MULTILINE,
    )

    def _replace(m: re.Match) -> str:
        prefix = m.group(1)   # "from sqlalchemy.orm import "
        names_raw = m.group(2)  # e.g. "Session, declarative_base, relationship"
        # Split on commas; strip surrounding whitespace and line-continuation backslashes.
        names = [n.strip().rstrip("\\") for n in names_raw.split(",")]
        names = [n for n in names if n and n != symbol]
        if not names:
            # Symbol was the only import — remove the entire line.
            return ""
        return prefix + ", ".join(names) + "\n"

    return flat_import_re.sub(_replace, source)


def consolidate_base_definitions(code_files: Dict[str, str]) -> Dict[str, str]:
    """Remove duplicate ``Base`` class definitions from model files.

    When the LLM generates multiple files that each define their own
    ``Base = declarative_base()`` or ``class Base(DeclarativeBase)``, SQLAlchemy
    creates separate metadata registries, which causes warnings and prevents
    Alembic from discovering all tables.

    This function checks whether ``app/database.py`` (the canonical source)
    defines ``Base``.  If so, it rewrites every *other* ``*.py`` file that also
    defines ``Base`` by:

    1. Removing the inline ``Base = declarative_base()`` line **or** the
       ``class Base(DeclarativeBase): pass`` block.
    2. Adding ``from app.database import Base`` near the top of the file
       (after any ``from __future__`` import, before other local imports).

    Files that already import ``Base`` from ``app.database`` are left unchanged.
    ``app/database.py`` itself is never modified.

    Args:
        code_files: Mapping of relative file path → source code.

    Returns:
        The (potentially modified) *code_files* mapping.
    """
    # Patterns that define a Base in a file
    _base_declarative_re = re.compile(
        r'^[ \t]*Base\s*=\s*declarative_base\s*\([^\n]*\)\s*\n?',
        re.MULTILINE,
    )
    _base_class_re = re.compile(
        r'^[ \t]*class\s+Base\s*\(\s*DeclarativeBase\s*\)\s*:\s*\n'
        r'(?:[ \t]+(?:pass|\.\.\.)\s*\n)',
        re.MULTILINE,
    )
    _base_import_re = re.compile(
        r'\bfrom\s+app\.database\s+import\b[^\n]*\bBase\b'
    )

    # Check if app/database.py defines Base (canonical source)
    db_content = code_files.get("app/database.py", "")
    if not db_content:
        return code_files
    has_base_in_db = bool(
        _base_declarative_re.search(db_content)
        or _base_class_re.search(db_content)
        # Catch non-stub class Base(DeclarativeBase) bodies (e.g. with extra metadata)
        or re.search(r'\bclass\s+Base\s*\([^)]*DeclarativeBase', db_content)
    )
    if not has_base_in_db:
        return code_files

    updated = dict(code_files)
    canonical_import = "from app.database import Base"

    for path, content in code_files.items():
        if path == "app/database.py":
            continue
        if not path.endswith(".py"):
            continue
        # Skip files that don't define Base locally
        has_local_base = bool(
            _base_declarative_re.search(content)
            or _base_class_re.search(content)
        )
        if not has_local_base:
            continue
        # Skip files that already import Base from app.database
        if _base_import_re.search(content):
            continue

        new_content = content
        # Remove inline Base = declarative_base() line
        new_content = _base_declarative_re.sub("", new_content)
        # Remove class Base(DeclarativeBase): pass block
        new_content = _base_class_re.sub("", new_content)

        # Remove `declarative_base` from any `from sqlalchemy.orm import ...` line
        # if it is no longer CALLED anywhere.  Checking for a call pattern
        # ``declarative_base(`` is precise: it ignores the import line itself so
        # that co-imported symbols (e.g. ``Session``) are preserved.
        if not re.search(r'\bdeclarative_base\s*\(', new_content):
            new_content = _remove_symbol_from_import(
                new_content, "sqlalchemy.orm", "declarative_base"
            )
        # Remove `DeclarativeBase` from any `from sqlalchemy.orm import ...` line
        # if no class in the file inherits from it directly (other than the Base
        # stub we just removed).  Checking for ``class Foo(DeclarativeBase)``
        # prevents removing the symbol when it still has legitimate usages.
        if not re.search(r'\bclass\s+\w+\s*\([^)]*DeclarativeBase', new_content):
            new_content = _remove_symbol_from_import(
                new_content, "sqlalchemy.orm", "DeclarativeBase"
            )

        # Insert `from app.database import Base` after any `from __future__` line,
        # or at the very beginning of the import block.
        if canonical_import not in new_content:
            future_match = re.search(r'^from\s+__future__\s+import[^\n]*\n', new_content, re.MULTILINE)
            if future_match:
                insert_pos = future_match.end()
                new_content = (
                    new_content[:insert_pos]
                    + canonical_import + "\n"
                    + new_content[insert_pos:]
                )
            else:
                # Insert before the first import statement
                first_import = re.search(r'^(?:import |from )', new_content, re.MULTILINE)
                if first_import:
                    insert_pos = first_import.start()
                    new_content = (
                        new_content[:insert_pos]
                        + canonical_import + "\n"
                        + new_content[insert_pos:]
                    )
                else:
                    new_content = canonical_import + "\n" + new_content

        if new_content != content:
            updated[path] = new_content
            logger.info(
                "consolidate_base_definitions: removed local Base definition from '%s', "
                "added 'from app.database import Base'",
                path,
            )

    return updated


def _is_stub_content(content: str) -> bool:
    """Return ``True`` when *content* is an auto-generated stub with no real implementation.

    A file is considered a stub when it contains *only*:

    * The ``"Generated module — replace with actual implementation."`` docstring marker.
    * ``pass`` or ``...`` class/function bodies with no field definitions.
    * Placeholder imports (``from typing import Any``).
    * Empty ``__init__.py`` style files (blank or only comments).

    Returns ``False`` when the file contains real field definitions (Pydantic
    ``Field(...)``, SQLAlchemy ``Column(...)``, ``relationship(...)``) or ORM
    infrastructure (``DeclarativeBase``, ``create_async_engine``,
    ``async_sessionmaker``, ``create_engine``, ``sessionmaker``) or annotated
    class attributes or substantive function bodies.

    Args:
        content: Python source code string to inspect.

    Returns:
        ``True`` if the file contains only stub/placeholder content.
    """
    _STUB_MARKER = "Generated module — replace with actual implementation."
    if not content or not content.strip():
        return True
    # Definitive "real content" indicators — if any are present the file is not a stub.
    # Uses the module-level _REAL_CONTENT_PATTERNS (pre-compiled for performance).
    for pattern in _REAL_CONTENT_PATTERNS:
        if pattern.search(content):
            return False
    # Stub marker → immediately a stub
    if _STUB_MARKER in content:
        return True
    # Parse and inspect class/function bodies for real content
    try:
        tree = ast.parse(content)
        for node in ast.walk(tree):
            if isinstance(node, ast.ClassDef):
                # Class has real fields if it has any AnnAssign or non-trivial Assign
                for child in node.body:
                    if isinstance(child, (ast.Assign, ast.AnnAssign)):
                        return False  # Real field definition found
                    if isinstance(child, ast.FunctionDef):
                        # Class method with real body (not just pass/docstring/return None)
                        for stmt in child.body:
                            if isinstance(stmt, ast.Return):
                                if stmt.value and not (
                                    isinstance(stmt.value, ast.Constant) and stmt.value.value is None
                                ):
                                    return False
                            elif not isinstance(stmt, (ast.Pass, ast.Expr)):
                                return False
            elif isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
                # Skip if this function is a method (handled by ClassDef loop above)
                if any(
                    isinstance(parent, ast.ClassDef)
                    for parent in ast.walk(tree)
                    if hasattr(parent, 'body') and node in getattr(parent, 'body', [])
                ):
                    continue
                # Module-level function — check for real body
                for stmt in node.body:
                    if isinstance(stmt, ast.Return):
                        if stmt.value and not (
                            isinstance(stmt.value, ast.Constant) and stmt.value.value is None
                        ):
                            return False
                    elif not isinstance(stmt, (ast.Pass, ast.Expr)):
                        return False
    except SyntaxError:
        return False  # Don't overwrite files we can't parse
    return True


