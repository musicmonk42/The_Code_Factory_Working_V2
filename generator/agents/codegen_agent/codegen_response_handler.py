# Suffixes that indicate an imported name is a module-level variable rather than
# a callable.  Names ending with these strings get a simple object assignment
# instead of a stub function definition.
_VARIABLE_SUFFIXES: Tuple[str, ...] = (
    "_router", "_app", "_engine", "_client", "_pool",
    "_db", "_session", "_manager", "_factory", "_registry",
    "_config", "_settings", "_broker", "_bus", "_cache",
)

# Verb prefixes that indicate a callable (function/method) regardless of suffix.
# e.g. ``get_db`` looks like a variable due to ``_db`` suffix, but the ``get_``
# prefix signals it is a getter function.
_VERB_PREFIXES: Tuple[str, ...] = (
    "get_", "set_", "create_", "delete_", "update_", "fetch_",
    "handle_", "process_", "validate_", "check_", "build_", "make_",
    "run_", "load_", "save_", "send_", "receive_", "parse_", "format_",
    "compute_", "generate_", "initialize_", "init_", "setup_",
)

# Known router variable names that should be stubbed as ``APIRouter()`` instances
# rather than plain ``None`` assignments.
_ROUTER_VARIABLE_PATTERNS: frozenset = frozenset({
    "router", "api_router", "app_router", "main_router",
})

# Class name suffixes that strongly suggest a Pydantic model.  Used by
# ``ensure_local_module_stubs`` to decide whether a stub class should inherit
# from ``pydantic.BaseModel`` rather than being a bare class.
_PYDANTIC_CLASS_SUFFIXES: tuple = (
    "Response", "Request", "Schema", "Create", "Update",
    "Read", "InDB", "Base", "Input", "Output", "Model",
    "Params", "Filter", "Detail", "Summary", "List",
)


def _is_router_variable(name: str) -> bool:
    """Return ``True`` when *name* is a FastAPI router variable.

    Matches exact names in :data:`_ROUTER_VARIABLE_PATTERNS` (e.g. ``router``,
    ``api_router``) as well as any name ending with ``_router`` (e.g.
    ``products_router``, ``orders_router``).

    Names that start with common verb prefixes (e.g. ``create_router``,
    ``get_router``) are excluded because they are factory or getter functions,
    not router instances.

    Args:
        name: The symbol name to check.

    Returns:
        ``True`` if the name should be stubbed as an ``APIRouter()`` instance.

    Examples:
        >>> _is_router_variable("router")
        True
        >>> _is_router_variable("products_router")
        True
        >>> _is_router_variable("create_router")
        False
        >>> _is_router_variable("db_engine")
        False
    """
    lower = name.lower()
    if any(lower.startswith(prefix) for prefix in _VERB_PREFIXES):
        return False
    return lower in _ROUTER_VARIABLE_PATTERNS or lower.endswith("_router")


def _is_likely_variable(name: str) -> bool:
    """Return ``True`` when *name* looks like a module-level variable, not a callable.

    Uses a suffix-based heuristic: well-known framework object names end with
    suffixes such as ``_router``, ``_engine``, ``_client``, etc.  When an
    imported name matches one of these patterns it should be stubbed as a simple
    variable assignment rather than a callable stub function.

    Names that start with common verb prefixes (``get_``, ``set_``, ``create_``,
    etc.) are always treated as callables even when their suffix would otherwise
    match a variable pattern.

    Args:
        name: The imported symbol name to classify.

    Returns:
        ``True`` if the name should be treated as a variable; ``False`` if it
        should be treated as a callable (function or class).

    Examples:
        >>> _is_likely_variable("api_router")
        True
        >>> _is_likely_variable("db_engine")
        True
        >>> _is_likely_variable("get_db")
        False
        >>> _is_likely_variable("get_current_user")
        False
        >>> _is_likely_variable("User")
        False
    """
    name_lower = name.lower()
    # Verb-prefixed names are always callables, never variables.
    if any(name_lower.startswith(prefix) for prefix in _VERB_PREFIXES):
        return False
    return any(name_lower.endswith(suffix) for suffix in _VARIABLE_SUFFIXES)


def ensure_local_module_stubs(code_files: Dict[str, str]) -> Dict[str, str]:
    """Scan generated Python files for local module imports and generate stubs.

    Addresses two related issues:

    1. **Missing modules** — if ``routes.py`` imports ``from app.auth import ...``
       but ``app/auth.py`` does not exist in *code_files*, a stub file is
       created with the required symbols.
    2. **Missing symbols** — if the target module file *does* exist in
       *code_files* but is missing the imported symbol (class or function), a
       stub definition is appended to that file.

    Import patterns detected
    ~~~~~~~~~~~~~~~~~~~~~~~~
    * ``from app.X import A, B`` — handled by :data:`_LOCAL_IMPORT_RE`.
    * ``from app import X [as alias]`` followed by ``X.A`` / ``alias.A``
      attribute-access usage — handled by :data:`_APP_SUBMODULE_IMPORT_RE`.
      The ``as``-alias form (e.g. ``from app import schemas as s``) is fully
      supported; ``s.User`` correctly maps to ``app/schemas.py``.

    Third-party, stdlib, and relative imports are intentionally ignored because
    their resolution is context-dependent.

    Name classification heuristic
    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    An imported name that starts with an uppercase letter is treated as a
    *class*; otherwise it is treated as a *function* returning ``None`` or
    a *variable* (based on known naming conventions such as ``_router``,
    ``_engine``, ``_client`` suffixes).  Variables get a minimal object
    assignment; functions get a ``return None`` body so they don't crash
    immediately at runtime.

    Args:
        code_files: Mapping of relative file paths to source code strings, as
            returned by :func:`parse_llm_response`.  Modified **in-place** and
            also returned so callers can chain the result.

    Returns:
        The (potentially augmented) *code_files* mapping.

    Side-effect:
        Sets the ``_stub_files_created`` attribute on the returned dict to the
        ``set`` of file paths that were created or augmented as stubs during
        this call.  Callers can inspect this set to decide whether a retry
        prompt should list the stub files that need real implementations.
    """
    # Track which files are created/modified as stubs during this call
    stub_files_created: Set[str] = set()
    # First pass: collect all required symbols per module path.
    # Maps "app/auth.py" -> {"get_current_user", "Role", ...}
    required_symbols: Dict[str, Set[str]] = {}

    for _filename, content in list(code_files.items()):
        for match in _LOCAL_IMPORT_RE.finditer(content):
            module_str = match.group(1).strip()
            imports_str = match.group(2).strip()

            # Convert "app.auth" -> "app/auth.py"
            module_path = module_str.replace(".", "/") + ".py"

            # Parse imported names, handling "A, B as b, C" forms.
            # Also skips any token that is not a valid Python identifier
            # (e.g. the opening "(" of a multi-line parenthesised import).
            symbols: Set[str] = set()
            for part in imports_str.split(","):
                name = part.split(" as ")[0].strip()
                if name and name != "*" and name.isidentifier():
                    symbols.add(name)

            if module_path not in required_symbols:
                required_symbols[module_path] = set()
            required_symbols[module_path].update(symbols)

        # Attribute-access scanning pass: detect ``from app import schemas [as s]``
        # followed by attribute-access patterns (``schemas.User`` or ``s.User``) so
        # that symbols reached via module-attribute syntax are also stubbed.
        # This is separate from the ``_LOCAL_IMPORT_RE`` scan above (which handles
        # ``from app.schemas import User``) and runs within the same per-file loop.
        #
        # For each matched token we record both the canonical module name (used to
        # build the ``app/<name>.py`` path) and the local alias actually used in the
        # source (used for the attribute-access scan).  When no ``as`` clause is
        # present the alias equals the module name.
        for mod_match in _APP_SUBMODULE_IMPORT_RE.finditer(content):
            for part in mod_match.group(1).split(","):
                token = part.strip()
                # Handle optional "X as alias" form.
                as_parts = re.split(r"\bas\b", token, maxsplit=1)
                module_name = as_parts[0].strip()
                local_alias = as_parts[1].strip() if len(as_parts) == 2 else module_name

                if not module_name or not module_name.isidentifier():
                    continue
                if not local_alias or not local_alias.isidentifier():
                    local_alias = module_name

                module_path = f"app/{module_name}.py"
                # Scan the entire file for ``<alias>.SomeName`` accesses.
                # The word boundary (\b) prevents false matches on longer names
                # (e.g. ``myschemas.X`` when alias is ``schemas``).
                attr_pattern = re.compile(
                    r"\b" + re.escape(local_alias) + r"\.([A-Za-z_]\w*)"
                )
                for attr_match in attr_pattern.finditer(content):
                    symbol = attr_match.group(1)
                    if symbol.isidentifier():
                        if module_path not in required_symbols:
                            required_symbols[module_path] = set()
                        required_symbols[module_path].add(symbol)

    # Maps module_path -> set of non-pydantic stub class names created in the second pass.
    # Consumed by the third pass to inject discovered instance methods.
    _stub_class_names: Dict[str, Set[str]] = {}

    # Second pass: ensure every required module+symbol exists in code_files.
    for module_path, symbols in required_symbols.items():
        if not symbols:
            continue

        if module_path not in code_files:
            # Module file is entirely missing — generate a stub.
            # Determine up-front whether any router-pattern symbols are present
            # so the APIRouter import can be emitted once at the module header.
            has_router_in_symbols = any(
                _is_router_variable(s) for s in symbols if not s[0].isupper()
            )
            # Determine whether any class stubs need Pydantic BaseModel inheritance.
            _is_pydantic_module = "schemas" in module_path or "models" in module_path
            has_pydantic_class = any(
                sym[0].isupper() and (
                    _is_pydantic_module
                    or any(sym.endswith(sfx) for sfx in _PYDANTIC_CLASS_SUFFIXES)
                )
                for sym in symbols
            )
            stub_lines = [
                '"""Generated module — replace with actual implementation."""\n',
                "from typing import Any\n",
            ]
            if has_pydantic_class:
                stub_lines.append("from pydantic import BaseModel\n")
            if has_router_in_symbols:
                stub_lines.append("from fastapi import APIRouter\n")
            stub_lines.append("\n")
            for sym in sorted(symbols):
                # Uppercase initial → class; router variable → APIRouter() instance;
                # other known variable suffixes → None variable;
                # otherwise → function returning None.
                if sym[0].isupper():
                    _sym_is_pydantic = _is_pydantic_module or any(
                        sym.endswith(sfx) for sfx in _PYDANTIC_CLASS_SUFFIXES
                    )
                    if _sym_is_pydantic:
                        stub_lines.append(
                            f"class {sym}(BaseModel):\n"
                            f'    """Stub Pydantic model."""\n'
                            f"    pass\n\n\n"
                        )
                    else:
                        stub_lines.append(
                            f"class {sym}:\n"
                            f'    """Stub class."""\n'
                            f"    pass\n\n\n"
                        )
                        _stub_class_names.setdefault(module_path, set()).add(sym)
                elif _is_router_variable(sym):
                    stub_lines.append(
                        f"{sym} = APIRouter()\n\n\n"
                    )
                elif _is_likely_variable(sym):
                    stub_lines.append(
                        f"{sym} = None  # Placeholder — assign actual value\n\n\n"
                    )
                else:
                    stub_lines.append(
                        f"def {sym}(*args: Any, **kwargs: Any) -> Any:\n"
                        f'    """Placeholder implementation."""\n'
                        f"    return None\n\n\n"
                    )
            code_files[module_path] = "".join(stub_lines)
            stub_files_created.add(module_path)
            logger.info(
                "ensure_local_module_stubs: created stub module %s with symbols %s",
                module_path,
                sorted(symbols),
            )
        else:
            # Module exists — identify and append any missing symbols.
            existing_content = code_files[module_path]
            try:
                tree = ast.parse(existing_content)
                # Collect names from function/class definitions at any nesting depth.
                defined_names: Set[str] = {
                    node.name
                    for node in ast.walk(tree)
                    if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef))
                }
                # Also collect top-level variable assignment names so that existing
                # module-level variables (e.g. ``db_engine = ...``) are not re-stubbed.
                for node in ast.iter_child_nodes(tree):
                    if isinstance(node, ast.Assign):
                        for target in node.targets:
                            if isinstance(target, ast.Name):
                                defined_names.add(target.id)
                    elif isinstance(node, ast.AnnAssign) and isinstance(node.target, ast.Name):
                        defined_names.add(node.target.id)
            except SyntaxError:
                defined_names = set()

            missing = symbols - defined_names
            if missing:
                # Emit the APIRouter import once at the top of the appended block
                # if any missing symbol needs it.
                has_router_missing = any(
                    _is_router_variable(sym)
                    for sym in missing
                    if not sym[0].isupper()
                )
                _is_pydantic_module = "schemas" in module_path or "models" in module_path
                has_pydantic_missing = any(
                    sym[0].isupper() and (
                        _is_pydantic_module
                        or any(sym.endswith(sfx) for sfx in _PYDANTIC_CLASS_SUFFIXES)
                    )
                    for sym in missing
                )
                appended_lines = ["\n\n# Supplemental symbols appended by module-stub pass\n"]
                if has_pydantic_missing and "from pydantic import BaseModel" not in existing_content:
                    appended_lines.append("from pydantic import BaseModel\n")
                if has_router_missing and "from fastapi import APIRouter" not in existing_content:
                    appended_lines.append("from fastapi import APIRouter\n")
                for sym in sorted(missing):
                    if sym[0].isupper():
                        _sym_is_pydantic = _is_pydantic_module or any(
                            sym.endswith(sfx) for sfx in _PYDANTIC_CLASS_SUFFIXES
                        )
                        if _sym_is_pydantic:
                            appended_lines.append(
                                f"\nclass {sym}(BaseModel):\n"
                                f'    """Stub Pydantic model."""\n'
                                f"    pass\n"
                            )
                        else:
                            appended_lines.append(
                                f"\nclass {sym}:\n"
                                f'    """Stub class."""\n'
                                f"    pass\n"
                            )
                            _stub_class_names.setdefault(module_path, set()).add(sym)
                    elif _is_router_variable(sym):
                        appended_lines.append(
                            f"\n{sym} = APIRouter()\n"
                        )
                    elif _is_likely_variable(sym):
                        appended_lines.append(
                            f"\n{sym} = None  # Placeholder — assign actual value\n"
                        )
                    else:
                        appended_lines.append(
                            f"\ndef {sym}(*args: Any, **kwargs: Any) -> Any:\n"
                            f'    """Placeholder implementation."""\n'
                            f"    return None\n"
                        )
                code_files[module_path] = existing_content + "".join(appended_lines)
                stub_files_created.add(module_path)
                logger.info(
                    "ensure_local_module_stubs: appended stubs for %s to %s",
                    sorted(missing),
                    module_path,
                )

    # Third pass: detect methods referenced on instances of stub classes and inject
    # placeholder method stubs so that attribute access does not raise AttributeError.
    # Example: ``auth_service = AuthService()`` followed by
    # ``Depends(auth_service.get_current_user)`` in a router file requires that
    # ``AuthService`` exposes a ``get_current_user`` method.
    #
    # This pass is intentionally non-fatal: a scan/injection failure must never
    # prevent the caller from receiving the already-valid code_files dict.
    if _stub_class_names:
        try:
            # Build reverse mapping: class_name -> module_path.
            # Guard: skip if all stub class sets were empty (avoids empty-alternation regex).
            _class_to_module: Dict[str, str] = {
                _cls: _mod_path
                for _mod_path, _cls_set in _stub_class_names.items()
                for _cls in _cls_set
            }
            if _class_to_module:
                # Pattern to find ``var = ClassName()`` instantiation sites.
                _cls_alt = "|".join(re.escape(c) for c in sorted(_class_to_module))
                _instantiation_re = re.compile(
                    r"^[ \t]*([A-Za-z_]\w*)\s*=\s*(" + _cls_alt + r")\s*\(",
                    re.MULTILINE,
                )
                # Maps variable_name -> (class_name, module_path)
                _instance_vars: Dict[str, Tuple[str, str]] = {}
                for _content in code_files.values():
                    for _m in _instantiation_re.finditer(_content):
                        _instance_vars[_m.group(1)] = (
                            _m.group(2), _class_to_module[_m.group(2)]
                        )

                if _instance_vars:
                    # Maps (module_path, class_name) -> {method_name: is_async}
                    _methods_to_inject: Dict[Tuple[str, str], Dict[str, bool]] = {}
                    for _var, (_cls, _mod_path) in _instance_vars.items():
                        _ev = re.escape(_var)
                        _depends_re = re.compile(
                            r"\bDepends\(\s*" + _ev + r"\.([A-Za-z_]\w*)\s*\)"
                        )
                        _await_re = re.compile(
                            r"\bawait\s+" + _ev + r"\.([A-Za-z_]\w*)\b"
                        )
                        _attr_re = re.compile(
                            r"\b" + _ev + r"\.([A-Za-z_]\w*)\b"
                        )
                        _key: Tuple[str, str] = (_mod_path, _cls)
                        _async_meths: Set[str] = set()
                        _all_meths: Set[str] = set()
                        for _fc in code_files.values():
                            for _am in _depends_re.finditer(_fc):
                                _async_meths.add(_am.group(1))
                                _all_meths.add(_am.group(1))
                            for _am in _await_re.finditer(_fc):
                                _async_meths.add(_am.group(1))
                                _all_meths.add(_am.group(1))
                            for _am in _attr_re.finditer(_fc):
                                _all_meths.add(_am.group(1))
                        if _key not in _methods_to_inject:
                            _methods_to_inject[_key] = {}
                        for _meth in _all_meths:
                            # Skip dunder attributes: they have special Python semantics
                            # and must never be shadowed by a generic stub method.
                            if _meth.startswith("__"):
                                continue
                            _is_async = _meth in _async_meths
                            if _meth in _methods_to_inject[_key]:
                                # async wins when the same method is discovered from
                                # multiple instance variables or call contexts.
                                _methods_to_inject[_key][_meth] = (
                                    _methods_to_inject[_key][_meth] or _is_async
                                )
                            else:
                                _methods_to_inject[_key][_meth] = _is_async

                    # Inject discovered methods into stub class bodies.
                    for (_mod_path, _cls), _methods in _methods_to_inject.items():
                        if not _methods or _mod_path not in code_files:
                            continue
                        _method_lines: List[str] = []
                        for _meth in sorted(_methods):
                            _prefix = "async " if _methods[_meth] else ""
                            # Each element is a complete method definition without a
                            # trailing blank line; elements are joined with a single
                            # blank line (PEP 8 convention for methods inside a class).
                            _method_lines.append(
                                f"    {_prefix}def {_meth}(self, *args, **kwargs):\n"
                                f'        raise NotImplementedError("Stub: implement {_meth}")'
                            )
                        # One blank line between methods; trailing newline closes the body.
                        _method_body = "\n\n".join(_method_lines) + "\n"
                        _stub_cls_re = re.compile(
                            r"(class\s+" + re.escape(_cls) + r"\s*:\s*\n"
                            r'\s*"""Stub class\."""\s*\n)'
                            r"(\s*pass\b[^\n]*)",
                            re.MULTILINE,
                        )
                        _new_content = _stub_cls_re.sub(
                            lambda m, _b=_method_body: m.group(1) + _b,
                            code_files[_mod_path],
                            count=1,
                        )
                        if _new_content != code_files[_mod_path]:
                            code_files[_mod_path] = _new_content
                            logger.info(
                                "ensure_local_module_stubs: injected %d method(s) %s "
                                "into %s.%s",
                                len(_methods),
                                sorted(_methods),
                                _mod_path,
                                _cls,
                            )
        except Exception as _third_pass_err:
            # Non-fatal: a scan/injection failure must never discard the already-
            # valid code_files dict produced by the first two passes.
            logger.warning(
                "ensure_local_module_stubs: third pass (method injection) failed "
                "(non-fatal): %s",
                _third_pass_err,
            )

    if stub_files_created:
        logger.info(
            "ensure_local_module_stubs: %d stub file(s) created/modified: %s",
            len(stub_files_created),
            sorted(stub_files_created),
        )
    return code_files


# Marker present in the docstring header of every auto-generated stub module.
_STUB_MODULE_MARKER = "Generated module — replace with actual implementation."


def get_stub_files(code_files: Dict[str, str]) -> Set[str]:
    """Return the set of stub files present in *code_files*.

    A file is considered a stub when its content contains the canonical stub
    module header emitted by :func:`ensure_local_module_stubs`.

    Args:
        code_files: Mapping of filename → source code.

    Returns:
        Set of filenames whose content identifies them as auto-generated stubs.
    """
    return {
        path
        for path, content in code_files.items()
        if _STUB_MODULE_MARKER in content
    }


def build_stub_retry_prompt_hint(code_files: Dict[str, str]) -> str:
    """Build a prompt hint listing stub files that need real implementations.

    Scans *code_files* for auto-generated stubs (files containing the stub
    module header) and returns a formatted string suitable for inclusion in a
    retry LLM prompt.  Returns an empty string when no stubs are detected.

    Args:
        code_files: Mapping of filename → source code.

    Returns:
        A formatted hint string listing stub files and the symbols they export,
        or ``""`` if no stubs are detected.
    """
    stub_files = get_stub_files(code_files)
    if not stub_files:
        return ""

    lines = [
        "IMPORTANT: The following files are placeholder stubs that MUST be replaced "
        "with real implementations:",
    ]
    for path in sorted(stub_files):
        content = code_files[path]
        # Extract exported symbol names from the stub content
        try:
            tree = ast.parse(content)
            symbols = [
                node.name
                for node in ast.walk(tree)
                if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef))
            ]
            # Also include top-level variable assignments
            for node in ast.iter_child_nodes(tree):
                if isinstance(node, ast.Assign):
                    for target in node.targets:
                        if isinstance(target, ast.Name):
                            symbols.append(target.id)
        except SyntaxError:
            symbols = []
        sym_list = ", ".join(sorted(set(symbols))) if symbols else "(unknown symbols)"
        lines.append(f"  - {path}: must export {sym_list}")
    lines.append(
        "Generate complete, production-ready implementations for each stub file listed above."
    )
    return "\n".join(lines)
