@lru_cache(maxsize=None)
def _is_tool_available(tool: str) -> bool:
    """
    Checks if an executable is available on PATH. Cached for efficiency.
    """
    from shutil import which

    available = which(tool) is not None
    logger.debug("Tool availability check: %s -> %s", tool, available)
    return available


