def remove_dead_imports(code: str, filename: str = "") -> str:
    """Remove unused/dead import statements from Python source code.

    Parses the code with AST, identifies all imported names, then checks which
    names are actually referenced in the code body.  Import statements where
    NONE of the imported names are referenced are removed.

    Banned/hallucinated imports are automatically replaced with their known-good
    alternatives before the dead-import check (see :data:`BANNED_IMPORTS`).

    Args:
        code: Python source code to clean.
        filename: Source filename used in log messages (optional).

    Returns:
        Cleaned source code with unused/banned imports removed or replaced.
    """
    if not code or not code.strip():
        return code

    # Only process Python files
    if filename and not filename.endswith(".py"):
        return code

    # --- Step 1: Replace banned imports with their known-good alternatives ---
    lines = code.splitlines(keepends=True)
    replaced_lines: List[str] = []
    for line in lines:
        replaced = False
        for banned_mod, replacement_mod in BANNED_IMPORTS.items():
            # Match "import banned_mod" or "from banned_mod import ..."
            if re.match(rf"^\s*(from\s+{re.escape(banned_mod)}\s+import|import\s+{re.escape(banned_mod)})", line):
                meta = KNOWN_HALLUCINATED_PACKAGES.get(banned_mod)
                if meta:
                    replacement_line = meta["replacement_import"] + "\n"
                    logger.warning(
                        "Replaced banned import '%s' with '%s' in %s: %s",
                        banned_mod, replacement_mod, filename or "<unknown>", meta["reason"]
                    )
                    replaced_lines.append(replacement_line)
                else:
                    logger.warning(
                        "Removed banned import '%s' from %s", banned_mod, filename or "<unknown>"
                    )
                replaced = True
                break
        if not replaced:
            replaced_lines.append(line)
    code = "".join(replaced_lines)

    # --- Step 1b: Replace banned module attribute accesses in code body ---
    # e.g. aioredis.create_redis_pool(...) -> redis.asyncio.create_redis_pool(...)
    for banned_mod, replacement_mod in BANNED_IMPORTS.items():
        if banned_mod in code:
            code = re.sub(
                rf'\b{re.escape(banned_mod)}\.',
                f'{replacement_mod}.',
                code,
            )

    # --- Step 2: Remove dead imports (imported but never used) ---
    try:
        tree = ast.parse(code)
    except SyntaxError:
        # If parsing fails after replacement, return as-is
        return code

    # Collect all imported names with their source line numbers
    import_nodes: List[Tuple[ast.stmt, List[str]]] = []
    for node in ast.walk(tree):
        if isinstance(node, ast.Import):
            names = [alias.asname if alias.asname else alias.name.split(".")[0]
                     for alias in node.names]
            import_nodes.append((node, names))
        elif isinstance(node, ast.ImportFrom):
            names = [alias.asname if alias.asname else alias.name
                     for alias in node.names
                     if alias.name != "*"]
            if names:
                import_nodes.append((node, names))

    if not import_nodes:
        return code

    # Collect all names used in the rest of the code (non-import nodes)
    used_names: Set[str] = set()
    for node in ast.walk(tree):
        if isinstance(node, ast.Name):
            used_names.add(node.id)
        elif isinstance(node, ast.Attribute):
            # Track the root name (e.g., "os" in "os.path.join")
            cur = node
            while isinstance(cur, ast.Attribute):
                cur = cur.value
            if isinstance(cur, ast.Name):
                used_names.add(cur.id)

    # Determine which import lines to remove
    lines_to_remove: Set[int] = set()
    for node, imported_names in import_nodes:
        # Check if ANY of the imported names are used
        if not any(name in used_names for name in imported_names):
            # None of the imported names are used — mark for removal
            import_line = ast.get_source_segment(code, node) or ""
            logger.warning(
                "Removed unused import '%s' from %s",
                import_line.strip(), filename or "<unknown>"
            )
            # Mark the line range for removal (AST uses 1-based line numbers)
            for lineno in range(node.lineno, node.end_lineno + 1):
                lines_to_remove.add(lineno)

    if not lines_to_remove:
        return code

    # Rebuild code without the removed import lines
    code_lines = code.splitlines(keepends=True)
    cleaned_lines = [
        line for i, line in enumerate(code_lines, start=1)
        if i not in lines_to_remove
    ]
    return "".join(cleaned_lines)


def extract_and_populate_requirements(files: Dict[str, str]) -> Dict[str, str]:
    """Extract third-party imports from generated Python files and populate requirements.txt.

    Scans all ``.py`` files in *files* using AST parsing to identify imported
    top-level package names.  Stdlib and project-local imports are filtered out.
    The remainder are mapped to their canonical PyPI names via
    :data:`_MODULE_TO_PYPI` and merged into ``requirements.txt`` (preserving
    any packages that already appear there).

    Args:
        files: Dictionary mapping filenames to file contents.

    Returns:
        Updated *files* dict with a populated/merged ``requirements.txt``.
    """
    # Determine project-local package prefixes from the file map
    local_prefixes: Set[str] = {"app", "tests", "test"}
    for path in files:
        # Top-level directory names are treated as project-local
        parts = path.replace("\\", "/").split("/")
        if len(parts) > 1 and parts[0]:
            local_prefixes.add(parts[0])
        # Also treat the stem of a root .py file as local (e.g. main.py → main)
        if len(parts) == 1 and path.endswith(".py"):
            local_prefixes.add(path[:-3])

    # Determine stdlib modules
    if hasattr(sys, "stdlib_module_names"):
        stdlib_modules: Set[str] = set(sys.stdlib_module_names)  # type: ignore[attr-defined]
    else:  # Python < 3.10 fallback
        stdlib_modules = {
            "abc", "ast", "asyncio", "base64", "collections", "contextlib",
            "copy", "csv", "datetime", "decimal", "email", "enum", "functools",
            "gc", "glob", "gzip", "hashlib", "hmac", "html", "http", "importlib",
            "inspect", "io", "itertools", "json", "logging", "math", "os",
            "pathlib", "pickle", "platform", "pprint", "queue", "random", "re",
            "secrets", "shutil", "signal", "socket", "sqlite3", "ssl", "stat",
            "string", "struct", "subprocess", "sys", "tempfile", "threading",
            "time", "traceback", "types", "typing", "unittest", "urllib",
            "uuid", "warnings", "weakref", "xml", "zipfile", "zlib",
        }

    # Collect third-party imports from all Python files
    third_party: Set[str] = set()
    for filename, content in files.items():
        if not filename.endswith(".py") or not content.strip():
            continue
        try:
            tree = ast.parse(content)
        except SyntaxError:
            continue
        for node in ast.walk(tree):
            if isinstance(node, ast.Import):
                for alias in node.names:
                    top = alias.name.split(".")[0]
                    if top and top not in stdlib_modules and top not in local_prefixes:
                        third_party.add(top)
            elif isinstance(node, ast.ImportFrom):
                if node.module:
                    top = node.module.split(".")[0]
                    if top and top not in stdlib_modules and top not in local_prefixes:
                        third_party.add(top)

    # Scan string literals in Python files for database URL protocols that imply dependencies.
    # This catches packages that are not explicitly imported but required at runtime via
    # SQLAlchemy async dialect strings (e.g. ``sqlite+aiosqlite:///``).
    _DB_PROTOCOL_DEPS: Dict[str, str] = {
        "aiosqlite": "aiosqlite",
        "asyncpg": "asyncpg",
        "aiomysql": "aiomysql",
        "aiopg": "aiopg",
    }
    for filename, content in files.items():
        if not filename.endswith(".py"):
            continue
        for protocol in _DB_PROTOCOL_DEPS:
            if f"+{protocol}" in content or f"import {protocol}" in content:
                third_party.add(protocol)

    # Scan Dockerfile for Python package invocations in CMD/ENTRYPOINT lines.
    # Parses line-by-line to handle both JSON array form (CMD ["uvicorn", ...])
    # and shell form (CMD uvicorn ...) reliably without cross-element regex issues.
    dockerfile_content = files.get("Dockerfile", "") or files.get("dockerfile", "")
    if dockerfile_content:
        for line in dockerfile_content.splitlines():
            stripped = line.strip()
            m = re.match(r'^(?:CMD|ENTRYPOINT)\s+(.+)', stripped, re.IGNORECASE)
            if not m:
                continue
            rest = m.group(1).strip()
            if rest.startswith("["):
                # JSON array form: CMD ["executable", "arg1", ...]
                first = re.search(r'"(\w+)"', rest)
                cmd = first.group(1) if first else ""
            else:
                # Shell form: CMD executable [args...]
                tokens = rest.split()
                cmd = tokens[0] if tokens else ""
            if cmd and cmd not in stdlib_modules and cmd not in local_prefixes:
                third_party.add(cmd)

    if not third_party:
        return files

    # Map import names to PyPI package names
    pypi_packages: Set[str] = set()
    for mod in third_party:
        pypi_name = _MODULE_TO_PYPI.get(mod, mod)
        pypi_packages.add(pypi_name)

    # Load existing requirements.txt content
    existing_reqs = files.get("requirements.txt", "")
    existing_lines = [
        line.strip() for line in existing_reqs.splitlines() if line.strip()
    ]
    # Build a set of already-declared package names (lowercase, strip version specs)
    def _pkg_name(req: str) -> str:
        return re.split(r"[><=!;\[]", req)[0].strip().lower()

    existing_pkg_names = {_pkg_name(line) for line in existing_lines}

    # Add missing packages
    new_packages = sorted(
        pkg for pkg in pypi_packages
        if _pkg_name(pkg) not in existing_pkg_names
    )
    if not new_packages:
        return files

    merged = existing_lines + new_packages
    result = dict(files)
    result["requirements.txt"] = "\n".join(merged) + "\n"
    logger.info(
        "extract_and_populate_requirements: added %d package(s) to requirements.txt: %s",
        len(new_packages), new_packages
    )
    return result


# Sync→async driver URL rewrite patterns.  Each entry is (compiled_regex, replacement).
# The negative lookahead ensures already-correct async URLs are not re-processed.
# NOTE: The capture group always includes the URL scheme separator (`:///` or `://`)
# so that the replacement can reconstruct the full URL correctly.
_SYNC_TO_ASYNC_URL_REWRITES: List[Tuple[re.Pattern, str]] = [
    (re.compile(r'\bpostgresql(?!\+asyncpg)(://)', re.IGNORECASE), r"postgresql+asyncpg\1"),
    (re.compile(r'\bpostgres(?!\+asyncpg)(://)', re.IGNORECASE), r"postgresql+asyncpg\1"),
    (re.compile(r'\bmysql(?!\+aiomysql)(://)', re.IGNORECASE), r"mysql+aiomysql\1"),
    (re.compile(r'\bsqlite(?!\+aiosqlite)(:///)', re.IGNORECASE), r"sqlite+aiosqlite\1"),
]

# Synchronous driver PyPI package names that should be removed for async projects.
_SYNC_DRIVER_PYPI_NAMES: Set[str] = {"psycopg2", "psycopg2-binary", "psycopg2_binary"}


def fix_async_database_url(files: Dict[str, str]) -> Dict[str, str]:
    """Rewrite synchronous DATABASE_URLs to async equivalents when ``create_async_engine`` is used.

    Scans all Python files in *files* for usage of ``create_async_engine`` or
    ``async_sessionmaker``.  When found, any database URL strings that use a
    synchronous driver scheme (``postgresql://``, ``mysql://``, ``sqlite:///``,
    etc.) are rewritten to their async equivalents
    (``postgresql+asyncpg://``, ``mysql+aiomysql://``, ``sqlite+aiosqlite:///``).

    Also updates ``requirements.txt``:

    * Removes synchronous driver packages (``psycopg2-binary``, ``psycopg2``).
    * Adds ``asyncpg`` if ``postgresql+asyncpg://`` URLs are present.
    * Adds ``aiosqlite`` if ``sqlite+aiosqlite:///`` URLs are present.

    Args:
        files: Dict mapping filenames to file contents (as returned by
               :func:`parse_llm_response`).

    Returns:
        Updated *files* dict with async-compatible database URLs and
        ``requirements.txt`` adjusted for async drivers.
    """
    # Check if any Python file uses the async SQLAlchemy API.
    uses_async_engine = any(
        filename.endswith(".py") and (
            "create_async_engine" in content
            or "async_sessionmaker" in content
        )
        for filename, content in files.items()
    )
    if not uses_async_engine:
        return files

    result = dict(files)
    changed_files: List[str] = []

    for filename, content in result.items():
        if not filename.endswith(".py"):
            continue
        new_content = content
        for pattern, replacement in _SYNC_TO_ASYNC_URL_REWRITES:
            new_content = pattern.sub(replacement, new_content)
        if new_content != content:
            result[filename] = new_content
            changed_files.append(filename)

    if changed_files:
        logger.info(
            "fix_async_database_url: rewrote sync database URL(s) in %d file(s): %s",
            len(changed_files),
            changed_files,
        )

    # Update requirements.txt: remove sync drivers, add async drivers as needed.
    reqs_content = result.get("requirements.txt", "")
    if reqs_content is None:
        reqs_content = ""

    def _pkg_name(req: str) -> str:
        return re.split(r"[><=!;\[]", req)[0].strip().lower()

    lines = [ln.strip() for ln in reqs_content.splitlines() if ln.strip()]
    filtered = [ln for ln in lines if _pkg_name(ln) not in _SYNC_DRIVER_PYPI_NAMES]
    pkg_names = {_pkg_name(ln) for ln in filtered}

    all_py_content = " ".join(
        content for fname, content in result.items() if fname.endswith(".py")
    )

    if "postgresql+asyncpg" in all_py_content and "asyncpg" not in pkg_names:
        filtered.append("asyncpg")
    if "sqlite+aiosqlite" in all_py_content and "aiosqlite" not in pkg_names:
        filtered.append("aiosqlite")

    new_reqs = ("\n".join(filtered) + "\n") if filtered else ""
    if new_reqs != reqs_content:
        result["requirements.txt"] = new_reqs
        logger.info(
            "fix_async_database_url: updated requirements.txt "
            "(removed sync drivers, added async driver packages)"
        )

    return result


def parse_llm_response(response: Union[str, Dict[str, Any]], lang: str = "python") -> Dict[str, str]:
    """
    Parses the LLM response, handling both multi-file JSON and single-file code.

    Multi-file mode expects:
        {
          "files": {
            "main.py": "<code>",
            "util.py": "<code>"
          }
        }

    Behavior:
    - For multi-file JSON:
        - Each file is cleaned and syntax-validated.
        - Valid files are returned as-is.
        - Invalid files are summarized into ERROR_FILENAME.
    - For non-JSON:
        - Treat as a single file; validate.
        - Return DEFAULT_FILENAME or ERROR_FILENAME accordingly.
    
    Args:
        response: Either a string (legacy behavior) or a dict (OpenAI API response format)
        lang: Target programming language for syntax validation
    
    Returns:
        Dictionary mapping filenames to code content
    """
    def _finalize_with_pydantic_v2_validation(code_files: Dict[str, str]) -> Dict[str, str]:
        """Auto-fix common Pydantic v1 patterns, then block materialization if issues remain."""
        # Fix Pydantic V1 → V2 error type strings in test assertions
        _v1_to_v2: Dict[str, str] = {
            '"value_error.missing"': '"missing"',
            "'value_error.missing'": "'missing'",
            '"type_error.str"': '"string_type"',
            "'type_error.str'": "'string_type'",
            '"type_error.integer"': '"int_parsing"',
            "'type_error.integer'": "'int_parsing'",
            '"type_error.float"': '"float_parsing"',
            "'type_error.float'": "'float_parsing'",
            '"type_error.bool"': '"bool_parsing"',
            "'type_error.bool'": "'bool_parsing'",
            '"value_error.any_str.min_length"': '"string_too_short"',
            "'value_error.any_str.min_length'": "'string_too_short'",
            '"value_error.any_str.max_length"': '"string_too_long"',
            "'value_error.any_str.max_length'": "'string_too_long'",
            '"value_error.number.not_gt"': '"greater_than"',
            "'value_error.number.not_gt'": "'greater_than'",
            '"value_error.number.not_ge"': '"greater_than_equal"',
            "'value_error.number.not_ge'": "'greater_than_equal'",
            '"value_error.number.not_lt"': '"less_than"',
            "'value_error.number.not_lt'": "'less_than'",
            '"value_error.number.not_le"': '"less_than_equal"',
            "'value_error.number.not_le'": "'less_than_equal'",
        }

        patched: Dict[str, str] = {}
        for fname, content in code_files.items():
            for old, new in _v1_to_v2.items():
                content = content.replace(old, new)
            # Fix hallucinated validate_item_data() calls → Item(**data)
            content = re.sub(r'validate_item_data\(([^)]+)\)', r'Item(**\1)', content)
            patched[fname] = content

        fixed_files = auto_fix_pydantic_v1_imports(patched)
        fixed_files = auto_fix_settings_instantiation(fixed_files)
        pydantic_errors = validate_pydantic_v2_compatibility(fixed_files)
        if pydantic_errors:
            logger.warning("Pydantic v2 compatibility validation failed: %s", pydantic_errors)
            return {
                ERROR_FILENAME: (
                    "Pydantic v2 compatibility validation failed.\n\n"
                    + "\n".join(f"- {error}" for error in pydantic_errors)
                )
            }
        # Ensure missing local module stubs are generated before materializing
        if lang == "python":
            fixed_files = ensure_local_module_stubs(fixed_files)
            # Re-run collision detection after stub generation: stubs may re-introduce
            # module files that collide with package directories already in code_files.
            fixed_files = _detect_module_package_collisions(fixed_files)
            # Fix 1: rewrite SQLAlchemy model imports to Pydantic schema imports for response_model=
            fixed_files = fix_response_model_type_mismatches(fixed_files)
            # Fix 3: alias ORM imports when models/ and schemas/ share a class name
            fixed_files = disambiguate_model_schema_imports(fixed_files)
            # Fix 5: consolidate duplicate Base class definitions to app/database.py
            fixed_files = consolidate_base_definitions(fixed_files)
        # Populate requirements.txt with any third-party imports found in the generated code
        fixed_files = extract_and_populate_requirements(fixed_files)
        # Fix async database driver: rewrite sync URLs (postgresql://) to async (postgresql+asyncpg://)
        if lang == "python":
            fixed_files = fix_async_database_url(fixed_files)
        return fixed_files

    # Handle dict response from OpenAI API
    if isinstance(response, dict):
        # Check if this is already a file map ({"files": {...}} or {"app/main.py": "..."})
        if "files" in response and isinstance(response["files"], dict):
            logger.info("Response is already a file map dict with 'files' key")
            raw = json.dumps(response)
        elif all(isinstance(v, str) for v in response.values()) and any(
            "/" in k or "." in k for k in response.keys()
        ):
            logger.info("Response is already a flat file map dict")
            raw = json.dumps({"files": response})
        else:
            # Extract content from OpenAI chat completion response
            raw = ''
            try:
                # Try OpenAI chat completion format first
                if 'choices' in response and response['choices']:
                    raw = response['choices'][0].get('message', {}).get('content', '')
            except (KeyError, TypeError):
                # KeyError: unexpected dict structure
                # TypeError: choices is not subscriptable or message is not a dict
                pass
            
            # Fallback: try to get 'content' or 'text' directly if extraction failed
            if not raw:
                raw = response.get('content', response.get('text', ''))
        
        logger.debug("Extracted content from dict response, length: %d", len(raw) if raw else 0)
    else:
        raw = response
    
    raw = raw.strip() if raw else ''
    
    # Log raw response length for debugging
    logger.debug(
        "Processing LLM response - length: %d chars, first 100: %s",
        len(raw),
        raw[:100] if raw else "(empty)"
    )
    
    # --- ENHANCEMENT: Try to strip common LLM response prefixes ---
    # Sometimes LLMs prefix their response with "json\n" or "python\n" before the actual content.
    # Also handles bare "json{" or "json  {" (no separator or whitespace-only separator).
    raw_for_json = raw
    for prefix in LLM_RESPONSE_PREFIXES:
        if raw_for_json.startswith(prefix):
            raw_for_json = raw_for_json[len(prefix):]
            logger.debug(f"Stripped LLM response prefix: {prefix.strip()}")
            break
    else:
        # Fallback: case-insensitive check for a leading "json" token before JSON content
        _lower = raw_for_json.lstrip()
        _token = "json"
        if _lower[:len(_token)].lower() == _token and len(_lower) > len(_token) and _lower[len(_token):].lstrip().startswith("{"):
            raw_for_json = _lower[len(_token):].lstrip()
            logger.debug("Stripped leading 'json' token from LLM response")
    
    # --- 1. Try raw JSON parsing FIRST (before cleaning) ---
    # This handles cases where LLM returns {"files": {...}} without markdown fences
    try:
        payload = json.loads(raw_for_json)
        if isinstance(payload, dict) and isinstance(payload.get("files"), dict):
            logger.info("LLM response is valid JSON (raw parsing)")
            files_obj = payload["files"]
            code_files: Dict[str, str] = {}
            errors: Dict[str, str] = {}
            repair_logs: List[str] = []

            for filename, content in files_obj.items():
                if not isinstance(content, str):
                    errors[filename] = "Content is not a string."
                    continue

                # Content is already per-file extracted from JSON — skip _clean_code_block()
                # to preserve YAML, requirements.txt, Helm templates, etc.
                cleaned = _normalize_file_content(content)

                # Skip Python syntax validation for known non-code file types
                if _is_non_python_artifact(filename, lang):
                    code_files[filename] = cleaned
                    continue
                validation_result = validate_and_repair_syntax(cleaned, lang, filename)

                if validation_result['valid']:
                    # Remove dead/banned imports before storing the file
                    cleaned_code = remove_dead_imports(validation_result['code'], filename)
                    code_files[filename] = cleaned_code
                    
                    # Log if auto-repair was used
                    if validation_result['auto_repaired']:
                        repair_info = f"Auto-repaired {filename}: {', '.join(validation_result['repairs_applied'])}"
                        repair_logs.append(repair_info)
                        logger.info(repair_info)
                else:
                    # Do NOT save syntactically invalid files - add to errors instead
                    # This prevents materialization of broken code that would fail validation
                    logger.error(
                        "Syntax validation failed for '%s' (%s). NOT materializing invalid file. Details: %s",
                        filename,
                        lang,
                        validation_result['error'],
                    )
                    errors[filename] = validation_result['error']

            # If any file failed, emit an aggregated error.txt
            if errors:
                lines = [
                    "One or more files failed syntax validation.",
                    "",
                ]
                for fname, emsg in errors.items():
                    lines.append(f"[{fname}]")
                    lines.append(str(emsg))
                    lines.append("")
                code_files[ERROR_FILENAME] = "\n".join(lines).rstrip()

            if code_files:
                # Perform production-ready validation
                is_production_ready, prod_error = validate_production_ready(code_files)
                if not is_production_ready:
                    logger.warning("Production-ready validation failed: %s", prod_error)
                    # Append to any pre-existing syntax errors rather than overwriting them.
                    existing = code_files.get(ERROR_FILENAME, "")
                    code_files[ERROR_FILENAME] = (
                        existing + ("\n\n" if existing else "") + prod_error
                    )
                else:
                    logger.info("Production-ready validation passed for %d files", len(code_files))

                # Resolve module/package collisions (e.g., routes.py + routes/__init__.py)
                code_files = _detect_module_package_collisions(code_files)

                # Warn about name-shadowing (e.g., handler 'list_products' hides imported name)
                shadow_warnings = _detect_name_shadowing(code_files)
                for w in shadow_warnings:
                    logger.warning("Name shadowing detected: %s", w)

                # Compute validation summary for pipeline gating decisions (Fix 6)
                non_error_files = {k: v for k, v in code_files.items() if k != ERROR_FILENAME}
                files_passed_count = len(non_error_files)
                files_failed_count = len(errors)
                total_count = files_passed_count + files_failed_count
                rejection_rate = files_failed_count / total_count if total_count > 0 else 0.0
                if rejection_rate > MAX_REJECTION_RATE or files_failed_count > MAX_FAILED_FILES_ABSOLUTE:
                    logger.warning(
                        "High file rejection rate: %d/%d files failed (%.0f%%)",
                        files_failed_count, total_count, rejection_rate * 100,
                    )

                # Finalize without __validation_summary__ to avoid polluting code processing
                finalized = _finalize_with_pydantic_v2_validation(code_files)
                # Attach summary after finalization so it is never treated as a code file.
                # Serialised as JSON so consumers can parse it without eval().
                finalized["__validation_summary__"] = json.dumps({
                    "files_passed": files_passed_count,
                    "files_failed": files_failed_count,
                    "rejection_rate": round(rejection_rate, 4),
                    "shadow_warnings": shadow_warnings,
                })
                return finalized
    except json.JSONDecodeError:
        # Not valid JSON, continue with cleaning approach
        logger.debug("Raw JSON parsing failed, trying with code block cleaning")
    
    # --- 2. For robustness, strip outer code fences before JSON parsing attempt ---
    cleaned_for_json = _clean_code_block(raw)
    
    # Strip leading "json" prefix from cleaned content (e.g. when _clean_code_block
    # extracted content from ```json ... ``` fences but left "json\n" prefix)
    if cleaned_for_json:
        _cj = cleaned_for_json.lstrip()
        if len(_cj) > 4 and _cj[:4].lower() == "json" and _cj[4:].lstrip().startswith("{"):
            cleaned_for_json = _cj[4:].lstrip()
            logger.debug("Stripped 'json' prefix from cleaned content before JSON parse")
    
    # --- 3. Try multi-file JSON format after cleaning (only if cleaning produced something) ---
    if cleaned_for_json:
        try:
            payload = json.loads(cleaned_for_json)
            if isinstance(payload, dict) and isinstance(payload.get("files"), dict):
                files_obj = payload["files"]
                code_files: Dict[str, str] = {}
                errors: Dict[str, str] = {}
                repair_logs: List[str] = []

                for filename, content in files_obj.items():
                    if not isinstance(content, str):
                        errors[filename] = "Content is not a string."
                        continue

                    # Content is already per-file extracted from JSON — skip _clean_code_block()
                    # to preserve YAML, requirements.txt, Helm templates, etc.
                    cleaned = _normalize_file_content(content)

                    # Skip Python syntax validation for known non-code file types
                    if _is_non_python_artifact(filename, lang):
                        code_files[filename] = cleaned
                        continue

                    # Infer per-file language from extension instead of using project-level lang
                    file_lang = _infer_language_from_filename(filename, default_lang=lang)
                    
                    # Use validate_and_repair_syntax with per-file language
                    validation_result = validate_and_repair_syntax(cleaned, file_lang, filename)

                    if validation_result['valid']:
                        # Remove dead/banned imports before storing the file
                        cleaned_code = remove_dead_imports(validation_result['code'], filename)
                        code_files[filename] = cleaned_code
                        
                        # Log if auto-repair was used
                        if validation_result['auto_repaired']:
                            repair_info = f"Auto-repaired {filename}: {', '.join(validation_result['repairs_applied'])}"
                            repair_logs.append(repair_info)
                            logger.info(repair_info)
                    else:
                        # Save normalized content anyway for debugging
                        code_files[filename] = cleaned
                        logger.warning(
                            "Syntax validation failed for '%s' (%s). Details: %s",
                            filename,
                            file_lang,
                            validation_result['error'],
                        )
                        errors[filename] = validation_result['error']

                # If any file failed, emit an aggregated error.txt
                if errors:
                    lines = [
                        "One or more files failed syntax validation.",
                        "",
                    ]
                    for fname, emsg in errors.items():
                        lines.append(f"[{fname}]")
                        lines.append(str(emsg))
                        lines.append("")
                    code_files[ERROR_FILENAME] = "\n".join(lines).rstrip()

                if code_files:
                    # Perform production-ready validation
                    is_production_ready, prod_error = validate_production_ready(code_files)
                    if not is_production_ready:
                        logger.warning("Production-ready validation failed for multi-file response")
                        # Append to any pre-existing syntax errors rather than overwriting them.
                        existing = code_files.get(ERROR_FILENAME, "")
                        code_files[ERROR_FILENAME] = (
                            existing + ("\n\n" if existing else "") + prod_error
                        )
                    
                    log_action(
                        "Parsed Multi-File Response",
                        {
                            "files": [f for f in code_files.keys() if f != ERROR_FILENAME],
                            "language": lang,
                            "had_errors": bool(errors),
                            "production_ready": is_production_ready,
                            "auto_repairs": repair_logs if repair_logs else None,
                        },
                    )

                    # Resolve module/package collisions (e.g., routes.py + routes/__init__.py)
                    code_files = _detect_module_package_collisions(code_files)

                    # Warn about name-shadowing (e.g., handler 'list_products' hides imported name)
                    shadow_warnings = _detect_name_shadowing(code_files)
                    for w in shadow_warnings:
                        logger.warning("Name shadowing detected: %s", w)

                    # Compute validation summary for pipeline gating decisions (Fix 6)
                    non_error_files = {k: v for k, v in code_files.items() if k != ERROR_FILENAME}
                    files_passed_count = len(non_error_files)
                    files_failed_count = len(errors)
                    total_count = files_passed_count + files_failed_count
                    rejection_rate = files_failed_count / total_count if total_count > 0 else 0.0
                    if rejection_rate > MAX_REJECTION_RATE or files_failed_count > MAX_FAILED_FILES_ABSOLUTE:
                        logger.warning(
                            "High file rejection rate: %d/%d files failed (%.0f%%)",
                            files_failed_count, total_count, rejection_rate * 100,
                        )

                    # Finalize without __validation_summary__ to avoid polluting code processing
                    finalized = _finalize_with_pydantic_v2_validation(code_files)
                    # Attach summary after finalization so it is never treated as a code file.
                    # Serialised as JSON so consumers can parse it without eval().
                    finalized["__validation_summary__"] = json.dumps({
                        "files_passed": files_passed_count,
                        "files_failed": files_failed_count,
                        "rejection_rate": round(rejection_rate, 4),
                        "shadow_warnings": shadow_warnings,
                    })
                    return finalized
                # If nothing usable, fall through to single-file handling.

        except json.JSONDecodeError:
            logger.info("LLM response is not valid JSON; using single-file fallback.")
        except Exception as e:
            logger.error("Unexpected error while parsing JSON LLM response: %s", e)

    # --- 2. Single-file fallback ---
    cleaned_code = _clean_code_block(raw)
    
    # --- GUARD: Detect JSON file-map blob that would be written as code ---
    # If cleaned_code or raw looks like a JSON {"files": {...}} bundle,
    # attempt one final parse to prevent writing JSON as code into main.py.
    for candidate in (cleaned_code, raw):
        if not candidate:
            continue
        _candidate = candidate.strip()
        # Strip leading "json" prefix if present
        if len(_candidate) > 4 and _candidate[:4].lower() == "json":
            _candidate = _candidate[4:].lstrip()
        if _candidate.startswith("{"):
            try:
                parsed_guard = json.loads(_candidate)
                if isinstance(parsed_guard, dict) and isinstance(parsed_guard.get("files"), dict):
                    files_obj = parsed_guard["files"]
                    # Validate that values are strings
                    valid_files = {
                        k: v for k, v in files_obj.items() if isinstance(v, str)
                    }
                    if valid_files:
                        logger.info(
                            "Guard: recovered %d files from JSON bundle in single-file fallback",
                            len(valid_files)
                        )
                        return _finalize_with_pydantic_v2_validation(valid_files)
            except (json.JSONDecodeError, TypeError):
                pass
    
    # If cleaning resulted in empty string but we have raw content,
    # check if it's likely explanatory prose or actual code
    if not cleaned_code and raw:
        # Check if the raw text contains prose indicators (explanatory text)
        raw_lower = raw.lower()
        PROSE_INDICATORS = (
            'i need', 'i apologize', 'i cannot', "i'm sorry", "i can't",
            'please provide', 'could you', 'would you', 'you need to',
            'more information', 'clarify', 'specify', 'details about',
            'unfortunately', 'however', 'before i can', 'in order to',
            'help me understand', 'not enough details', 'provide enough',
        )
        
        # If raw text contains prose indicators, it's explanatory, not code
        if any(indicator in raw_lower for indicator in PROSE_INDICATORS):
            error_msg = (
                "LLM response did not contain recognizable code. "
                "The response appears to be an explanation or clarification request. "
                f"Response preview: {raw[:200]}..."
            )
            logger.error(error_msg)
            return {ERROR_FILENAME: error_msg}
        
        # Check if the raw text contains recognizable code patterns
        # Limitation: This check may produce false negatives for minimal/unusual code (e.g., Python
        # scripts with only print statements or comments). However, this is an acceptable trade-off
        # as the primary goal is to prevent requirements.txt and curl commands from being treated as code.
        # If no code patterns found, return error instead of treating as code
        if not any(pattern in raw for pattern in _CODE_PATTERNS):
            error_msg = (
                "LLM response did not contain recognizable code patterns. "
                "The response may be requirements.txt content, curl commands, or other non-code text. "
                f"Response preview: {raw[:200]}..."
            )
            logger.error(error_msg)
            return {ERROR_FILENAME: error_msg}
        
        # Otherwise, treat as raw code (legacy behavior for compatibility)
        logger.warning(
            "No code markers found in response after cleaning. "
            "Falling back to using raw response as-is for compatibility. "
            "Preview: %s",
            raw[:150] + "..." if len(raw) > 150 else raw
        )
        log_action(
            "Parsed Single-File Response (Raw Fallback)",
            {"file": DEFAULT_FILENAME, "language": lang, "production_ready": False},
        )
        return _finalize_with_pydantic_v2_validation({DEFAULT_FILENAME: raw})
    
    # Use validate_and_repair_syntax with inferred language for single file
    file_lang = _infer_language_from_filename(DEFAULT_FILENAME, default_lang=lang)
    validation_result = validate_and_repair_syntax(cleaned_code, file_lang, DEFAULT_FILENAME)

    if validation_result['valid']:
        # Use repaired code if auto-repair was applied
        final_code = validation_result['code']
        
        # Perform production-ready validation for single file
        single_file_dict = {DEFAULT_FILENAME: final_code}
        is_production_ready, prod_error = validate_production_ready(single_file_dict)
        
        if not is_production_ready:
            logger.warning("Production-ready validation failed for single-file response")
            return _finalize_with_pydantic_v2_validation({
                DEFAULT_FILENAME: final_code,
                ERROR_FILENAME: prod_error
            })
        
        log_action(
            "Parsed Single-File Response",
            {
                "file": DEFAULT_FILENAME, 
                "language": lang, 
                "production_ready": True,
                "auto_repaired": validation_result['auto_repaired'],
                "repairs_applied": validation_result['repairs_applied'] if validation_result['auto_repaired'] else None,
            },
        )
        return _finalize_with_pydantic_v2_validation({DEFAULT_FILENAME: final_code})

    logger.error(
        "Syntax validation failed for single-file response (%s). Details: %s",
        lang,
        validation_result['error'],
    )
    error_content = (
        f"Generated code failed syntax validation for language '{lang}'.\n\n"
        f"Validation Tool Output:\n{validation_result['error']}"
    )
    return {ERROR_FILENAME: error_content}


