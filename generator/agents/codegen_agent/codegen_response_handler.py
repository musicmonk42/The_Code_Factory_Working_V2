def _classify_stub_module(module_path: str, symbols: Set[str]) -> str:
    """Classify a missing module into a stub category for template selection.

    Inspects both the file-system path (directory components and stem) and the
    set of imported symbols to determine which Jinja2 stub template best
    matches the module's purpose.

    Priority ordering rationale
    ~~~~~~~~~~~~~~~~~~~~~~~~~~~
    1. ``"database"`` — checked first because ``get_db`` / ``async_sessionmaker``
       are unambiguous session-factory signals regardless of path placement.
    2. ``"service"`` — checked before ``"auth"`` so that
       ``app/services/auth.py`` is treated as a service layer file rather than
       an auth module.  Service stubs generate full CRUD async ORM functions.
    3. ``"auth"`` — detected by well-known filename stems (``auth``, ``jwt``,
       ``oauth``, ``security``) or canonical symbol names such as
       ``get_current_user``.
    4. ``"config"`` — detected by filename stems ``config`` / ``settings``.
    5. ``"model"`` — detected by the ``models/`` directory component; schemas/
       directories are excluded to prevent false positives.
    6. ``"schema"`` — detected by the ``schemas/`` directory component.
    7. ``"router"`` — detected by the ``routers/`` / ``routes/`` directory.
    8. Stem-level fallbacks cover flat project layouts that skip sub-directories.
    9. ``"generic"`` — catch-all for anything that does not match above rules.

    Args:
        module_path: Relative file path of the module (e.g. ``"app/auth.py"``).
        symbols:     Set of symbol names imported from the module.  Used for
                     symbol-hint detection when path signals are ambiguous.

    Returns:
        One of the category strings listed above (``"database"``, ``"service"``,
        ``"auth"``, ``"config"``, ``"model"``, ``"schema"``, ``"router"``, or
        ``"generic"``).

    Examples:
        >>> _classify_stub_module("app/database.py", {"get_db", "async_sessionmaker"})
        'database'
        >>> _classify_stub_module("app/services/user_service.py", {"get_user", "create_user"})
        'service'
        >>> _classify_stub_module("app/auth.py", {"get_current_user", "create_access_token"})
        'auth'
        >>> _classify_stub_module("app/config.py", {"Settings"})
        'config'
        >>> _classify_stub_module("app/models/user.py", {"User"})
        'model'
        >>> _classify_stub_module("app/schemas/user.py", {"UserRead", "UserCreate"})
        'schema'
        >>> _classify_stub_module("app/routers/users.py", {"router"})
        'router'
        >>> _classify_stub_module("app/utils.py", {"helper_fn"})
        'generic'
    """
    path_obj = Path(module_path)
    stem = path_obj.stem.lower()
    # Directory components only (exclude filename) for directory-based rules.
    dir_parts = [p.lower() for p in path_obj.parts[:-1]]
    sym_lower = {s.lower() for s in symbols}

    # Priority 1 — Database: unambiguous async session factory symbols.
    if "async_sessionmaker" in sym_lower or "get_db" in sym_lower:
        return "database"

    # Priority 2 — Service: directory named service/services.
    # Checked BEFORE auth so app/services/auth.py → "service", not "auth".
    if any(p in ("service", "services") for p in dir_parts):
        return "service"

    # Priority 3 — Auth: filename stem or directory component is auth-related.
    _auth_names = frozenset({"auth", "authentication", "security", "jwt", "oauth", "token"})
    if stem in _auth_names or any(p in _auth_names for p in dir_parts):
        return "auth"
    # Auth: well-known auth-specific symbol names imported from the module.
    _auth_sym_hints = frozenset({
        "get_current_user", "create_access_token", "decode_token",
        "verify_token", "oauth2_scheme", "authenticate_user",
    })
    if sym_lower & _auth_sym_hints:
        return "auth"

    # Priority 4 — Config: filename stem or directory is config/settings.
    _config_names = frozenset({"config", "settings", "configuration"})
    if stem in _config_names or any(p in _config_names for p in dir_parts):
        return "config"

    # Priority 5 — Model: directory named model/models (but NOT schemas/).
    # The schema exclusion prevents app/models/schema_compat.py mis-classification.
    if any(p in ("model", "models") for p in dir_parts):
        if not any(p in ("schema", "schemas") for p in dir_parts):
            return "model"

    # Priority 6 — Schema: directory named schema/schemas.
    if any(p in ("schema", "schemas") for p in dir_parts):
        return "schema"

    # Priority 7 — Router: directory named router/routers/route/routes.
    if any(p in ("router", "routers", "route", "routes") for p in dir_parts):
        return "router"

    # Priority 8 — Stem-level fallbacks for flat project layouts (no sub-dirs).
    if "service" in stem:
        return "service"
    if stem in _auth_names or any(kw in stem for kw in ("auth", "security", "jwt", "oauth")):
        return "auth"
    if "config" in stem or "setting" in stem:
        return "config"
    if stem == "model" and "schema" not in stem:
        return "model"
    if "schema" in stem:
        return "schema"
    if "router" in stem or stem.startswith("route"):
        return "router"

    # Priority 9 — Generic catch-all.
    return "generic"


# Module-level Jinja2 environment for stub templates (lazy-initialised).
_STUB_TEMPLATE_ENV: Any = None
_STUB_TEMPLATES_DIR = Path(__file__).parent / "templates" / "stubs"


def _render_stub_template(
    category: str,
    module_path: str,
    symbols: Set[str],
) -> Optional[str]:
    """Render a Jinja2 stub template for *category* and return the source string.

    Selects the template file ``<category>_stub.jinja2`` from the
    ``templates/stubs/`` directory (relative to this file).  Falls back to
    ``generic_stub.jinja2`` when no category-specific template exists, so
    every module is guaranteed a usable stub regardless of classification.

    Symbol partitioning
    ~~~~~~~~~~~~~~~~~~~
    Symbols exported from the missing module are partitioned into four
    mutually-exclusive lists that map to Jinja2 template variables:

    * ``class_names``    — uppercase-initial identifiers (classes / enums).
    * ``router_names``   — lowercase names that pass :func:`_is_router_variable`
      (e.g. ``router``, ``api_router``, ``products_router``).
    * ``variable_names`` — lowercase names that pass :func:`_is_likely_variable`
      but are not router variables (e.g. ``db_engine``, ``redis_client``).
    * ``function_names`` — all remaining lowercase names (callables).

    Returns ``None`` when Jinja2 is not installed or template rendering fails
    for any reason, so callers can fall back gracefully to inline stub
    generation.

    Args:
        category:    Stub category string returned by :func:`_classify_stub_module`
                     (``"auth"``, ``"model"``, ``"schema"``, ``"router"``,
                     ``"service"``, ``"config"``, or ``"generic"``).
        module_path: Relative path of the target module file, e.g.
                     ``"app/auth.py"``.  Used to derive ``base_name`` and
                     ``module_name`` template variables.
        symbols:     Set of symbol names that must be exported by the stub.

    Returns:
        Rendered Python source string ready to be written as a ``.py`` file,
        or ``None`` when rendering is unavailable or fails.

    Examples:
        >>> _render_stub_template("generic", "app/utils.py", {"helper"})  # doctest: +SKIP
        'from __future__ import annotations\\n...'
    """
    global _STUB_TEMPLATE_ENV  # noqa: PLW0603
    # Short-circuit: Jinja2 must be installed and the templates directory must exist.
    if not HAS_JINJA2 or not _STUB_TEMPLATES_DIR.is_dir():
        return None
    try:
        # Lazily initialise the shared Jinja2 Environment to avoid repeated
        # FileSystemLoader construction on every call.
        if _STUB_TEMPLATE_ENV is None:
            _STUB_TEMPLATE_ENV = Environment(
                loader=FileSystemLoader(str(_STUB_TEMPLATES_DIR)),
                trim_blocks=True,
                lstrip_blocks=True,
                autoescape=False,
            )
        template_name = f"{category}_stub.jinja2"
        from jinja2 import TemplateNotFound
        try:
            template = _STUB_TEMPLATE_ENV.get_template(template_name)
        except TemplateNotFound:
            # No category-specific template — fall back to generic stub.
            logger.debug(
                "_render_stub_template: no template '%s'; falling back to generic_stub.jinja2",
                template_name,
            )
            template = _STUB_TEMPLATE_ENV.get_template("generic_stub.jinja2")

        # Partition symbols by kind for template context variables.
        # Empty-string guard (``s and``) prevents IndexError on ``s[0]``
        # in the unlikely event that an empty token slips through the parser.
        class_names: List[str] = sorted(s for s in symbols if s and s[0].isupper())
        router_names: List[str] = sorted(
            s for s in symbols if s and not s[0].isupper() and _is_router_variable(s)
        )
        variable_names: List[str] = sorted(
            s for s in symbols
            if s and not s[0].isupper() and not _is_router_variable(s) and _is_likely_variable(s)
        )
        function_names: List[str] = sorted(
            s for s in symbols
            if s and not s[0].isupper() and not _is_router_variable(s)
            and not _is_likely_variable(s)
        )

        # Derive a human-readable base resource name by stripping well-known
        # module-type suffixes (e.g. "product_service" → "product").
        stem = Path(module_path).stem
        base_name = re.sub(
            r"_(service|router|controller|model|schema|view|handler|manager)$",
            "",
            stem,
            flags=re.IGNORECASE,
        )
        module_name = module_path.replace("/", ".").removesuffix(".py")

        rendered: str = template.render(
            class_names=class_names,
            function_names=function_names,
            router_names=router_names,
            variable_names=variable_names,
            base_name=base_name,
            module_name=module_name,
        )
        return rendered
    except Exception as _tmpl_err:
        logger.warning(
            "_render_stub_template: failed to render %s template for %s: %s",
            category,
            module_path,
            _tmpl_err,
        )
        return None


def ensure_local_module_stubs(code_files: Dict[str, str]) -> Dict[str, str]:
    """Scan generated Python files for local module imports and generate stubs.

    Addresses two related issues:

    1. **Missing modules** — if ``routes.py`` imports ``from app.auth import ...``
       but ``app/auth.py`` does not exist in *code_files*, a stub file is
       created with the required symbols.
    2. **Missing symbols** — if the target module file *does* exist in
       *code_files* but is missing the imported symbol (class or function), a
       stub definition is appended to that file.

    Import patterns detected
    ~~~~~~~~~~~~~~~~~~~~~~~~
    * ``from app.X import A, B`` — handled by :data:`_LOCAL_IMPORT_RE`.
    * ``from app import X [as alias]`` followed by ``X.A`` / ``alias.A``
      attribute-access usage — handled by :data:`_APP_SUBMODULE_IMPORT_RE`.
      The ``as``-alias form (e.g. ``from app import schemas as s``) is fully
      supported; ``s.User`` correctly maps to ``app/schemas.py``.

    Third-party, stdlib, and relative imports are intentionally ignored because
    their resolution is context-dependent.

    Name classification heuristic
    ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    An imported name that starts with an uppercase letter is treated as a
    *class*; otherwise it is treated as a *function* returning ``None`` or
    a *variable* (based on known naming conventions such as ``_router``,
    ``_engine``, ``_client`` suffixes).  Variables get a minimal object
    assignment; functions get a ``return None`` body so they don't crash
    immediately at runtime.

    Args:
        code_files: Mapping of relative file paths to source code strings, as
            returned by :func:`parse_llm_response`.  Modified **in-place** and
            also returned so callers can chain the result.

    Returns:
        The (potentially augmented) *code_files* mapping.

    Side-effect:
        Sets the ``_stub_files_created`` attribute on the returned dict to the
        ``set`` of file paths that were created or augmented as stubs during
        this call.  Callers can inspect this set to decide whether a retry
        prompt should list the stub files that need real implementations.
    """
    # Track which files are created/modified as stubs during this call
    stub_files_created: Set[str] = set()
    # First pass: collect all required symbols per module path.
    # Maps "app/auth.py" -> {"get_current_user", "Role", ...}
    required_symbols: Dict[str, Set[str]] = {}

    for _filename, content in list(code_files.items()):
        for match in _LOCAL_IMPORT_RE.finditer(content):
            module_str = match.group(1).strip()
            imports_str = match.group(2).strip()

            # Convert "app.auth" -> "app/auth.py"
            module_path = module_str.replace(".", "/") + ".py"

            # Parse imported names, handling "A, B as b, C" forms.
            # Also skips any token that is not a valid Python identifier
            # (e.g. the opening "(" of a multi-line parenthesised import).
            symbols: Set[str] = set()
            for part in imports_str.split(","):
                name = part.split(" as ")[0].strip()
                if name and name != "*" and name.isidentifier():
                    symbols.add(name)

            if module_path not in required_symbols:
                required_symbols[module_path] = set()
            required_symbols[module_path].update(symbols)

        # Attribute-access scanning pass: detect ``from app import schemas [as s]``
        # followed by attribute-access patterns (``schemas.User`` or ``s.User``) so
        # that symbols reached via module-attribute syntax are also stubbed.
        # This is separate from the ``_LOCAL_IMPORT_RE`` scan above (which handles
        # ``from app.schemas import User``) and runs within the same per-file loop.
        #
        # For each matched token we record both the canonical module name (used to
        # build the ``app/<name>.py`` path) and the local alias actually used in the
        # source (used for the attribute-access scan).  When no ``as`` clause is
        # present the alias equals the module name.
        for mod_match in _APP_SUBMODULE_IMPORT_RE.finditer(content):
            for part in mod_match.group(1).split(","):
                token = part.strip()
                # Handle optional "X as alias" form.
                as_parts = re.split(r"\bas\b", token, maxsplit=1)
                module_name = as_parts[0].strip()
                local_alias = as_parts[1].strip() if len(as_parts) == 2 else module_name

                if not module_name or not module_name.isidentifier():
                    continue
                if not local_alias or not local_alias.isidentifier():
                    local_alias = module_name

                module_path = f"app/{module_name}.py"
                # Scan the entire file for ``<alias>.SomeName`` accesses.
                # The word boundary (\b) prevents false matches on longer names
                # (e.g. ``myschemas.X`` when alias is ``schemas``).
                attr_pattern = re.compile(
                    r"\b" + re.escape(local_alias) + r"\.([A-Za-z_]\w*)"
                )
                for attr_match in attr_pattern.finditer(content):
                    symbol = attr_match.group(1)
                    if symbol.isidentifier():
                        if module_path not in required_symbols:
                            required_symbols[module_path] = set()
                        required_symbols[module_path].add(symbol)

    # Maps module_path -> set of non-pydantic stub class names created in the second pass.
    # Consumed by the third pass to inject discovered instance methods.
    _stub_class_names: Dict[str, Set[str]] = {}

    # Second pass: ensure every required module+symbol exists in code_files.
    for module_path, symbols in required_symbols.items():
        if not symbols:
            continue

        if module_path not in code_files:
            # Module file is entirely missing — generate a stub.
            # Determine up-front whether any router-pattern symbols are present
            # so the APIRouter import can be emitted once at the module header.
            pass  # fall through to stub generation below
        elif not _is_stub_content(code_files[module_path]):
            # Fix 2: file already has real (non-stub) content — do NOT overwrite it.
            logger.info(
                "ensure_local_module_stubs: skipping %s — file already has non-stub content (%d bytes)",
                module_path, len(code_files[module_path]),
            )
            continue

        if module_path not in code_files:
            # Module file is entirely missing — generate a stub.
            # Determine up-front whether any router-pattern symbols are present
            # so the APIRouter import can be emitted once at the module header.
            has_router_in_symbols = any(
                _is_router_variable(s) for s in symbols if s and not s[0].isupper()
            )
            # Issue 1 fix: distinguish ORM model files from Pydantic schema files.
            # Files under models/ (but not schemas/) should use SQLAlchemy Base,
            # not pydantic.BaseModel, because service layers use ORM patterns.
            _is_sqlalchemy_module = "/models/" in module_path and "/schemas/" not in module_path
            # Pydantic is appropriate only for schemas/ files or suffix-matched names.
            _is_pydantic_module = (
                "schemas" in module_path
                or (not _is_sqlalchemy_module and "/models/" in module_path)
            )
            has_pydantic_class = any(
                sym[0].isupper() and (
                    _is_pydantic_module
                    or any(sym.endswith(sfx) for sfx in _PYDANTIC_CLASS_SUFFIXES)
                )
                for sym in symbols
            ) and not _is_sqlalchemy_module
            has_sqlalchemy_class = _is_sqlalchemy_module and any(
                sym[0].isupper() for sym in symbols
            )
            # Detect database modules by the presence of async session-factory symbols.
            _is_database_module = "async_sessionmaker" in symbols or "get_db" in symbols
            if _is_database_module:
                # Emit a self-contained, production-ready async database module that:
                #  • Reads DATABASE_URL from the environment (twelve-factor config)
                #  • Uses the modern SQLAlchemy 2.x async_sessionmaker API
                #  • Exposes a FastAPI-compatible `get_db` dependency
                #  • Provides a shared declarative Base for ORM model inheritance
                #
                # This template intentionally does NOT contain the canonical
                # "Generated module — replace with actual implementation." marker
                # so that it passes the stub-detection checks in
                # `_detect_stub_patterns` / `validate_production_ready`.
                db_stub = (
                    '"""Database configuration and async session management.\n\n'
                    "This module is auto-generated by The Code Factory and is safe\n"
                    "to use as-is for development and lightweight production workloads.\n\n"
                    "Configuration\n"
                    "-------------\n"
                    "DATABASE_URL (env var):\n"
                    "    Any SQLAlchemy-compatible async connection string.\n"
                    "    Default: ``sqlite+aiosqlite:///./app.db`` (for local development).\n"
                    "    Production example: ``postgresql+asyncpg://user:pw@host/db``\n"
                    '"""\n'
                    "from __future__ import annotations\n\n"
                    "import os\n"
                    "from typing import AsyncGenerator\n\n"
                    "from sqlalchemy.ext.asyncio import (\n"
                    "    AsyncSession,\n"
                    "    async_sessionmaker,\n"
                    "    create_async_engine,\n"
                    ")\n"
                    "from sqlalchemy.orm import DeclarativeBase\n\n"
                    "# ---------------------------------------------------------------------------\n"
                    "# Engine & session factory\n"
                    "# ---------------------------------------------------------------------------\n"
                    'DATABASE_URL: str = os.getenv(\n'
                    '    "DATABASE_URL", "sqlite+aiosqlite:///./app.db"\n'
                    ")\n\n"
                    "engine = create_async_engine(\n"
                    "    DATABASE_URL,\n"
                    "    echo=os.getenv('SQL_ECHO', 'false').lower() == 'true',\n"
                    ")\n\n"
                    "AsyncSessionLocal: async_sessionmaker[AsyncSession] = async_sessionmaker(\n"
                    "    engine,\n"
                    "    expire_on_commit=False,\n"
                    ")\n\n\n"
                    "# ---------------------------------------------------------------------------\n"
                    "# Declarative base — import this in all ORM models\n"
                    "# ---------------------------------------------------------------------------\n"
                    "class Base(DeclarativeBase):\n"
                    "    pass\n\n\n"
                    "# ---------------------------------------------------------------------------\n"
                    "# FastAPI dependency\n"
                    "# ---------------------------------------------------------------------------\n"
                    "async def get_db() -> AsyncGenerator[AsyncSession, None]:\n"
                    '    """Yield a transactional async database session.\n\n'
                    "    Usage::\n\n"
                    "        @router.get('/')\n"
                    "        async def endpoint(db: AsyncSession = Depends(get_db)):\n"
                    "            ...\n"
                    '    """\n'
                    "    async with AsyncSessionLocal() as session:\n"
                    "        try:\n"
                    "            yield session\n"
                    "            await session.commit()\n"
                    "        except Exception:\n"
                    "            await session.rollback()\n"
                    "            raise\n"
                )
                code_files[module_path] = db_stub
                stub_files_created.add(module_path)
                logger.info(
                    "ensure_local_module_stubs: created database module %s with symbols %s",
                    module_path,
                    sorted(symbols),
                )
                continue

            # ------------------------------------------------------------------
            # Target 1: Try Jinja2 template for well-known module categories.
            # Categories "service" and "generic" keep the legacy inline approach
            # so that the third-pass method-injection regex continues to work.
            # ------------------------------------------------------------------
            _stub_category = _classify_stub_module(module_path, symbols)
            if _stub_category not in ("service", "generic", "database"):
                _rendered = _render_stub_template(_stub_category, module_path, symbols)
                if _rendered is not None:
                    code_files[module_path] = _rendered
                    stub_files_created.add(module_path)
                    logger.info(
                        "ensure_local_module_stubs: created %s stub module %s"
                        " with symbols %s (template)",
                        _stub_category,
                        module_path,
                        sorted(symbols),
                    )
                    continue

            stub_lines = [
                '"""Generated module — replace with actual implementation."""\n',
                "from typing import Any\n",
            ]
            if has_sqlalchemy_class:
                stub_lines.append("from sqlalchemy import Column, Integer, String\n")
                stub_lines.append("from app.database import Base\n")
            elif has_pydantic_class:
                stub_lines.append("from pydantic import BaseModel\n")
            stub_lines.append("\n")
            for sym in sorted(symbols):
                # Uppercase initial → class; router variable → None assignment;
                # other known variable suffixes → None variable;
                # otherwise → function returning None.
                if sym[0].isupper():
                    if has_sqlalchemy_class and _is_sqlalchemy_module:
                        # Issue 1: generate a proper SQLAlchemy ORM stub.
                        tablename = _pluralize_tablename(sym)
                        stub_lines.append(
                            f"class {sym}(Base):\n"
                            f"    __tablename__ = '{tablename}'\n"
                            f"    id = Column(Integer, primary_key=True, autoincrement=True)\n"
                            f"    name = Column(String, nullable=False)\n\n\n"
                        )
                    else:
                        # For generic modules, always generate minimal bare class stubs.
                        # For other categories, check Pydantic suffix patterns.
                        _sym_is_pydantic = _stub_category != "generic" and (
                            _is_pydantic_module or any(
                                sym.endswith(sfx) for sfx in _PYDANTIC_CLASS_SUFFIXES
                            )
                        )
                        if _sym_is_pydantic:
                            stub_lines.append(
                                f"class {sym}(BaseModel):\n"
                                f'    """Stub Pydantic model."""\n'
                                f"    pass\n\n\n"
                            )
                        else:
                            stub_lines.append(
                                f"class {sym}:\n"
                                f'    """Stub class."""\n'
                                f"    pass\n\n\n"
                            )
                            _stub_class_names.setdefault(module_path, set()).add(sym)
                elif _is_router_variable(sym):
                    stub_lines.append(
                        f"{sym} = None\n\n\n"
                    )
                elif _is_likely_variable(sym):
                    stub_lines.append(
                        f"{sym} = None  # Placeholder — assign actual value\n\n\n"
                    )
                else:
                    stub_lines.append(
                        f"def {sym}(*args: Any, **kwargs: Any) -> Any:\n"
                        f'    """Placeholder implementation."""\n'
                        f"    return None\n\n\n"
                    )
            code_files[module_path] = "".join(stub_lines)
            stub_files_created.add(module_path)
            logger.info(
                "ensure_local_module_stubs: created stub module %s with symbols %s",
                module_path,
                sorted(symbols),
            )
        else:
            # Module exists — identify and append any missing symbols.
            existing_content = code_files[module_path]
            try:
                tree = ast.parse(existing_content)
                # Collect names from function/class definitions at any nesting depth.
                defined_names: Set[str] = {
                    node.name
                    for node in ast.walk(tree)
                    if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef))
                }
                # Also collect top-level variable assignment names so that existing
                # module-level variables (e.g. ``db_engine = ...``) are not re-stubbed.
                for node in ast.iter_child_nodes(tree):
                    if isinstance(node, ast.Assign):
                        for target in node.targets:
                            if isinstance(target, ast.Name):
                                defined_names.add(target.id)
                    elif isinstance(node, ast.AnnAssign) and isinstance(node.target, ast.Name):
                        defined_names.add(node.target.id)
            except SyntaxError:
                defined_names = set()

            missing = symbols - defined_names
            if missing:
                # Emit the APIRouter import once at the top of the appended block
                # if any missing symbol needs it.
                has_router_missing = any(
                    _is_router_variable(sym)
                    for sym in missing
                    if not sym[0].isupper()
                )
                # Issue 1 fix: distinguish ORM model files from Pydantic schema files.
                _is_sqlalchemy_module = "/models/" in module_path and "/schemas/" not in module_path
                _is_pydantic_module = (
                    "schemas" in module_path
                    or (not _is_sqlalchemy_module and "/models/" in module_path)
                )
                has_sqlalchemy_missing = _is_sqlalchemy_module and any(
                    sym[0].isupper() for sym in missing
                )
                has_pydantic_missing = not has_sqlalchemy_missing and any(
                    sym[0].isupper() and (
                        _is_pydantic_module
                        or any(sym.endswith(sfx) for sfx in _PYDANTIC_CLASS_SUFFIXES)
                    )
                    for sym in missing
                )
                appended_lines = ["\n\n# Supplemental symbols appended by module-stub pass\n"]
                if has_sqlalchemy_missing and "from app.database import Base" not in existing_content:
                    appended_lines.append("from sqlalchemy import Column, Integer, String\n")
                    appended_lines.append("from app.database import Base\n")
                if has_pydantic_missing and "from pydantic import BaseModel" not in existing_content:
                    appended_lines.append("from pydantic import BaseModel\n")
                if has_router_missing and "from fastapi import APIRouter" not in existing_content:
                    appended_lines.append("from fastapi import APIRouter\n")
                for sym in sorted(missing):
                    if sym[0].isupper():
                        if has_sqlalchemy_missing and _is_sqlalchemy_module:
                            tablename = _pluralize_tablename(sym)
                            appended_lines.append(
                                f"\nclass {sym}(Base):\n"
                                f"    __tablename__ = '{tablename}'\n"
                                f"    id = Column(Integer, primary_key=True, autoincrement=True)\n"
                                f"    name = Column(String, nullable=False)\n"
                            )
                        else:
                            _sym_is_pydantic = _is_pydantic_module or any(
                                sym.endswith(sfx) for sfx in _PYDANTIC_CLASS_SUFFIXES
                            )
                            if _sym_is_pydantic:
                                appended_lines.append(
                                    f"\nclass {sym}(BaseModel):\n"
                                    f'    """Stub Pydantic model."""\n'
                                    f"    pass\n"
                                )
                            else:
                                appended_lines.append(
                                    f"\nclass {sym}:\n"
                                    f'    """Stub class."""\n'
                                    f"    pass\n"
                                )
                                _stub_class_names.setdefault(module_path, set()).add(sym)
                    elif _is_router_variable(sym):
                        appended_lines.append(
                            f"\n{sym} = APIRouter()\n"
                        )
                    elif _is_likely_variable(sym):
                        appended_lines.append(
                            f"\n{sym} = None  # Placeholder — assign actual value\n"
                        )
                    else:
                        appended_lines.append(
                            f"\ndef {sym}(*args: Any, **kwargs: Any) -> Any:\n"
                            f'    """Placeholder implementation."""\n'
                            f"    return None\n"
                        )
                code_files[module_path] = existing_content + "".join(appended_lines)
                stub_files_created.add(module_path)
                logger.info(
                    "ensure_local_module_stubs: appended stubs for %s to %s",
                    sorted(missing),
                    module_path,
                )

    # Third pass: detect methods referenced on instances of stub classes and inject
    # placeholder method stubs so that attribute access does not raise AttributeError.
    # Example: ``auth_service = AuthService()`` followed by
    # ``Depends(auth_service.get_current_user)`` in a router file requires that
    # ``AuthService`` exposes a ``get_current_user`` method.
    #
    # This pass is intentionally non-fatal: a scan/injection failure must never
    # prevent the caller from receiving the already-valid code_files dict.
    if _stub_class_names:
        try:
            # Build reverse mapping: class_name -> module_path.
            # Guard: skip if all stub class sets were empty (avoids empty-alternation regex).
            _class_to_module: Dict[str, str] = {
                _cls: _mod_path
                for _mod_path, _cls_set in _stub_class_names.items()
                for _cls in _cls_set
            }
            if _class_to_module:
                # Pattern to find ``var = ClassName()`` instantiation sites.
                _cls_alt = "|".join(re.escape(c) for c in sorted(_class_to_module))
                _instantiation_re = re.compile(
                    r"^[ \t]*([A-Za-z_]\w*)\s*=\s*(" + _cls_alt + r")\s*\(",
                    re.MULTILINE,
                )
                # Maps variable_name -> (class_name, module_path)
                _instance_vars: Dict[str, Tuple[str, str]] = {}
                for _content in code_files.values():
                    for _m in _instantiation_re.finditer(_content):
                        _instance_vars[_m.group(1)] = (
                            _m.group(2), _class_to_module[_m.group(2)]
                        )

                if _instance_vars:
                    # Maps (module_path, class_name) -> {method_name: is_async}
                    _methods_to_inject: Dict[Tuple[str, str], Dict[str, bool]] = {}
                    for _var, (_cls, _mod_path) in _instance_vars.items():
                        _ev = re.escape(_var)
                        _depends_re = re.compile(
                            r"\bDepends\(\s*" + _ev + r"\.([A-Za-z_]\w*)\s*\)"
                        )
                        _await_re = re.compile(
                            r"\bawait\s+" + _ev + r"\.([A-Za-z_]\w*)\b"
                        )
                        _attr_re = re.compile(
                            r"\b" + _ev + r"\.([A-Za-z_]\w*)\b"
                        )
                        _key: Tuple[str, str] = (_mod_path, _cls)
                        _async_meths: Set[str] = set()
                        _all_meths: Set[str] = set()
                        for _fc in code_files.values():
                            for _am in _depends_re.finditer(_fc):
                                _async_meths.add(_am.group(1))
                                _all_meths.add(_am.group(1))
                            for _am in _await_re.finditer(_fc):
                                _async_meths.add(_am.group(1))
                                _all_meths.add(_am.group(1))
                            for _am in _attr_re.finditer(_fc):
                                _all_meths.add(_am.group(1))
                        if _key not in _methods_to_inject:
                            _methods_to_inject[_key] = {}
                        for _meth in _all_meths:
                            # Skip dunder attributes: they have special Python semantics
                            # and must never be shadowed by a generic stub method.
                            if _meth.startswith("__"):
                                continue
                            _is_async = _meth in _async_meths
                            if _meth in _methods_to_inject[_key]:
                                # async wins when the same method is discovered from
                                # multiple instance variables or call contexts.
                                _methods_to_inject[_key][_meth] = (
                                    _methods_to_inject[_key][_meth] or _is_async
                                )
                            else:
                                _methods_to_inject[_key][_meth] = _is_async

                    # Inject discovered methods into stub class bodies.
                    for (_mod_path, _cls), _methods in _methods_to_inject.items():
                        if not _methods or _mod_path not in code_files:
                            continue
                        _method_lines: List[str] = []
                        for _meth in sorted(_methods):
                            _prefix = "async " if _methods[_meth] else ""
                            # Each element is a complete method definition without a
                            # trailing blank line; elements are joined with a single
                            # blank line (PEP 8 convention for methods inside a class).
                            _method_lines.append(
                                f"    {_prefix}def {_meth}(self, *args, **kwargs):\n"
                                f'        raise NotImplementedError("Stub: implement {_meth}")'
                            )
                        # One blank line between methods; trailing newline closes the body.
                        _method_body = "\n\n".join(_method_lines) + "\n"
                        _stub_cls_re = re.compile(
                            r"(class\s+" + re.escape(_cls) + r"\s*:\s*\n"
                            r'\s*"""Stub class\."""\s*\n)'
                            r"(\s*pass\b[^\n]*)",
                            re.MULTILINE,
                        )
                        _new_content = _stub_cls_re.sub(
                            lambda m, _b=_method_body: m.group(1) + _b,
                            code_files[_mod_path],
                            count=1,
                        )
                        if _new_content != code_files[_mod_path]:
                            code_files[_mod_path] = _new_content
                            logger.info(
                                "ensure_local_module_stubs: injected %d method(s) %s "
                                "into %s.%s",
                                len(_methods),
                                sorted(_methods),
                                _mod_path,
                                _cls,
                            )
        except Exception as _third_pass_err:
            # Non-fatal: a scan/injection failure must never discard the already-
            # valid code_files dict produced by the first two passes.
            logger.warning(
                "ensure_local_module_stubs: third pass (method injection) failed "
                "(non-fatal): %s",
                _third_pass_err,
            )

    if stub_files_created:
        logger.info(
            "ensure_local_module_stubs: %d stub file(s) created/modified: %s",
            len(stub_files_created),
            sorted(stub_files_created),
        )
    return code_files


# Marker present in the docstring header of every auto-generated stub module.
_STUB_MODULE_MARKER = "Generated module — replace with actual implementation."


def get_stub_files(code_files: Dict[str, str]) -> Set[str]:
    """Return the set of stub files present in *code_files*.

    A file is considered a stub when its content contains the canonical stub
    module header emitted by :func:`ensure_local_module_stubs`.

    Args:
        code_files: Mapping of filename → source code.

    Returns:
        Set of filenames whose content identifies them as auto-generated stubs.
    """
    return {
        path
        for path, content in code_files.items()
        if _STUB_MODULE_MARKER in content
    }


def build_stub_retry_prompt_hint(code_files: Dict[str, str]) -> str:
    """Build a prompt hint listing stub files that need real implementations.

    Scans *code_files* for auto-generated stubs (files identified by
    :func:`get_stub_files`) and returns a formatted string suitable for
    inclusion in a retry LLM prompt.  Returns an empty string when no stubs
    are detected, allowing callers to skip the retry pass entirely.

    Template rendering path
    ~~~~~~~~~~~~~~~~~~~~~~~
    When Jinja2 is available and ``stub_retry_hint.jinja2`` exists in the
    ``templates/stubs/`` directory:

    1. Each stub file's exported symbols are extracted via :mod:`ast` parsing.
    2. Stubs are classified into categories using :func:`_classify_stub_module`
       and grouped into a ``{category: [(path, sym_list, content), ...]}`` dict.
    3. The dict is passed as ``grouped_stubs`` to ``stub_retry_hint.jinja2``,
       which renders a structured, per-category prompt section.
    4. If the template is missing or rendering fails, the function falls back
       to a plain-text list (plain-text path described below).

    Plain-text fallback path
    ~~~~~~~~~~~~~~~~~~~~~~~~
    When Jinja2 is unavailable or the template is not found, produces a simple
    bullet-point list in the form::

        IMPORTANT: The following files are placeholder stubs ...
          - app/auth.py: must export get_current_user, Role
          ...
        Generate complete, production-ready implementations ...

    Args:
        code_files: Mapping of relative filename → Python source code for all
                    files in the current generation result.

    Returns:
        A formatted hint string listing stub files and the symbols they export,
        or ``""`` when no stubs remain in *code_files*.
    """
    stub_files = get_stub_files(code_files)
    if not stub_files:
        return ""

    def _extract_symbols(content: str) -> List[str]:
        """Extract top-level function, class, and variable names from *content*."""
        try:
            tree = ast.parse(content)
            syms = [
                node.name
                for node in ast.walk(tree)
                if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef))
            ]
            # Also collect module-level simple assignments (e.g. ``router = APIRouter()``).
            for node in ast.iter_child_nodes(tree):
                if isinstance(node, ast.Assign):
                    for target in node.targets:
                        if isinstance(target, ast.Name):
                            syms.append(target.id)
            return sorted(set(syms))
        except SyntaxError:
            return []

    # Group stub files by module type for per-category template sections.
    grouped: Dict[str, List[Tuple[str, str, str]]] = {}
    for path in sorted(stub_files):
        content = code_files[path]
        syms = _extract_symbols(content)
        sym_list = ", ".join(syms) if syms else "(unknown symbols)"
        category = _classify_stub_module(path, set(syms))
        grouped.setdefault(category, []).append((path, sym_list, content))

    # Prefer Jinja2 template rendering for richer, per-category guidance.
    if HAS_JINJA2 and _STUB_TEMPLATES_DIR.is_dir():
        try:
            # Reuse the shared environment when already initialised.
            if _STUB_TEMPLATE_ENV is not None:
                env = _STUB_TEMPLATE_ENV
            else:
                env = Environment(
                    loader=FileSystemLoader(str(_STUB_TEMPLATES_DIR)),
                    trim_blocks=True,
                    lstrip_blocks=True,
                    autoescape=False,
                )
            from jinja2 import TemplateNotFound
            try:
                template = env.get_template("stub_retry_hint.jinja2")
                return template.render(grouped_stubs=grouped)
            except TemplateNotFound:
                # Template absent — fall through to plain-text fallback below.
                pass
        except Exception as _tmpl_err:
            logger.debug("build_stub_retry_prompt_hint: template render failed: %s", _tmpl_err)

    # Plain-text fallback: simple bullet list comprehensible by any LLM.
    lines = [
        "IMPORTANT: The following files are placeholder stubs that MUST be replaced "
        "with real implementations:",
    ]
    for path in sorted(stub_files):
        content = code_files[path]
        syms = _extract_symbols(content)
        sym_list = ", ".join(syms) if syms else "(unknown symbols)"
        lines.append(f"  - {path}: must export {sym_list}")
    lines.append(
        "Generate complete, production-ready implementations for each stub file listed above."
    )
    return "\n".join(lines)
