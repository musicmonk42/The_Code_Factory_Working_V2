# Pre-compiled pattern for stripping markdown fences wrapping entire file content
_FENCE_PATTERN = re.compile(
    r"^```(?:python|py|json|dockerfile|yaml|toml|bash|sh|text)?\s*\n(.*?)```\s*$",
    re.DOTALL | re.IGNORECASE,
)

# Pre-compiled pattern for matching local "from app.X import ..." statements.
# Group 1: the dotted module path (e.g. "app.auth")
# Group 2: the imported names string (e.g. "get_current_user, Role")
_LOCAL_IMPORT_RE = re.compile(
    r"^\s*from\s+(app(?:\.[a-zA-Z_][a-zA-Z0-9_]*)+)"
    r"\s+import\s+(.+)$",
    re.MULTILINE,
)

# Pre-compiled pattern for "from app import X [as alias], Y [as alias2], ..." statements.
# This catches attribute-access style imports where the LLM writes
# ``from app import schemas`` and then uses ``schemas.User``.
# Group 1: the raw imports string (e.g. "schemas, models as m")
# NOTE: Multi-line parenthesised forms are deliberately excluded here (the opening
# ``(`` would be the only captured token and is filtered out by ``isidentifier()``),
# consistent with the treatment in ``_LOCAL_IMPORT_RE``.
_APP_SUBMODULE_IMPORT_RE = re.compile(
    r"^\s*from\s+app\s+import\s+(.+)$",
    re.MULTILINE,
)


def _normalize_file_content(content: str) -> str:
    """
    Normalize file content from LLM responses before syntax validation.

    Handles common issues where LLM output contains escaped characters
    that are literal two-character sequences instead of real control chars:
      - ``\\n`` (backslash + n) → real newline
      - ``\\t`` (backslash + t) → real tab
      - ``\\r\\n`` → real newline
      - Stray backslash at line endings (line continuation characters)
    Also strips:
      - Leading BOM (U+FEFF)
      - Markdown fences (```python ... ```)
    """
    if not content:
        return content

    # Strip BOM
    content = content.lstrip("\ufeff")

    # Unescape literal two-char sequences that should be real control chars.
    # Order matters: \\r\\n before \\n to avoid partial replacement.
    content = content.replace("\\r\\n", "\n")
    content = content.replace("\\n", "\n")
    content = content.replace("\\t", "\t")

    # Handle stray backslashes at line endings (line continuation character errors)
    # This fixes the common LLM artifact where backslashes are added at line ends
    # Pattern: backslash followed by newline (literal line continuation)
    # We replace it with just a newline to remove the continuation character
    content = content.replace("\\\n", "\n")
    # Also handle Windows line endings
    content = content.replace("\\\r\n", "\n")

    # Strip markdown fences if the entire content is wrapped in them
    m = _FENCE_PATTERN.match(content.strip())
    if m:
        content = m.group(1)

    return content


