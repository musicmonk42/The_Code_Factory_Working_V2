DEFAULT_FILENAME = "main.py"
ERROR_FILENAME = "error.txt"

# Very lightweight heuristic: any assignment to a suspicious name is flagged.
SECRET_REGEX = re.compile(
    r"(api_key|apikey|secret|token|password)\s*=",
    re.IGNORECASE,
)

# Language-specific comment styles for traceability
COMMENT_STYLES: Dict[str, Tuple[str, str]] = {
    # lang or extension : (start, end)
    "python": ("#", ""),
    "py": ("#", ""),
    "javascript": ("//", ""),
    "js": ("//", ""),
    "typescript": ("//", ""),
    "ts": ("//", ""),
    "java": ("//", ""),
    "csharp": ("//", ""),
    "cs": ("//", ""),
    "go": ("//", ""),
    "rust": ("//", ""),
    "rs": ("//", ""),
    "ruby": ("#", ""),
    "rb": ("#", ""),
    "php": ("//", ""),
    "swift": ("//", ""),
    "kotlin": ("//", ""),
    "kt": ("//", ""),
    "css": ("/*", "*/"),
    "sql": ("--", ""),
}

DEFAULT_COMMENT_STYLE = ("#", "")


