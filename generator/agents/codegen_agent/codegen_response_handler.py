@lru_cache(maxsize=256)
def _infer_language_from_filename(filename: str, default_lang: str = "python") -> str:
    """
    Infer the programming language from a filename's extension.
    
    This is used to determine the appropriate syntax validator when the
    language is not explicitly provided or when validating multi-file responses.
    
    Industry Standards:
        - Follows GitHub Linguist language detection
        - Results cached for performance (LRU cache)
        - Thread-safe caching implementation
        - Input validation for security
    
    Args:
        filename: The filename to analyze (can include path)
        default_lang: Default language to return if extension is unknown
        
    Returns:
        Inferred language name (e.g., 'python', 'javascript', 'java')
        
    Raises:
        TypeError: If filename is not a string
        ValueError: If filename is empty
        
    Examples:
        >>> _infer_language_from_filename("main.py")
        'python'
        >>> _infer_language_from_filename("app.js")
        'javascript'
        >>> _infer_language_from_filename("README.md")
        'markdown'
    """
    # Input validation - defensive programming
    if not isinstance(filename, str):
        raise TypeError(f"filename must be a string, got {type(filename).__name__}")
    
    filename = filename.strip()
    if not filename:
        raise ValueError("filename cannot be empty")
    
    # Security: use basename only to avoid path traversal influencing detection
    basename = os.path.basename(filename)
    
    # Extract extension (case-insensitive)
    ext = os.path.splitext(basename)[1].lstrip(".").lower()
    
    # Return detected language or default
    return _EXTENSION_TO_LANGUAGE.get(ext, default_lang)


@lru_cache(maxsize=256)
def _should_skip_syntax_validation(filename: str) -> bool:
    """
    Determine if a file should skip syntax validation.
    
    Documentation files, configuration files, and other non-code files
    should not be validated as source code.
    
    Industry Standards:
        - Distinguishes data/config from executable code
        - O(1) lookup performance using frozenset
        - Results cached for performance
        - Input validation for security
    
    Args:
        filename: The filename to check
        
    Returns:
        True if syntax validation should be skipped
        
    Raises:
        TypeError: If filename is not a string
        ValueError: If filename is empty
        
    Examples:
        >>> _should_skip_syntax_validation("README.md")
        True
        >>> _should_skip_syntax_validation("config.yaml")
        True
        >>> _should_skip_syntax_validation("main.py")
        False
    """
    # Input validation
    if not isinstance(filename, str):
        raise TypeError(f"filename must be a string, got {type(filename).__name__}")
    
    filename = filename.strip()
    if not filename:
        raise ValueError("filename cannot be empty")
    
    # Get inferred language and check against non-code set
    try:
        lang = _infer_language_from_filename(filename, default_lang="")
        return lang in _NON_CODE_LANGUAGES
    except Exception as e:
        # Log error but default to not skipping (validate for safety)
        logger.error(
            "Error determining if validation should be skipped for '%s': %s. Defaulting to validate.",
            filename,
            e,
            exc_info=True
        )
        return False


def _validate_syntax(code: str, lang: str, filename: str) -> Tuple[bool, str]:
    """
    Validates code syntax using language-appropriate mechanisms.
    
    Industry Standards:
        - Input validation and type checking
        - File type detection to skip non-code files
        - Language inference from filename
        - Comprehensive error messages
        - Graceful degradation when tools unavailable
    
    Args:
        code: The code content to validate
        lang: The programming language
        filename: The filename (used for language detection and context)

    Returns:
        Tuple of (is_valid, message):
            - is_valid: True if syntactically valid or validation skipped
            - message: Empty if valid, error description otherwise
            
    Raises:
        TypeError: If inputs are not of expected types
        
    Examples:
        >>> _validate_syntax("def hello(): pass", "python", "main.py")
        (True, "")
        >>> _validate_syntax("# Documentation", "python", "README.md")
        (True, "Skipped validation for markdown file")
    """
    # Input validation - industry standard defensive programming
    if not isinstance(code, str):
        raise TypeError(f"code must be a string, got {type(code).__name__}")
    
    if not isinstance(lang, str):
        raise TypeError(f"lang must be a string, got {type(lang).__name__}")
    
    if not isinstance(filename, str):
        raise TypeError(f"filename must be a string, got {type(filename).__name__}")
    
    # Check if this file should skip validation (e.g., README.md, config files)
    try:
        if _should_skip_syntax_validation(filename):
            detected_type = _infer_language_from_filename(filename, default_lang="unknown")
            logger.debug(
                "Skipping syntax validation for %s (detected as %s file)",
                filename,
                detected_type
            )
            return True, f"Skipped validation for {detected_type} file"
    except (TypeError, ValueError):
        # Re-raise validation errors
        raise
    except Exception as e:
        # Log unexpected errors but continue with validation for safety
        logger.warning(
            "Error checking if validation should be skipped for '%s': %s. Proceeding with validation.",
            filename,
            e
        )
    
    # Infer language from filename if not provided or if it's generic
    try:
        inferred_lang = _infer_language_from_filename(filename, default_lang="python")
        
        # Use inferred language if original lang is empty or generic
        if not lang or lang.lower() in ("", "text", "plain"):
            lang = inferred_lang
            logger.debug(
                "Inferred language '%s' from filename '%s'",
                lang,
                filename
            )
    except Exception as e:
        logger.warning(
            "Could not infer language from filename '%s': %s. Using provided lang '%s'.",
            filename,
            e,
            lang
        )
    
    lang_l = (lang or "").lower()

    # Validate that code is not empty
    if not code.strip():
        logger.warning("Empty code block for %s; treating as error.", filename)
        return False, (
            "Empty code block received - no valid code generated. "
            "This may indicate the LLM returned explanatory text instead of code. "
            "Try providing more specific requirements or examples."
        )

    # --- Python ---
    if lang_l in ("python", "py"):
        try:
            compile(code, filename, "exec")
            return True, ""
        except SyntaxError as e:
            msg = f"{e.__class__.__name__}: {e}"
            logger.error(
                "Error during python syntax validation for %s: %s",
                filename,
                msg,
            )
            return False, msg

    # --- JavaScript / TypeScript ---
    if lang_l in ("javascript", "js", "typescript", "ts"):
        if not _is_tool_available("node"):
            logger.info("Node.js not available; skipping JS/TS syntax validation.")
            return True, "node not available; skipped."

        try:
            with tempfile.NamedTemporaryFile("w", suffix=".js", delete=False) as tmp:
                tmp.write(code)
                tmp_path = tmp.name
            # node --check <file>
            proc = subprocess.run(
                ["node", "--check", tmp_path],
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                text=True,
            )
            os.unlink(tmp_path)
            if proc.returncode == 0:
                return True, ""
            return False, proc.stderr.strip() or proc.stdout.strip()
        except Exception as e:
            logger.error(
                "Error during JS/TS syntax validation for %s: %s",
                filename,
                e,
            )
            return False, str(e)

    # --- Java ---
    if lang_l == "java":
        if not _is_tool_available("javac"):
            logger.info("javac not available; skipping Java syntax validation.")
            return True, "javac not available; skipped."

        try:
            with tempfile.TemporaryDirectory() as tmpdir:
                src_path = os.path.join(tmpdir, filename or "Main.java")
                with open(src_path, "w", encoding="utf-8") as f:
                    f.write(code)
                proc = subprocess.run(
                    ["javac", src_path],
                    stdout=subprocess.PIPE,
                    stderr=subprocess.PIPE,
                    text=True,
                )
                if proc.returncode == 0:
                    return True, ""
                return False, proc.stderr.strip() or proc.stdout.strip()
        except Exception as e:
            logger.error(
                "Error during Java syntax validation for %s: %s",
                filename,
                e,
            )
            return False, str(e)

    # --- Go ---
    if lang_l == "go":
        if not _is_tool_available("go"):
            logger.info("Go toolchain not available; skipping Go syntax validation.")
            return True, "go not available; skipped."

        try:
            with tempfile.TemporaryDirectory() as tmpdir:
                src_path = os.path.join(tmpdir, filename or "main.go")
                with open(src_path, "w", encoding="utf-8") as f:
                    f.write(code)
                proc = subprocess.run(
                    ["go", "build", src_path],
                    stdout=subprocess.PIPE,
                    stderr=subprocess.PIPE,
                    text=True,
                )
                if proc.returncode == 0:
                    return True, ""
                return False, proc.stderr.strip() or proc.stdout.strip()
        except Exception as e:
            logger.error(
                "Error during Go syntax validation for %s: %s",
                filename,
                e,
            )
            return False, str(e)

    # --- Fallback for unsupported languages ---
    logger.info(
        "No specific syntax validator for language '%s'; treating '%s' as valid.",
        lang,
        filename,
    )
    return True, "No validator for this language; skipped."


def validate_and_repair_syntax(code: str, language: str, filename: str) -> Dict[str, Any]:
    """
    Validate syntax and attempt auto-repair if validation fails.
    
    This function implements a two-phase approach:
    1. First, try validating the original code
    2. If validation fails, attempt auto-repair and validate again
    
    Args:
        code: The source code to validate
        language: Programming language (e.g., "python", "javascript")
        filename: Name of the file (for context in error messages)
    
    Returns:
        Dictionary with keys:
            - 'valid': bool - Whether the code is syntactically valid
            - 'code': str - Original or repaired code
            - 'error': str or None - Error message if validation failed
            - 'repairs_applied': List[str] - List of repairs that were applied
            - 'auto_repaired': bool - Whether auto-repair was needed and successful
    """
    # Try original code first
    is_valid, error_msg = _validate_syntax(code, language, filename)
    
    if is_valid:
        return {
            'valid': True,
            'code': code,
            'error': None,
            'repairs_applied': [],
            'auto_repaired': False
        }
    
    # Validation failed - try auto-repair
    logger.info(f"Syntax validation failed for {filename}. Attempting auto-repair...")
    
    repair_result = SyntaxAutoRepair.auto_repair(code, language)
    
    if repair_result['was_modified']:
        logger.info(f"Applied {len(repair_result['fixes_applied'])} auto-repairs: {repair_result['fixes_applied']}")
        
        # Validate repaired code
        is_valid_repaired, error_msg_repaired = _validate_syntax(
            repair_result['repaired_code'], 
            language, 
            filename
        )
        
        if is_valid_repaired:
            logger.info(f"✓ Auto-repair successful for {filename}")
            return {
                'valid': True,
                'code': repair_result['repaired_code'],
                'error': None,
                'repairs_applied': repair_result['fixes_applied'],
                'auto_repaired': True
            }
        else:
            logger.warning(f"Auto-repair failed to fix {filename}: {error_msg_repaired}")
    else:
        logger.info("No repairs were applicable for the detected syntax errors")
    
    # Return original validation error
    return {
        'valid': False,
        'code': code,
        'error': error_msg,
        'repairs_applied': [],
        'auto_repaired': False
    }


