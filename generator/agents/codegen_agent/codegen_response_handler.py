# --- Security and Monitoring ---
# ==============================================================================

def monitor_and_scan_code(code_files: Dict[str, str]) -> Dict[str, str]:
    """
    Runs lightweight secret detection and delegates to a unified SAST scanner.

    - Does NOT mutate code_files.
    - Emits audit events via log_action.
    """
    # 1. Secret heuristics
    for filename, code in code_files.items():
        if SECRET_REGEX.search(code):
            log_action(
                "Secret Scan Finding",
                {
                    "file": filename,
                    "detail": "Potential secret-like assignment detected "
                              "(heuristic: sensitive variable name).",
                },
            )

    # 2. Unified SAST via scan_for_vulnerabilities
    try:
        results = scan_for_vulnerabilities(code_files)
        total_issues = 0
        findings_summary: Dict[str, int] = {}

        if isinstance(results, dict):
            for fname, info in results.items():
                issues = None
                if isinstance(info, dict):
                    issues = info.get("issues")
                if isinstance(issues, list):
                    count = len(issues)
                    if count:
                        findings_summary[fname] = count
                        total_issues += count

        if total_issues:
            logger.warning(
                "Unified SAST scan completed. Total issues found: %s",
                total_issues,
            )
        else:
            logger.info("Unified SAST scan completed. No issues found.")

        log_action(
            "Unified SAST Scan Complete",
            {
                "total_issues": total_issues,
                "files_scanned": len(code_files),
                "finding_summary": findings_summary,
            },
        )

    except Exception as e:
        logger.error("Error during unified SAST scan: %s", e)
        log_action(
            "Unified SAST Scan Error",
            {
                "error": str(e),
                "files_scanned": len(code_files),
            },
        )

    return code_files


