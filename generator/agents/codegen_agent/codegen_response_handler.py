def validate_pydantic_v2_compatibility(files: Dict[str, str]) -> List[str]:
    """
    Validate that generated code uses Pydantic v2 syntax.

    Checks for deprecated Pydantic v1 imports and patterns that will fail
    in a Pydantic v2 environment.

    Args:
        files: Dictionary mapping filenames to file contents.

    Returns:
        List of validation error messages (empty if all files are valid).
    """
    errors: List[str] = []
    requirements_content = files.get("requirements.txt", "")

    for filename, content in files.items():
        if not filename.endswith(".py"):
            continue

        # Check for deprecated BaseSettings import location
        if "from pydantic import BaseSettings" in content:
            errors.append(
                f"{filename}: Uses deprecated 'from pydantic import BaseSettings'. "
                f"Use 'from pydantic_settings import BaseSettings' instead (Pydantic v2)."
            )

        has_deprecated_validators = False
        has_deprecated_class_config = False
        try:
            tree = ast.parse(content)
            for node in ast.walk(tree):
                if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
                    for decorator in node.decorator_list:
                        decorator_node = decorator.func if isinstance(decorator, ast.Call) else decorator
                        decorator_name = None
                        if isinstance(decorator_node, ast.Name):
                            decorator_name = decorator_node.id
                        elif isinstance(decorator_node, ast.Attribute):
                            decorator_name = decorator_node.attr
                        if decorator_name in {"validator", "root_validator"}:
                            has_deprecated_validators = True
                            break

                if isinstance(node, ast.ClassDef):
                    base_names = [
                        base.id if isinstance(base, ast.Name)
                        else base.attr if isinstance(base, ast.Attribute)
                        else None
                        for base in node.bases
                    ]
                    if "BaseModel" in base_names and any(
                        isinstance(child, ast.ClassDef) and child.name == "Config"
                        for child in node.body
                    ):
                        has_deprecated_class_config = True
        except SyntaxError:
            # If parsing fails, fall back to string checks.
            has_deprecated_validators = "@validator(" in content or "@root_validator(" in content
            has_deprecated_class_config = "class Config:" in content and "BaseModel" in content

        # Check for deprecated Pydantic v1 validators
        if has_deprecated_validators:
            errors.append(
                f"{filename}: Uses deprecated Pydantic v1 validator decorators. "
                f"Use '@field_validator' or '@model_validator' (Pydantic v2)."
            )

        # Check for deprecated Pydantic v1 config style
        if has_deprecated_class_config:
            errors.append(
                f"{filename}: Uses deprecated 'class Config:' on a Pydantic model. "
                f"Use 'model_config = {{...}}' instead (Pydantic v2)."
            )

        # Check that pydantic-settings is declared when BaseSettings is used
        if "BaseSettings" in content and "pydantic-settings" not in requirements_content:
            errors.append(
                f"{filename}: Uses BaseSettings but 'pydantic-settings' is not listed "
                f"in requirements.txt. Add 'pydantic-settings>=2.0.0'."
            )

        # Check for Pydantic v1 regex= kwarg in Field() or constr() (renamed to pattern= in v2).
        # The pattern handles one level of nested parens in default values (e.g. Field(default=f(), regex=...))
        # while avoiding false positives in unrelated calls like pandas str.contains(regex=True).
        import re as _re_check
        if _re_check.search(
            r'\b(?:Field|constr)\s*\((?:[^()]|\([^)]*\))*\bregex\s*=',
            content, _re_check.DOTALL
        ):
            errors.append(
                f"{filename}: Uses Pydantic v1 'regex=' keyword argument in Field() or constr(). "
                f"Use 'pattern=' instead (Pydantic v2)."
            )

    return errors


def _fix_class_config_to_model_config(content: str) -> str:
    """
    Transform 'class Config:' nested inside BaseModel/BaseSettings subclasses
    into 'model_config = ConfigDict(...)' assignments (Pydantic v2 style).

    Returns the transformed content string (unchanged if no transformation is needed).
    """
    import re as _re

    # Pydantic v2 renames for known Config attributes
    _ATTR_RENAMES = {
        "orm_mode": "from_attributes",
        "schema_extra": "json_schema_extra",
    }

    try:
        tree = ast.parse(content)
    except SyntaxError:
        return content

    lines = content.splitlines(keepends=True)

    # Collect all class Config AST nodes nested inside BaseModel/BaseSettings classes
    config_nodes = []
    for node in ast.walk(tree):
        if isinstance(node, ast.ClassDef):
            base_names = [
                base.id if isinstance(base, ast.Name)
                else base.attr if isinstance(base, ast.Attribute)
                else None
                for base in node.bases
            ]
            if "BaseModel" in base_names or "BaseSettings" in base_names:
                for child in node.body:
                    if isinstance(child, ast.ClassDef) and child.name == "Config":
                        config_nodes.append(child)

    if not config_nodes:
        return content

    # Sort in reverse order to replace from bottom to top (preserve line numbers)
    config_nodes.sort(key=lambda n: n.lineno, reverse=True)

    changed = False
    for config_node in config_nodes:
        # Extract key=value pairs from the Config class body
        kwargs = []
        for stmt in config_node.body:
            if isinstance(stmt, ast.Assign):
                for target in stmt.targets:
                    if isinstance(target, ast.Name):
                        attr_name = _ATTR_RENAMES.get(target.id, target.id)
                        try:
                            attr_val = ast.unparse(stmt.value)
                        except AttributeError:
                            # Python < 3.9 fallback: handle simple constants
                            if isinstance(stmt.value, ast.Constant):
                                attr_val = repr(stmt.value.value)
                            elif hasattr(ast, "NameConstant") and isinstance(stmt.value, ast.NameConstant):
                                attr_val = repr(stmt.value.value)
                            else:
                                attr_val = "..."
                        kwargs.append(f"{attr_name}={attr_val}")

        # Determine indentation of 'class Config:' line (1-indexed → 0-indexed)
        config_line_idx = config_node.lineno - 1
        config_line_text = lines[config_line_idx]
        indent_str = config_line_text[: len(config_line_text) - len(config_line_text.lstrip())]

        # Build the replacement line
        kwargs_str = ", ".join(kwargs)
        replacement = f"{indent_str}model_config = ConfigDict({kwargs_str})\n"

        # Replace lines from config_node.lineno to config_node.end_lineno (inclusive, 1-indexed)
        start_idx = config_node.lineno - 1  # inclusive, 0-indexed
        end_idx = config_node.end_lineno    # exclusive, 0-indexed
        lines[start_idx:end_idx] = [replacement]
        changed = True

    if not changed:
        return content

    result = "".join(lines)

    # Add 'ConfigDict' to existing pydantic import or insert a new import line
    if not _re.search(r'from\s+pydantic\s+import\b[^\n]*\bConfigDict\b', result):
        if _re.search(r'from\s+pydantic\s+import\s+', result):
            result = _re.sub(
                r'(from\s+pydantic\s+import\s+)([^\n]+)',
                lambda m: m.group(1) + m.group(2).rstrip() + ", ConfigDict",
                result,
                count=1,
            )
        else:
            result = "from pydantic import ConfigDict\n" + result

    return result


def auto_fix_pydantic_v1_imports(files: Dict[str, str]) -> Dict[str, str]:
    """
    Automatically fix common Pydantic v1 → v2 migration issues in generated files.

    Fixes applied:
    - `from pydantic import BaseSettings` → `from pydantic_settings import BaseSettings`
    - `.dict()` method calls → `.model_dump()` (Pydantic v2)
    - `Field(..., example=...)` → `Field(..., json_schema_extra={"example": ...})` (Pydantic v2)
    - V1 error type strings in test assertions → V2 equivalents
    - `pydantic==1.x` pin in requirements.txt → `pydantic>=2.0`
    - Adds `pydantic-settings>=2.0.0` to requirements.txt when BaseSettings is used.
    - `class Config:` inside BaseModel/BaseSettings subclasses → `model_config = ConfigDict(...)`

    Args:
        files: Dictionary mapping filenames to file contents.

    Returns:
        Updated files dictionary with fixes applied.
    """
    import re as _re

    # V1 → V2 error type string mappings used in test assertions
    _V1_TO_V2_ERROR_TYPES: Dict[str, str] = {
        "value_error.number.not_gt": "greater_than",
        "value_error.number.not_ge": "greater_than_equal",
        "value_error.number.not_lt": "less_than",
        "value_error.number.not_le": "less_than_equal",
        "value_error.missing": "missing",
        "value_error.str.regex": "string_pattern_mismatch",
        "value_error.any_str.min_length": "string_too_short",
        "value_error.any_str.max_length": "string_too_long",
        "value_error.list.min_items": "too_short",
        "value_error.list.max_items": "too_long",
        "type_error.integer": "int_type",
        "type_error.float": "float_type",
        "type_error.str": "string_type",
        "type_error.bool": "bool_type",
        "type_error.none.not_allowed": "none_required",
    }

    fixed_files = dict(files)

    # Determine whether any Python file uses BaseSettings
    uses_base_settings = any(
        "BaseSettings" in content
        for fname, content in fixed_files.items()
        if fname.endswith(".py")
    )

    for filename in list(fixed_files.keys()):
        content = fixed_files[filename]

        if filename.endswith(".py"):
            # Fix: from pydantic import BaseSettings → from pydantic_settings import BaseSettings
            if "from pydantic import BaseSettings" in content:
                content = content.replace(
                    "from pydantic import BaseSettings",
                    "from pydantic_settings import BaseSettings",
                )
                logger.info("auto_fix_pydantic_v1_imports: fixed BaseSettings import in %s", filename)

            # Fix: .dict() → .model_dump() (Pydantic v2 rename)
            # Only replace .dict() call patterns, not method definitions or unrelated uses
            if ".dict(" in content:
                content = _re.sub(r'\.dict\(', '.model_dump(', content)
                logger.info("auto_fix_pydantic_v1_imports: replaced .dict() with .model_dump() in %s", filename)

            # Fix: Field(..., example=...) → Field(..., json_schema_extra={"example": ...})
            # Match Field calls with an 'example' keyword argument not already inside json_schema_extra
            def _fix_field_example(m: "re.Match[str]") -> str:
                full_match = m.group(0)
                # Skip if already using json_schema_extra
                if "json_schema_extra" in full_match:
                    return full_match
                example_val = m.group(1)
                replacement = full_match.replace(
                    f", example={example_val}",
                    f', json_schema_extra={{"example": {example_val}}}',
                )
                return replacement

            if "example=" in content and "Field(" in content:
                content = _re.sub(
                    r'Field\([^)]*,\s*example=([^,)]+)[^)]*\)',
                    _fix_field_example,
                    content,
                )
                logger.info("auto_fix_pydantic_v1_imports: replaced Field(example=) in %s", filename)

            # Fix: Field(..., regex=...) → Field(..., pattern=...) and constr(regex=...) → constr(pattern=...)
            # The 'regex' kwarg was renamed to 'pattern' in Pydantic v2.
            # The pattern handles one level of nested parens in default values
            # (e.g. Field(default=func(), regex=...)) while avoiding false positives
            # in unrelated calls such as pandas str.contains(regex=True).
            if _re.search(
                r'\b(?:Field|constr)\s*\((?:[^()]|\([^)]*\))*\bregex\s*=',
                content, _re.DOTALL
            ):
                content = _re.sub(
                    r'(\b(?:Field|constr)\s*\((?:[^()]|\([^)]*\))*)\bregex\s*=',
                    lambda m: m.group(1) + 'pattern=',
                    content,
                    flags=_re.DOTALL,
                )
                logger.info("auto_fix_pydantic_v1_imports: replaced regex= with pattern= in %s", filename)

            # Fix: V1 error type strings in test assertions → V2 equivalents
            for v1_type, v2_type in _V1_TO_V2_ERROR_TYPES.items():
                if v1_type in content:
                    content = content.replace(v1_type, v2_type)
                    logger.info(
                        "auto_fix_pydantic_v1_imports: replaced error type '%s' → '%s' in %s",
                        v1_type, v2_type, filename,
                    )

            # Fix: @root_validator → @model_validator(mode='before')
            # Must run before @validator fix to avoid partial matches
            if "@root_validator" in content:
                # Fix import: standalone 'root_validator' token → 'model_validator'
                # Use word boundaries to avoid matching 'custom_root_validator' etc.
                content = _re.sub(
                    r'(from\s+pydantic\s+import\s+[^\n]*)\broot_validator\b',
                    lambda m: m.group(1) + "model_validator",
                    content,
                )
                # Replace @root_validator(pre=True) or @root_validator(pre=False) → @model_validator(mode='before')
                content = _re.sub(
                    r'@root_validator\([^)]*\)',
                    "@model_validator(mode='before')",
                    content,
                )
                # Replace bare @root_validator → @model_validator(mode='before')
                content = _re.sub(
                    r'@root_validator\b(?!\s*\()',
                    "@model_validator(mode='before')",
                    content,
                )
                logger.info(
                    "auto_fix_pydantic_v1_imports: replaced @root_validator with @model_validator in %s",
                    filename,
                )

            # Fix: @validator(...) → @field_validator(..., mode='before')
            if _re.search(r'@validator\s*\(', content):
                # Fix import: standalone 'validator' token → 'field_validator'
                # Use word boundary to avoid matching 'root_validator', 'field_validator' etc.
                content = _re.sub(
                    r'(from\s+pydantic\s+import\s+[^\n]*)\bvalidator\b(?!_)',
                    lambda m: m.group(1) + "field_validator",
                    content,
                )
                # Replace @validator(...) → @field_validator(..., mode='before')
                def _upgrade_validator(m: "re.Match[str]") -> str:
                    inner = m.group(1)
                    # If pre=True is already present, remove it and add mode='before'
                    if "pre=True" in inner:
                        inner = _re.sub(r',\s*pre=True|pre=True,?\s*', "", inner)
                        inner = inner.strip().rstrip(",")
                        return f"@field_validator({inner}, mode='before')"
                    elif "mode=" not in inner:
                        return f"@field_validator({inner}, mode='before')"
                    return f"@field_validator({inner})"

                content = _re.sub(r'@validator\s*\(([^)]*)\)', _upgrade_validator, content)
                logger.info(
                    "auto_fix_pydantic_v1_imports: replaced @validator with @field_validator in %s",
                    filename,
                )

            # Fix: Remove always=True/False from @field_validator calls (Pydantic V1 arg, not valid in V2)
            if _re.search(r'@field_validator\s*\([^)]*always\s*=\s*(?:True|False)', content):
                def _strip_always(m: "re.Match[str]") -> str:
                    # Remove ", always=True", "always=True," or "always=True" (standalone)
                    result = _re.sub(r',\s*always\s*=\s*(?:True|False)', '', m.group(0))
                    result = _re.sub(r'always\s*=\s*(?:True|False)\s*,\s*', '', result)
                    result = _re.sub(r'always\s*=\s*(?:True|False)', '', result)
                    # Clean up artifacts like "(," or ", )"
                    result = _re.sub(r'\(\s*,\s*', '(', result)
                    result = _re.sub(r',\s*\)', ')', result)
                    return result

                content = _re.sub(r'@field_validator\s*\([^)]*\)', _strip_always, content)
                logger.info(
                    "auto_fix_pydantic_v1_imports: removed always=True/False from @field_validator in %s",
                    filename,
                )

            # Fix: class Config: inside BaseModel/BaseSettings → model_config = ConfigDict(...)
            if "class Config:" in content and ("BaseModel" in content or "BaseSettings" in content):
                new_content = _fix_class_config_to_model_config(content)
                if new_content != content:
                    content = new_content
                    logger.info(
                        "auto_fix_pydantic_v1_imports: replaced class Config: with model_config = ConfigDict() in %s",
                        filename,
                    )

            fixed_files[filename] = content

        elif filename == "requirements.txt":
            # Upgrade pydantic==1.x pin to pydantic>=2.0
            if _re.search(r'pydantic\s*==\s*1\.', content):
                content = _re.sub(r'pydantic\s*==\s*1\.[^\s\n]*', 'pydantic>=2.0', content)
                logger.info(
                    "auto_fix_pydantic_v1_imports: upgraded pydantic v1 pin to pydantic>=2.0 in requirements.txt"
                )

            if uses_base_settings and "pydantic-settings" not in content:
                content = content.rstrip("\n") + "\npydantic-settings>=2.0.0\n"
                logger.info(
                    "auto_fix_pydantic_v1_imports: added pydantic-settings to requirements.txt"
                )
            fixed_files[filename] = content

    if uses_base_settings and "requirements.txt" not in fixed_files:
        fixed_files["requirements.txt"] = "pydantic-settings>=2.0.0\n"
        logger.info(
            "auto_fix_pydantic_v1_imports: created requirements.txt with pydantic-settings"
        )

    # Populate requirements.txt with third-party imports extracted from generated code
    fixed_files = extract_and_populate_requirements(fixed_files)
    return fixed_files


def auto_fix_settings_instantiation(files: Dict[str, str]) -> Dict[str, str]:
    """
    Convert module-level ``settings = Settings()`` patterns to a lazy ``get_settings()``
    function backed by ``@lru_cache``.

    Module-level instantiation of ``Settings()`` causes Pydantic ``ValidationError``
    at import time when required env vars are absent (e.g. during cold-start validation).
    The lazy pattern defers instantiation until the settings are actually needed.

    Fixes applied:
    - ``settings = Settings()`` at module level → ``@lru_cache() / get_settings()``
    - Adds ``from functools import lru_cache`` import when not already present
    - Adds safe ``Field(default=...)`` values for common env-dependent fields that have
      no default (see :data:`_SETTINGS_SAFE_DEFAULTS`).
    - Adds ``from pydantic import Field`` import when ``Field`` defaults are injected
      and the import is not already present.

    Args:
        files: Dictionary mapping filenames to file contents.

    Returns:
        Updated files dictionary with fixes applied.
    """
    # Compiled patterns reused across all files in this call.
    _module_level_pat = re.compile(
        r'^(settings\s*=\s*Settings\(\)\s*(?:#.*)?)$', re.MULTILINE
    )
    # Matches an existing `from functools import lru_cache` (with or without other names).
    _lru_import_pat = re.compile(
        r'^\s*from\s+functools\s+import\b[^\n]*\blru_cache\b', re.MULTILINE
    )
    # Matches an existing `from pydantic import ... Field ...`
    _field_import_pat = re.compile(
        r'^\s*from\s+pydantic\s+import\b[^\n]*\bField\b', re.MULTILINE
    )

    fixed_files: Dict[str, str] = {}
    for filename, content in files.items():
        if not filename.endswith(".py"):
            fixed_files[filename] = content
            continue

        if "BaseSettings" not in content:
            fixed_files[filename] = content
            continue

        original = content

        # ------------------------------------------------------------------
        # Step 1: Add safe defaults for common required fields missing a
        # default inside any BaseSettings subclass.
        # ------------------------------------------------------------------
        # We use a simple line-by-line state machine rather than a full AST
        # parse so that this fixer works on syntactically incomplete files.
        lines = content.splitlines(keepends=True)
        new_lines: List[str] = []
        in_settings_class = False
        field_default_added = False
        for line in lines:
            # Enter a BaseSettings subclass (handles dotted base names like
            # ``pydantic_settings.BaseSettings``).
            if re.match(r'^class\s+\w+\s*\(.*BaseSettings.*\)\s*:', line):
                in_settings_class = True
            elif in_settings_class and re.match(r'^class\s+', line):
                # A new top-level class definition ends the settings context.
                in_settings_class = False

            if in_settings_class:
                for field_name, safe_default in _SETTINGS_SAFE_DEFAULTS.items():
                    # Pattern: `    field_name: SomeType` with no `=` before any comment.
                    _ann_pat = (
                        r'^(\s+' + re.escape(field_name) + r'\s*:\s*\S+)'
                        r'(\s*(?:#.*)?)$'
                    )
                    # Guard: skip if the code portion (before any comment) already has `=`.
                    _code_part = line.split('#')[0]
                    if re.match(_ann_pat, line.rstrip('\n')) and '=' not in _code_part:
                        line = re.sub(
                            _ann_pat,
                            lambda m, d=safe_default: (
                                m.group(1) + f' = Field(default="{d}")' + m.group(2)
                            ),
                            line.rstrip('\n'),
                        ) + '\n'
                        field_default_added = True
                        logger.info(
                            "auto_fix_settings_instantiation: added default for '%s' in %s",
                            field_name,
                            filename,
                        )
                        break
            new_lines.append(line)
        content = "".join(new_lines)

        # Ensure `from pydantic import Field` is present when we injected Field().
        if field_default_added and not _field_import_pat.search(content):
            if "from pydantic import" in content:
                # Extend the existing pydantic import line.
                content = re.sub(
                    r'(from pydantic import )([^\n]+)',
                    lambda m: (
                        m.group(0)
                        if "Field" in m.group(2)
                        else m.group(1) + m.group(2).rstrip() + ", Field"
                    ),
                    content,
                    count=1,
                )
            else:
                # Inject a brand-new import right after any pydantic_settings import,
                # or prepend to the file if no pydantic import exists yet.
                if "from pydantic_settings import" in content:
                    content = re.sub(
                        r'(from pydantic_settings import [^\n]+\n)',
                        r'\1from pydantic import Field\n',
                        content,
                        count=1,
                    )
                else:
                    content = "from pydantic import Field\n" + content
            logger.info(
                "auto_fix_settings_instantiation: added 'from pydantic import Field' in %s",
                filename,
            )

        # ------------------------------------------------------------------
        # Step 2: Replace module-level ``settings = Settings()`` with a lazy
        # ``get_settings()`` function guarded by ``@lru_cache``.
        # ------------------------------------------------------------------
        if _module_level_pat.search(content):
            # Determine BEFORE the substitution whether an lru_cache import
            # already exists.  The replacement text itself contains the token
            # "lru_cache", so any post-substitution check would be a false
            # positive and the import would never be added (dead code).
            _needs_lru_import = not bool(_lru_import_pat.search(content))

            _lazy_replacement = (
                "@lru_cache()\n"
                "def get_settings() -> Settings:\n"
                "    return Settings()\n\n"
                "settings = get_settings()"
            )
            content = _module_level_pat.sub(_lazy_replacement, content)
            logger.info(
                "auto_fix_settings_instantiation: replaced module-level Settings() "
                "with lazy get_settings() in %s",
                filename,
            )

            # Add the lru_cache import only when it was absent before the sub.
            if _needs_lru_import:
                if "from functools import" in content:
                    # Extend the existing functools import line.
                    content = re.sub(
                        r'(from functools import )([^\n]+)',
                        lambda m: (
                            m.group(0)
                            if "lru_cache" in m.group(2)
                            else m.group(1) + m.group(2).rstrip() + ", lru_cache"
                        ),
                        content,
                        count=1,
                    )
                elif "from __future__" in content:
                    # Insert after the `from __future__` line.
                    content = re.sub(
                        r'(from __future__[^\n]+\n)',
                        r'\1from functools import lru_cache\n',
                        content,
                        count=1,
                    )
                else:
                    content = "from functools import lru_cache\n" + content
                logger.info(
                    "auto_fix_settings_instantiation: added "
                    "'from functools import lru_cache' in %s",
                    filename,
                )

        fixed_files[filename] = content

    return fixed_files


def _infer_comment_style(lang: str, filename: str) -> Tuple[str, str]:
    """
    Infers the appropriate comment delimiters based on language or file extension.
    """
    lang_l = (lang or "").lower()
    if lang_l in COMMENT_STYLES:
        return COMMENT_STYLES[lang_l]

    ext = os.path.splitext(filename)[1].lstrip(".").lower()
    return COMMENT_STYLES.get(ext, DEFAULT_COMMENT_STYLE)


def add_traceability_comments(
    code_files: Dict[str, str],
    requirements: Dict[str, Any],
    lang: str,
) -> Dict[str, str]:
    """
    Annotates files with traceability information linking code to requirements.

    Rules:
    - For JSON files:
        - If requirement phrases appear, add a `_traceability` map with REQ IDs.
    - For code files:
        - If requirement phrases appear in file, prepend a comment header:
            CODE TRACEABILITY
            This file implements:
              - REQ-001: ...
    """
    features = [
        f
        for f in (requirements.get("features") or [])
        if isinstance(f, str) and f.strip()
    ]

    # Build stable IDs for each feature
    req_map: Dict[str, str] = {
        f"REQ-{i+1:03d}": text for i, text in enumerate(features)
    }

    updated: Dict[str, str] = {}

    for filename, content in code_files.items():
        # JSON: attach structured metadata
        if filename.lower().endswith(".json"):
            try:
                data = json.loads(content)
            except Exception:
                updated[filename] = content
                continue

            serialized_lower = json.dumps(data).lower()
            trace: Dict[str, str] = {}
            for rid, text in req_map.items():
                if text.lower() in serialized_lower:
                    trace[rid] = text

            if trace and isinstance(data, dict):
                data["_traceability"] = trace
                updated[filename] = json.dumps(data, indent=2)
            else:
                updated[filename] = content
            continue

        # Code files: prepend header if any requirements match
        lower_content = content.lower()
        matched: Dict[str, str] = {
            rid: text for rid, text in req_map.items() if text.lower() in lower_content
        }

        if not matched:
            updated[filename] = content
            continue

        start, end = _infer_comment_style(lang, filename)

        header_lines = [
            f"{start} CODE TRACEABILITY",
            f"{start} This file implements the following requirements:",
        ]
        for rid, text in matched.items():
            header_lines.append(f"{start}  - {rid}: {text}")
        if end:
            header_lines.append(end)

        header_block = "\n".join(header_lines)
        updated[filename] = f"{header_block}\n\n{content}"

    return updated


