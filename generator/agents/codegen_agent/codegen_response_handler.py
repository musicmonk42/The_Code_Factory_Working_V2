def parse_llm_response(response: Union[str, Dict[str, Any]], lang: str = "python") -> Dict[str, str]:
    """
    Parses the LLM response, handling both multi-file JSON and single-file code.

    Multi-file mode expects:
        {
          "files": {
            "main.py": "<code>",
            "util.py": "<code>"
          }
        }

    Behavior:
    - For multi-file JSON:
        - Each file is cleaned and syntax-validated.
        - Valid files are returned as-is.
        - Invalid files are summarized into ERROR_FILENAME.
    - For non-JSON:
        - Treat as a single file; validate.
        - Return DEFAULT_FILENAME or ERROR_FILENAME accordingly.
    
    Args:
        response: Either a string (legacy behavior) or a dict (OpenAI API response format)
        lang: Target programming language for syntax validation
    
    Returns:
        Dictionary mapping filenames to code content
    """
    # Handle dict response from OpenAI API
    if isinstance(response, dict):
        # Extract content from OpenAI chat completion response
        raw = ''
        try:
            # Try OpenAI chat completion format first
            if 'choices' in response and response['choices']:
                raw = response['choices'][0].get('message', {}).get('content', '')
        except (KeyError, TypeError):
            # KeyError: unexpected dict structure
            # TypeError: choices is not subscriptable or message is not a dict
            pass
        
        # Fallback: try to get 'content' or 'text' directly if extraction failed
        if not raw:
            raw = response.get('content', response.get('text', ''))
        
        logger.debug("Extracted content from dict response, length: %d", len(raw) if raw else 0)
    else:
        raw = response
    
    raw = raw.strip() if raw else ''
    
    # Log raw response length for debugging
    logger.debug(
        "Processing LLM response - length: %d chars, first 100: %s",
        len(raw),
        raw[:100] if raw else "(empty)"
    )
    
    # For robustness, strip outer code fences before JSON parsing attempt.
    cleaned_for_json = _clean_code_block(raw)
    
    # --- 1. Try multi-file JSON format (only if cleaning produced something) ---
    if cleaned_for_json:
        try:
            payload = json.loads(cleaned_for_json)
            if isinstance(payload, dict) and isinstance(payload.get("files"), dict):
                files_obj = payload["files"]
                code_files: Dict[str, str] = {}
                errors: Dict[str, str] = {}
                repair_logs: List[str] = []

                for filename, content in files_obj.items():
                    if not isinstance(content, str):
                        errors[filename] = "Content is not a string."
                        continue

                    cleaned = _clean_code_block(content)
                    
                    # Use validate_and_repair_syntax instead of _validate_syntax
                    validation_result = validate_and_repair_syntax(cleaned, lang, filename)

                    if validation_result['valid']:
                        code_files[filename] = validation_result['code']
                        
                        # Log if auto-repair was used
                        if validation_result['auto_repaired']:
                            repair_info = f"Auto-repaired {filename}: {', '.join(validation_result['repairs_applied'])}"
                            repair_logs.append(repair_info)
                            logger.info(repair_info)
                    else:
                        logger.warning(
                            "Syntax validation failed for '%s' (%s). Details: %s",
                            filename,
                            lang,
                            validation_result['error'],
                        )
                        errors[filename] = validation_result['error']

                # If any file failed, emit an aggregated error.txt
                if errors:
                    lines = [
                        "One or more files failed syntax validation.",
                        "",
                    ]
                    for fname, emsg in errors.items():
                        lines.append(f"[{fname}]")
                        lines.append(str(emsg))
                        lines.append("")
                    code_files[ERROR_FILENAME] = "\n".join(lines).rstrip()

                if code_files:
                    # Perform production-ready validation
                    is_production_ready, prod_error = validate_production_ready(code_files)
                    if not is_production_ready:
                        logger.warning("Production-ready validation failed for multi-file response")
                        code_files[ERROR_FILENAME] = prod_error
                    
                    log_action(
                        "Parsed Multi-File Response",
                        {
                            "files": [f for f in code_files.keys() if f != ERROR_FILENAME],
                            "language": lang,
                            "had_errors": bool(errors),
                            "production_ready": is_production_ready,
                            "auto_repairs": repair_logs if repair_logs else None,
                        },
                    )
                    return code_files
                # If nothing usable, fall through to single-file handling.

        except json.JSONDecodeError:
            logger.info("LLM response is not valid JSON; using single-file fallback.")
        except Exception as e:
            logger.error("Unexpected error while parsing JSON LLM response: %s", e)

    # --- 2. Single-file fallback ---
    cleaned_code = _clean_code_block(raw)
    
    # If cleaning resulted in empty string but we have raw content,
    # check if it's likely explanatory prose or actual code
    if not cleaned_code and raw:
        # Check if the raw text contains prose indicators (explanatory text)
        raw_lower = raw.lower()
        PROSE_INDICATORS = (
            'i need', 'i apologize', 'i cannot', "i'm sorry", "i can't",
            'please provide', 'could you', 'would you', 'you need to',
            'more information', 'clarify', 'specify', 'details about',
            'unfortunately', 'however', 'before i can', 'in order to',
            'help me understand', 'not enough details', 'provide enough',
        )
        
        # If raw text contains prose indicators, it's explanatory, not code
        if any(indicator in raw_lower for indicator in PROSE_INDICATORS):
            error_msg = (
                "LLM response did not contain recognizable code. "
                "The response appears to be an explanation or clarification request. "
                f"Response preview: {raw[:200]}..."
            )
            logger.error(error_msg)
            return {ERROR_FILENAME: error_msg}
        
        # Otherwise, treat as raw code (legacy behavior for compatibility)
        logger.warning(
            "No code markers found in response after cleaning. "
            "Falling back to using raw response as-is for compatibility. "
            "Preview: %s",
            raw[:150] + "..." if len(raw) > 150 else raw
        )
        log_action(
            "Parsed Single-File Response (Raw Fallback)",
            {"file": DEFAULT_FILENAME, "language": lang, "production_ready": False},
        )
        return {DEFAULT_FILENAME: raw}
    
    # Use validate_and_repair_syntax instead of _validate_syntax
    validation_result = validate_and_repair_syntax(cleaned_code, lang, DEFAULT_FILENAME)

    if validation_result['valid']:
        # Use repaired code if auto-repair was applied
        final_code = validation_result['code']
        
        # Perform production-ready validation for single file
        single_file_dict = {DEFAULT_FILENAME: final_code}
        is_production_ready, prod_error = validate_production_ready(single_file_dict)
        
        if not is_production_ready:
            logger.warning("Production-ready validation failed for single-file response")
            return {
                DEFAULT_FILENAME: final_code,
                ERROR_FILENAME: prod_error
            }
        
        log_action(
            "Parsed Single-File Response",
            {
                "file": DEFAULT_FILENAME, 
                "language": lang, 
                "production_ready": True,
                "auto_repaired": validation_result['auto_repaired'],
                "repairs_applied": validation_result['repairs_applied'] if validation_result['auto_repaired'] else None,
            },
        )
        return {DEFAULT_FILENAME: final_code}

    logger.error(
        "Syntax validation failed for single-file response (%s). Details: %s",
        lang,
        validation_result['error'],
    )
    error_content = (
        f"Generated code failed syntax validation for language '{lang}'.\n\n"
        f"Validation Tool Output:\n{validation_result['error']}"
    )
    return {ERROR_FILENAME: error_content}


