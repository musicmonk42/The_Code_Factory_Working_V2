def _detect_module_package_collisions(code_files: Dict[str, str]) -> Dict[str, str]:
    """
    Detect and resolve module/package name collisions in the generated file map.

    When the multi-pass ensemble generates both ``app/routes.py`` (a module) and
    ``app/routes/__init__.py`` (a package), Python cannot resolve both.  This
    function removes the bare module file and keeps the package directory tree,
    which is more complete.

    Args:
        code_files: Mapping of filename → content as returned by the parser.

    Returns:
        A new mapping with collisions resolved (module file removed).
    """
    cleaned: Dict[str, str] = dict(code_files)

    # Build a set of package roots: any path of the form "foo/__init__.py"
    # maps to a package root of "foo"
    package_roots: set = set()
    for path in list(cleaned.keys()):
        norm = path.replace("\\", "/")
        if norm.endswith("/__init__.py"):
            root = norm[: -len("/__init__.py")]
            package_roots.add(root)

    for root in package_roots:
        module_path = root + ".py"
        if module_path in cleaned:
            logger.warning(
                "Module/package collision detected: removing '%s' in favour of '%s/__init__.py'",
                module_path,
                root,
            )
            del cleaned[module_path]

    return cleaned


def _detect_name_shadowing(code_files: Dict[str, str]) -> List[str]:
    """
    Detect handler function names that shadow imported names, which would cause
    infinite recursion at runtime.

    For each Python file, uses ``ast.parse()`` to:
      1. Collect all imported names.
      2. Collect all function/class definition names.
      3. Flag any definition name that matches an imported name.

    Args:
        code_files: Mapping of filename → content.

    Returns:
        List of warning strings, one per shadowing occurrence.
    """
    import ast as _ast

    shadow_issues: List[str] = []

    for filename, content in code_files.items():
        if not filename.endswith(".py"):
            continue
        try:
            tree = _ast.parse(content, filename=filename)
        except SyntaxError:
            continue

        imported_names: set = set()
        for node in _ast.walk(tree):
            if isinstance(node, _ast.Import):
                for alias in node.names:
                    imported_names.add(alias.asname if alias.asname else alias.name)
            elif isinstance(node, _ast.ImportFrom):
                for alias in node.names:
                    imported_names.add(alias.asname if alias.asname else alias.name)

        for node in _ast.walk(tree):
            if isinstance(node, (_ast.FunctionDef, _ast.AsyncFunctionDef, _ast.ClassDef)):
                if node.name in imported_names:
                    shadow_issues.append(
                        f"{filename}: definition '{node.name}' shadows an imported name "
                        f"— this will cause infinite recursion or NameError at runtime"
                    )

    return shadow_issues


def _contains_code_markers(text: str) -> bool:
    """
    Check if text contains indicators that it's actual code rather than prose.
    
    This function helps distinguish between:
    - Actual code (imports, function definitions, class declarations, etc.)
    - Explanatory text from LLM (apologies, clarification requests, questions)
    
    Args:
        text: The text to analyze
        
    Returns:
        True if text appears to be code, False if it's likely prose/explanation
        
    Examples:
        >>> _contains_code_markers("import os\\ndef main(): pass")
        True
        >>> _contains_code_markers("I need more information about your requirements")
        False
        
    Note:
        This is a heuristic check that uses indicator counting. It's designed to
        catch cases where the LLM returns explanatory text instead of code.
    """
    if not text or len(text) < 5:
        return False
    
    # Code indicators (must have at least one)
    CODE_INDICATORS = (
        # Python-specific keywords and constructs
        'import ', 'from ', 'def ', 'class ', 'async def',
        'if __name__', 'return ', 'yield ', 'raise ',
        'for ', 'while ', 'try:', 'except', 'with ',
        'lambda ', 'assert ', 'pass', 'break', 'continue',
        # Function/method calls - common in Python
        'print(', 'len(', 'range(', 'str(', 'int(', 'open(',
        # Common patterns
        '= ', '==', '!=', '()', '[]', '{}',
        # Comments/docstrings
        '#', '"""', "'''",
        # Operators
        '+=', '-=', '*=', '/=', '**', '//',
        # Other languages
        'function ', 'const ', 'let ', 'var ', 'public ', 'private ',
        'package ', 'namespace ', 'using ', 'include ', '#include',
        # YAML / Kubernetes / Docker / Config markers
        'apiversion:', 'kind:', 'metadata:', 'spec:', 'replicas:',
        'services:', 'image:', 'ports:', 'volumes:', 'environment:',
        'name:', 'version:', 'description:', 'dependencies:',
        # Requirements.txt version specifiers
        '>=', '<=', '~=',
        # Helm/Jinja2 template markers
        '{{', '}}', '{%', '%}',
        # Dockerfile markers
        'FROM ', 'RUN ', 'COPY ', 'CMD ', 'ENTRYPOINT ', 'WORKDIR ', 'EXPOSE ',
    )
    
    # Prose indicators (if these dominate, it's likely not code)
    # Import shared Presidio placeholders
    try:
        from generator.runner.runner_security_utils import PRESIDIO_PLACEHOLDERS
        presidio_placeholders = PRESIDIO_PLACEHOLDERS
    except ImportError:
        # Fallback if import fails
        presidio_placeholders = ['<ORGANIZATION>', '<URL>', '<PERSON>', '<API_KEY>']
    
    PROSE_INDICATORS = (
        'I need', 'I apologize', 'I cannot', "I'm sorry", "I can't",
        'please provide', 'could you', 'would you', 'you need to',
        'more information', 'clarify', 'specify', 'details about',
        'unfortunately', 'however', 'therefore',
        'before I can', 'in order to', 'help me understand',
        'not enough details', 'provide enough', 'are placeholders',
    ) + tuple(presidio_placeholders)
    
    text_lower = text.lower()
    
    # Count indicators (using lowercase for consistent comparison)
    code_score = sum(1 for indicator in CODE_INDICATORS if indicator.lower() in text_lower)
    prose_score = sum(1 for indicator in PROSE_INDICATORS if indicator.lower() in text_lower)
    
    # If prose dominates, it's not code
    if prose_score > code_score:
        return False
    
    # Must have at least one code indicator
    return code_score > 0


def _clean_code_block(code_content: str) -> str:
    """
    Extracts clean code from LLM responses by removing markdown fences and conversational text.
    
    This function implements a multi-strategy approach to extract code from various LLM response formats:
    1. Language-specific markdown fences (```python, ```py)
    2. Generic markdown fences (```)
    3. Conversational preamble detection and removal
    
    When multiple code blocks are present, the largest block is returned as it's most likely
    to contain the complete implementation rather than a small example.
    
    Args:
        code_content: Raw LLM response text that may contain code mixed with explanatory text
        
    Returns:
        Cleaned code string with markdown fences and preamble removed
        
    Examples:
        >>> _clean_code_block("```python\\ndef hello():\\n    pass\\n```")
        'def hello():\\n    pass'
        
        >>> _clean_code_block("Here's the code:\\n\\nimport os\\n\\ndef main(): pass")
        'import os\\n\\ndef main(): pass'
    
    Note:
        This function is designed to be robust against various LLM response formats
        and will gracefully degrade to returning the input stripped if no patterns match.
    """
    if not code_content:
        logger.warning("_clean_code_block received empty input")
        return ""
    
    text = code_content.strip()
    
    # Strategy 1: Extract from language-specific markdown fences (```python, ```py, etc.)
    # Pattern matches: ```<language>\ncode\n```
    # Supports: python, py, javascript, js, typescript, ts, java, go, rust, cpp, c++, json, yaml, yml, markdown, md, etc.
    code_block_pattern = r'```(?:python|py|javascript|js|typescript|ts|java|go|rust|cpp|c\+\+|c#|csharp|ruby|php|swift|kotlin|json|yaml|yml|markdown|md|dockerfile|toml|xml|html|css|bash|sh|shell|sql)\s*\n(.*?)```'
    matches = re.findall(code_block_pattern, text, flags=re.DOTALL | re.IGNORECASE)
    
    if matches:
        # Return the largest code block (most likely to be the actual code)
        largest_match = max(matches, key=len).strip()
        logger.debug(
            "Extracted code from language-specific fence (length: %d chars)",
            len(largest_match)
        )
        return largest_match
    
    # Strategy 2: Extract from generic markdown fences (```)
    # This pattern matches ANY fence without a language specifier or with other languages
    generic_pattern = r'```\s*\n(.*?)```'
    matches = re.findall(generic_pattern, text, flags=re.DOTALL)
    if matches:
        largest_match = max(matches, key=len).strip()
        logger.debug(
            "Extracted code from generic fence (length: %d chars)",
            len(largest_match)
        )
        return largest_match
    
    # Strategy 2.5: Try single-line code fence format (```code```)
    inline_pattern = r'```(.*?)```'
    matches = re.findall(inline_pattern, text, flags=re.DOTALL)
    if matches:
        largest_match = max(matches, key=len).strip()
        # Only use if it looks like code (contains newlines or code indicators)
        if '\n' in largest_match or _contains_code_markers(largest_match):
            logger.debug(
                "Extracted code from inline fence (length: %d chars)",
                len(largest_match)
            )
            return largest_match
    
    # Strategy 3: No code fences found - strip conversational preamble
    # This handles cases where the LLM didn't use markdown but included explanatory text
    lines = text.split('\n')
    code_start_idx = 0
    
    # Conversational markers that indicate non-code preamble
    # These are checked at line start after stripping and must be followed by space or colon
    PREAMBLE_PATTERNS = (
        'here is', 'here\'s', 'this is', 'this will', 
        'the following', 'below is', 'below you',
        'i will', 'i\'ve', 'i have', 'sure', 'certainly',
        'of course', 'absolutely'
    )
    # Code markers that indicate the start of actual code
    CODE_MARKERS = ('import ', 'from ', 'def ', 'class ', '#', '"""', "'''", '@', 'if ', 'for ', 'while ',
                   'function ', 'const ', 'let ', 'var ', 'package ', 'public ', 'private ', 'namespace ')
    
    for i, line in enumerate(lines):
        stripped = line.strip().lower()
        
        # Skip empty lines
        if not stripped:
            continue
            
        # Skip lines that look like conversational preamble (phrase-based, more specific)
        if any(stripped.startswith(pattern) for pattern in PREAMBLE_PATTERNS):
            code_start_idx = i + 1
            continue
            
        # Stop at first line that looks like code
        if stripped.startswith(CODE_MARKERS):
            break
            
        # Check for assignment statements (but not in conversational context)
        if '=' in stripped and not any(stripped.startswith(pattern) for pattern in PREAMBLE_PATTERNS):
            break
    
    result = '\n'.join(lines[code_start_idx:]).strip()
    
    if code_start_idx > 0:
        logger.debug(
            "Stripped %d lines of preamble text (result length: %d chars)",
            code_start_idx,
            len(result)
        )
    
    # ADDED: Check if result contains any code markers
    # This prevents explanatory text from being passed to syntax validation
    if not _contains_code_markers(result):
        logger.warning(
            "No code markers found in LLM response after cleaning. "
            "Response may be explanatory text rather than code. "
            "Preview: %s",
            result[:150] + "..." if len(result) > 150 else result
        )
        # Return empty string to trigger proper error handling downstream
        return ""
    
    return result


