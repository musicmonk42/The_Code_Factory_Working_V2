class TestFileDiscovery:
    """Test recursive file discovery for testgen."""

    def test_rglob_finds_nested_python_files(self, tmp_path):
        """Test that rglob correctly finds Python files in nested directories."""
        # Arrange - Create nested structure
        output_dir = tmp_path / "generated"
        app_dir = output_dir / "app"
        routes_dir = app_dir / "routes"
        tests_dir = output_dir / "tests"

        for d in [app_dir, routes_dir, tests_dir]:
            d.mkdir(parents=True)

        # Create Python files
        (app_dir / "main.py").write_text("# main\n")
        (app_dir / "__init__.py").write_text("")
        (routes_dir / "api.py").write_text("# api\n")
        (routes_dir / "health.py").write_text("# health\n")
        (tests_dir / "test_main.py").write_text("# test\n")

        # Act - Simulate file discovery like omnicore_service.py:2874-2877
        source_files = [
            f for f in output_dir.rglob("*.py")
            if not f.name.startswith("test_")
        ]

        # Assert - Should find all non-test Python files
        assert len(source_files) == 4  # main.py, __init__.py, api.py, health.py
        file_names = {f.name for f in source_files}
        assert "main.py" in file_names
        assert "api.py" in file_names
        assert "health.py" in file_names
        assert "__init__.py" in file_names
        assert "test_main.py" not in file_names

    def test_glob_fails_to_find_nested_files(self, tmp_path):
        """Test that non-recursive glob would miss nested files (demonstrating the bug)."""
        # Arrange - Create nested structure
        output_dir = tmp_path / "generated"
        app_dir = output_dir / "app"
        app_dir.mkdir(parents=True)

        # Create files at different levels
        (output_dir / "main.py").write_text("# root main\n")
        (app_dir / "routes.py").write_text("# app routes\n")

        # Act - Use non-recursive glob (the old buggy behavior)
        root_files = list(output_dir.glob("*.py"))

        # Assert - glob only finds root level files
        assert len(root_files) == 1
        assert root_files[0].name == "main.py"
        # Demonstrates that glob misses app/routes.py


