class TestZIPExclusion:
    """Test that output ZIP files are excluded from packaging."""

    @pytest.mark.asyncio
    async def test_output_zip_excluded_from_artifact_collection(self, tmp_path):
        """Test that _output.zip files are excluded from artifact collection."""
        # Arrange - Create directory with files including an _output.zip
        output_dir = tmp_path / "generated"
        output_dir.mkdir()

        # Create regular files
        (output_dir / "main.py").write_text("# main\n")
        (output_dir / "requirements.txt").write_text("fastapi\n")

        # Create nested directory with more files
        app_dir = output_dir / "app"
        app_dir.mkdir()
        (app_dir / "routes.py").write_text("# routes\n")

        # Create an existing _output.zip (should be excluded)
        existing_zip = output_dir / "job123_output.zip"
        existing_zip.write_text("fake zip content\n")

        # Act - Collect artifacts (simulating omnicore_service.py:3036-3037)
        artifacts = list(output_dir.rglob('*'))
        artifact_files = [
            f for f in artifacts
            if f.is_file() and not f.name.endswith('_output.zip')
        ]

        # Assert - _output.zip should be excluded
        file_names = {f.name for f in artifact_files}
        assert "main.py" in file_names
        assert "requirements.txt" in file_names
        assert "routes.py" in file_names
        assert "job123_output.zip" not in file_names

        # Verify total count
        assert len(artifact_files) == 3  # main.py, requirements.txt, routes.py

    def test_download_endpoint_excludes_output_zips(self, tmp_path):
        """Test that download endpoint logic excludes _output.zip files."""
        # Arrange
        job_dir = tmp_path / "job456"
        job_dir.mkdir()

        # Create files
        (job_dir / "main.py").write_text("# main\n")
        (job_dir / "existing_output.zip").write_text("old zip\n")

        # Act - Simulate download logic (jobs.py:478-481)
        files_to_zip = []
        for file_path in job_dir.rglob('*'):
            if file_path.is_file() and not file_path.name.endswith('_output.zip'):
                files_to_zip.append(file_path)

        # Assert
        assert len(files_to_zip) == 1
        assert files_to_zip[0].name == "main.py"


